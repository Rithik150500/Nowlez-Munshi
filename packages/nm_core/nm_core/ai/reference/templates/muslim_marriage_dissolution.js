/**
 * PLAINT FOR DISSOLUTION OF MUSLIM MARRIAGE
 * UNDER SECTION 2 OF THE DISSOLUTION OF MUSLIM
 * MARRIAGES ACT, 1939
 * ─────────────────────────────────────────────
 * Category : Personal Law — Muslim Matrimonial
 * Court    : District Court / Family Court
 * Statute  : Section 2, Dissolution of Muslim Marriages Act, 1939
 * Source   : Standard form used in Indian Muslim
 *            matrimonial practice
 *
 * The Plaint for Dissolution of Muslim Marriage under the
 * Dissolution of Muslim Marriages Act, 1939 is the fourth
 * template in the family law expansion batch and it introduces
 * Muslim personal law in matrimonial matters. This template is
 * particularly important because Muslim personal law has a
 * fundamentally different relationship with the courts than
 * Hindu or Christian personal law, and the Dissolution of
 * Muslim Marriages Act is one of the few statutory provisions
 * that brings Muslim matrimonial disputes into the formal
 * court system. To grasp where this template fits, you have to
 * understand the basic structure of Muslim matrimonial law in
 * India and the historical reasons that led to the enactment of
 * the Dissolution of Muslim Marriages Act.
 *
 * THE STRUCTURE OF MUSLIM MATRIMONIAL LAW IN INDIA:
 *
 *   Muslim matrimonial law in India is largely based on
 *   uncodified personal law derived from the Quran, the
 *   Sunnah, and the various schools of Islamic jurisprudence
 *   (the four Sunni schools of Hanafi, Shafi'i, Maliki, and
 *   Hanbali, and the Shia schools of Ithna Ashari, Ismaili,
 *   and Zaydi). The most important feature of Muslim
 *   matrimonial law that distinguishes it from Hindu and
 *   Christian matrimonial law is its predominantly
 *   extra-judicial character. Marriage in Islamic law is
 *   regarded as a civil contract rather than a sacrament,
 *   and the dissolution of marriage is also regarded
 *   primarily as a private matter that can be effected by
 *   the parties themselves without the intervention of any
 *   court. This is fundamentally different from the position
 *   under Hindu law, where marriage is regarded as a
 *   sacrament that can only be dissolved by a court decree
 *   under the Hindu Marriage Act, 1955.
 *
 *   The principal modes of dissolution of marriage in
 *   traditional Islamic law are talaq (unilateral repudiation
 *   by the husband), khula (divorce at the instance of the
 *   wife with the husband's consent and on payment of
 *   compensation), mubaraat (divorce by mutual consent), and
 *   faskh (judicial dissolution by a qazi or Islamic judge
 *   on certain limited grounds). Of these, talaq has
 *   traditionally been the most common and the most
 *   controversial because it allowed the husband to
 *   repudiate the wife unilaterally without any judicial
 *   oversight or any specific grounds. The Muslim Women
 *   (Protection of Rights on Marriage) Act, 2019 has now
 *   prohibited the practice of triple talaq (talaq-e-biddat)
 *   and made it a criminal offence, but other forms of
 *   talaq remain available to Muslim husbands.
 *
 * THE PROBLEM THAT THE 1939 ACT WAS DESIGNED TO SOLVE:
 *
 *   The historical problem that gave rise to the Dissolution
 *   of Muslim Marriages Act, 1939 was the inadequate
 *   protection that traditional Islamic law provided to
 *   Muslim wives who wanted to dissolve their marriages.
 *   While Muslim husbands had the right of talaq, Muslim
 *   wives had only the limited remedies of khula (which
 *   required the husband's consent) and faskh (which was
 *   available only on a few narrow grounds and which had
 *   become difficult to obtain because of the absence of
 *   qazis in the Indian legal system after the colonial
 *   reorganisation of the courts). This left Muslim wives in
 *   a particularly vulnerable position because they could
 *   not unilaterally dissolve their marriages even when the
 *   husband had committed serious matrimonial wrongs.
 *
 *   The Dissolution of Muslim Marriages Act, 1939 was
 *   enacted to address this anomaly by giving Muslim wives a
 *   statutory right to seek dissolution of their marriages
 *   on a defined set of grounds through the formal court
 *   system. The Act is unusual in Indian personal law
 *   statutes in that it applies only to one gender — only
 *   Muslim wives, not Muslim husbands, can use the Act
 *   because Muslim husbands already have the right of talaq
 *   under personal law. The Act is therefore best understood
 *   as a remedial statute that supplements rather than
 *   replaces the personal law of Muslim matrimonial
 *   dissolution.
 *
 * THE NINE GROUNDS UNDER SECTION 2:
 *
 *   Section 2 of the Dissolution of Muslim Marriages Act,
 *   1939 sets out nine specific grounds on which a Muslim
 *   wife can seek dissolution of her marriage. The grounds,
 *   in plain language, are as follows. First, that the
 *   whereabouts of the husband have not been known for a
 *   period of four years. Second, that the husband has
 *   neglected or has failed to provide for the maintenance
 *   of the wife for a period of two years. Third, that the
 *   husband has been sentenced to imprisonment for a period
 *   of seven years or upwards. Fourth, that the husband has
 *   failed to perform, without reasonable cause, his marital
 *   obligations for a period of three years. Fifth, that the
 *   husband was impotent at the time of the marriage and
 *   continues to be so. Sixth, that the husband has been
 *   insane for a period of two years or is suffering from
 *   leprosy or a virulent venereal disease. Seventh, that
 *   the wife, having been given in marriage by her father
 *   or other guardian before she attained the age of fifteen
 *   years, repudiated the marriage before attaining the age
 *   of eighteen years (provided that the marriage has not
 *   been consummated). This is the so-called "option of
 *   puberty" which has its roots in classical Islamic law.
 *   Eighth, that the husband treats her with cruelty, which
 *   is defined in Section 2(viii) to include habitual
 *   assault, association with women of evil repute,
 *   compulsion to lead an immoral life, disposal of her
 *   property, obstruction of her religious observances,
 *   non-equitable treatment if there is more than one wife,
 *   and any other form of cruelty recognised by Muslim
 *   personal law. Ninth, that there is any other ground
 *   recognised as valid for the dissolution of marriages
 *   under Muslim law.
 *
 *   The most commonly invoked grounds in modern Indian
 *   practice are the failure to maintain (Section 2(ii))
 *   and cruelty (Section 2(viii)), which together cover the
 *   bulk of Muslim wife-initiated matrimonial petitions.
 *
 * STRUCTURAL FEATURES OF THIS TEMPLATE:
 *
 *   First, look at the form. The proceeding under the
 *   Dissolution of Muslim Marriages Act is technically a
 *   civil suit rather than a matrimonial petition (which is
 *   why the document is called a "plaint" rather than a
 *   "petition"). This is a distinctive feature of Muslim
 *   matrimonial proceedings under the 1939 Act and reflects
 *   the historical understanding of marriage in Islamic law
 *   as a civil contract.
 *
 *   Second, look at the parties. The plaintiff is always
 *   the Muslim wife, and the defendant is the husband. The
 *   gendered structure of the Act means that this template
 *   cannot be used by Muslim husbands, who must rely on
 *   personal law remedies such as talaq, khula, or
 *   mubaraat.
 *
 *   Third, look at the body. Paragraph 1 establishes the
 *   religious status of the parties, which is essential for
 *   jurisdiction under the Act. Paragraph 2 narrates the
 *   marriage and its terms, including the mehr (dower) which
 *   is a distinctive feature of Muslim marriage. Paragraphs
 *   3 onwards set out the specific grounds being relied
 *   upon under Section 2.
 *
 *   Fourth, look at the prayer. Unlike Hindu and Christian
 *   divorce decrees, the decree under the 1939 Act is for
 *   "dissolution of marriage" rather than for "divorce."
 *   This terminology reflects the conceptual difference
 *   between the Islamic and the Christian/Hindu approaches
 *   to matrimonial dissolution.
 */

