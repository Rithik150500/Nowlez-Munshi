/**
 * APPLICATION BY OPERATIONAL CREDITOR UNDER SECTION 9 OF THE
 * INSOLVENCY AND BANKRUPTCY CODE, 2016 (FORM 5)
 * ─────────────────────────────────────────────────────────────
 * Category : Insolvency Law — Initiation of CIRP
 * Tribunal : National Company Law Tribunal (NCLT)
 * Statute  : Section 9, Insolvency and Bankruptcy Code, 2016
 * Form     : Form 5 of the Insolvency and Bankruptcy
 *            (Application to Adjudicating Authority) Rules, 2016
 * Source   : Standard form prescribed under the IBC Rules
 *
 * The Application under Section 9 of the IBC by an operational
 * creditor is the second insolvency template in your library and
 * it is the natural follow-up to the Section 8 Demand Notice at
 * Template 61. To grasp the relationship between the two
 * documents, you have to think about the sequence of events that
 * leads to the initiation of corporate insolvency by an
 * operational creditor.
 *
 * THE PROCEDURAL SEQUENCE:
 *
 *   The procedural sequence for an operational creditor seeking
 *   to initiate insolvency proceedings against a corporate
 *   debtor involves three distinct stages. The first stage is
 *   the Section 8 Demand Notice, which the operational creditor
 *   must serve on the corporate debtor before any application
 *   can be filed. The second stage is the ten-day waiting
 *   period during which the corporate debtor has the
 *   opportunity to either pay the debt or to bring to the
 *   notice of the operational creditor the existence of any
 *   pre-existing dispute. The third stage, which arises only
 *   if the corporate debtor fails to do either, is the actual
 *   Section 9 application before the NCLT, which is what this
 *   template covers.
 *
 *   This three-stage sequence has important practical
 *   implications. The operational creditor cannot skip the
 *   demand notice stage and go directly to the NCLT, because
 *   Section 9(3)(b) of the Code requires the application to be
 *   accompanied by an affidavit confirming that the demand
 *   notice was duly served and that no notice of dispute was
 *   received from the corporate debtor. The NCLT will reject
 *   any application that is not accompanied by such an
 *   affidavit, regardless of the merits of the underlying
 *   debt. So the demand notice is not just a courtesy but a
 *   jurisdictional precondition.
 *
 * THE NCLT JURISDICTION:
 *
 *   The Section 9 application is filed before the National
 *   Company Law Tribunal, which is the adjudicating authority
 *   for all corporate insolvency matters under the IBC. The
 *   NCLT was established under the Companies Act, 2013, and
 *   its jurisdiction was significantly expanded by the IBC in
 *   2016 to include the entire universe of corporate
 *   insolvency proceedings. The NCLT operates through
 *   benches located in major cities across India, with the
 *   bench having jurisdiction over the matter being the one in
 *   whose territorial jurisdiction the registered office of
 *   the corporate debtor is located. So an application
 *   against a corporate debtor whose registered office is in
 *   New Delhi would be filed before the New Delhi Bench of
 *   the NCLT, while an application against a corporate debtor
 *   in Mumbai would be filed before the Mumbai Bench.
 *
 *   This is the first template in your library that goes
 *   before the NCLT rather than before a regular court. The
 *   case caption uses the standard NCLT format which is
 *   different from the formats you have seen in the High
 *   Court and District Court templates. The application is
 *   addressed to "the Hon'ble National Company Law Tribunal"
 *   and is filed under the case category "Company Petition"
 *   with the abbreviation "CP (IB)" indicating that it is a
 *   company petition under the IBC.
 *
 * THE SUBSTANTIVE THRESHOLD AND THE MOBILOX TEST:
 *
 *   Two substantive issues determine whether a Section 9
 *   application will succeed. The first is the THRESHOLD
 *   AMOUNT. Until March 2020, the minimum amount of debt
 *   required to file an IBC application was Rs. 1 lakh. In
 *   March 2020, in response to the economic disruption
 *   caused by the COVID-19 pandemic, the Government raised
 *   the threshold to Rs. 1 crore. This dramatic increase has
 *   significantly limited the number of operational creditors
 *   who can use the IBC, because most small and medium
 *   suppliers have outstanding amounts well below the new
 *   threshold. Operational creditors with smaller debts must
 *   now use ordinary civil litigation or DRT proceedings
 *   instead.
 *
 *   The second substantive issue is whether the debt is
 *   DISPUTED. Under Section 9(5)(d) of the Code, the NCLT
 *   must reject the application if the corporate debtor has
 *   raised a dispute regarding the debt before the receipt of
 *   the demand notice. The leading case on what constitutes a
 *   dispute is Mobilox Innovations Pvt. Ltd. v. Kirusa
 *   Software Pvt. Ltd. (2018), in which the Supreme Court laid
 *   down what is known as the "Mobilox test." The Court held
 *   that the dispute does not have to be a strong dispute or
 *   even a likely-to-succeed dispute. It is enough if the
 *   dispute is a "plausible contention which requires further
 *   investigation" and is not "patently feeble" or
 *   "hopelessly weak." This is a low threshold that favours
 *   corporate debtors and is designed to ensure that the IBC
 *   is not used as a debt recovery mechanism for disputed
 *   debts. The IBC's role, in the words of the Supreme Court,
 *   is to deal with corporate insolvency, not to enforce
 *   contractual claims that are subject to genuine disputes.
 *
 * THE CONSEQUENCES OF ADMISSION:
 *
 *   You should also understand what happens once the NCLT
 *   admits a Section 9 application. The admission triggers the
 *   commencement of the Corporate Insolvency Resolution
 *   Process (CIRP), which is a time-bound process designed to
 *   either rescue the corporate debtor as a going concern or
 *   to liquidate it in an orderly manner. The CIRP has
 *   several drastic consequences for the corporate debtor.
 *   First, a moratorium under Section 14 of the Code comes
 *   into force, which prohibits any other proceedings against
 *   the corporate debtor and freezes all enforcement actions.
 *   Second, the existing management of the corporate debtor
 *   is suspended and the company is taken over by an Interim
 *   Resolution Professional appointed by the NCLT. Third, the
 *   creditors of the corporate debtor are invited to file
 *   their claims with the IRP and to participate in the
 *   resolution process through a Committee of Creditors. The
 *   process is intended to be completed within 270 days,
 *   although extensions are routinely granted in practice.
 *
 *   Because of these drastic consequences, the threat of a
 *   Section 9 application is often more important than the
 *   application itself. Many corporate debtors who are
 *   reluctant to pay an undisputed operational debt will hurry
 *   to settle once they receive a Section 8 demand notice,
 *   precisely because they fear the consequences of admission
 *   of a Section 9 application. This makes the Section 8
 *   notice and the Section 9 application together an
 *   extraordinarily powerful tool for collecting genuinely
 *   undisputed operational debts above the Rs. 1 crore
 *   threshold.
 */

