/**
 * SHAREHOLDERS' AGREEMENT
 * ──────────────────────────
 * Category : Commercial Drafting — Corporate Governance
 * Type     : Multi-party shareholder relationship agreement
 * Statute  : Companies Act, 2013; Indian Contract Act, 1872;
 *            SEBI Regulations (for listed companies)
 * Source   : Standard form used in Indian corporate practice
 *
 * The Shareholders' Agreement, often abbreviated as SHA or
 * sometimes SSHA (when combined with a Share Subscription
 * Agreement), is the fourth commercial drafting template in
 * your library and it introduces an entirely new dimension of
 * commercial practice that you have not encountered before,
 * namely the law of corporate governance. To grasp where the
 * SHA fits in commercial practice, you have to understand the
 * gap that it fills in the regulatory framework that governs
 * Indian companies.
 *
 * THE GAP THAT THE SHA FILLS:
 *
 *   Every Indian company is governed primarily by two legal
 *   documents: the Memorandum of Association, which sets out
 *   the company's name, its registered office, its objects,
 *   and the limits of its authority; and the Articles of
 *   Association, which set out the internal rules governing
 *   the management of the company, including the powers and
 *   duties of directors, the conduct of board meetings and
 *   shareholder meetings, the rights attached to different
 *   classes of shares, and the procedures for the transfer of
 *   shares. The Memorandum and Articles together are often
 *   called the "constitutional documents" of the company.
 *
 *   The Memorandum and Articles are public documents that are
 *   filed with the Registrar of Companies and are accessible
 *   to anyone who cares to look at them. They are also limited
 *   in scope by the Companies Act, which prescribes certain
 *   minimum requirements that they must contain and certain
 *   matters that they cannot deal with. As a result, the
 *   constitutional documents are typically generic and do not
 *   address many of the specific arrangements that the
 *   shareholders of a particular company may wish to put in
 *   place between themselves.
 *
 *   The Shareholders' Agreement fills this gap. It is a
 *   private contract between some or all of the shareholders
 *   of the company that supplements the constitutional
 *   documents by setting out the specific arrangements that
 *   the parties have agreed between themselves. Unlike the
 *   constitutional documents, the SHA is not a public document
 *   and does not have to be filed with anyone. It is also not
 *   subject to the same statutory restrictions, so it can deal
 *   with matters that the Articles cannot.
 *
 * THE TWO MAIN TYPES OF SHA:
 *
 *   Shareholders' Agreements come in two main varieties that
 *   you should be able to recognise. The first is the
 *   FOUNDERS' AGREEMENT, which is signed between the
 *   co-founders of a company at or shortly after its
 *   incorporation. A Founders' Agreement typically deals with
 *   the equity split between the founders, the vesting of
 *   founder shares over time, the roles and responsibilities
 *   of each founder, the procedures for resolving disputes
 *   between founders, and the consequences if a founder leaves
 *   the company before the vesting period is complete.
 *
 *   The second is the INVESTOR AGREEMENT, which is signed
 *   between the company, its existing shareholders, and a new
 *   investor (typically a venture capital fund, private equity
 *   fund, or strategic investor) when the investor acquires
 *   shares in the company through a primary issuance or a
 *   secondary purchase. An Investor Agreement typically deals
 *   with the rights and protections of the investor, including
 *   board representation, information rights, anti-dilution
 *   protection, exit rights, and reserved matters that require
 *   the investor's consent.
 *
 *   This template is drafted as a generic SHA that incorporates
 *   elements of both varieties and can be adapted to either
 *   context. The key clauses that you should learn to recognise
 *   are common to both types of SHA.
 *
 * THE KEY CLAUSES IN A SHA:
 *
 *   First is the BOARD COMPOSITION clause, which specifies how
 *   many directors each shareholder (or class of shareholders)
 *   is entitled to nominate to the board, and how the chairman
 *   of the board is to be selected. This clause is critical
 *   because it determines who actually controls the
 *   day-to-day decisions of the company.
 *
 *   Second is the RESERVED MATTERS clause, which lists the
 *   specific matters that cannot be approved by the board or
 *   the shareholders without the affirmative consent of certain
 *   designated shareholders (typically the investors or the
 *   minority shareholders). Reserved matters typically include
 *   significant transactions like the issuance of new shares,
 *   the borrowing of money above a threshold, the acquisition
 *   or disposal of assets, the entry into related-party
 *   transactions, and the amendment of the constitutional
 *   documents. The reserved matters clause is the principal
 *   mechanism by which minority shareholders protect themselves
 *   from being overruled by the majority on matters of
 *   fundamental importance.
 *
 *   Third are the TRANSFER RESTRICTIONS, which limit the
 *   ability of shareholders to sell their shares to outsiders.
 *   The most common transfer restriction is the RIGHT OF FIRST
 *   REFUSAL (ROFR), which gives the existing shareholders the
 *   right to purchase any shares that another shareholder
 *   wishes to sell, on the same terms that the seller has
 *   been offered by a third party. A related concept is the
 *   RIGHT OF FIRST OFFER (ROFO), which requires the seller
 *   to offer the shares to the existing shareholders first
 *   before approaching any third party.
 *
 *   Fourth is the TAG-ALONG clause, which protects minority
 *   shareholders by giving them the right to participate in
 *   any sale of shares by the majority shareholder. If the
 *   majority shareholder finds a buyer for its shares, the
 *   minority shareholder can "tag along" and require the
 *   buyer to purchase its shares as well, on the same terms.
 *   This prevents the majority from selling out and leaving
 *   the minority stuck with a new majority shareholder of
 *   unknown character.
 *
 *   Fifth is the DRAG-ALONG clause, which is the mirror image
 *   of the tag-along clause. It protects the majority by
 *   giving them the right to compel the minority to sell
 *   their shares if the majority finds a buyer for the entire
 *   company. Without a drag-along clause, a single
 *   recalcitrant minority shareholder could block a sale of
 *   the entire company by refusing to sell their shares.
 *
 *   Sixth is the ANTI-DILUTION clause, which protects
 *   investors against the issuance of new shares at a price
 *   lower than the price they paid. The anti-dilution
 *   protection typically takes the form of either "full
 *   ratchet" protection (the investor's shares are repriced
 *   to the new lower price) or "weighted average" protection
 *   (the investor's shares are repriced to a weighted average
 *   of the original price and the new lower price).
 *
 *   Seventh and finally is the EXIT clause, which sets out
 *   the procedures for an eventual exit from the investment.
 *   The exit may take the form of an IPO, a sale to a
 *   strategic acquirer, a sale to another financial investor,
 *   or a buy-back by the company itself. The exit clause
 *   typically includes timelines within which the parties
 *   will work towards an exit.
 *
 * THE ENFORCEABILITY ISSUE:
 *
 *   One of the most important practical issues with
 *   Shareholders' Agreements in India is whether the
 *   provisions of the SHA are enforceable as a matter of
 *   company law. The Supreme Court has held in cases like
 *   V.B. Rangaraj versus V.B. Gopalakrishnan that any
 *   provision in a SHA that is inconsistent with the
 *   Companies Act or with the Articles of Association of
 *   the company is unenforceable to that extent. The
 *   solution to this problem is to ensure that the
 *   substantive rights granted to shareholders under the SHA
 *   are also incorporated into the Articles of Association
 *   of the company. In practice, well-drafted Shareholders'
 *   Agreements always require the Articles to be amended
 *   simultaneously with or shortly after the execution of
 *   the SHA, so that the rights are enforceable both as
 *   contractual rights between the shareholders and as
 *   constitutional rights of the company.
 */

