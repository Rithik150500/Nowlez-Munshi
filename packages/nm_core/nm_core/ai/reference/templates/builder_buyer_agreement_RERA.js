/**
 * BUILDER-BUYER AGREEMENT (APARTMENT BUYER AGREEMENT)
 * UNDER SECTION 13 OF THE REAL ESTATE (REGULATION
 * AND DEVELOPMENT) ACT, 2016
 * ─────────────────────────────────────────────────────
 * Category : Real Estate — Commercial Transaction
 * Type     : Bilateral promoter-allottee agreement
 * Statute  : Section 13, Real Estate (Regulation and
 *            Development) Act, 2016; Indian Contract Act, 1872;
 *            Transfer of Property Act, 1882
 * Source   : Standard form developed in Indian real estate
 *            practice based on RERA model agreements
 *
 * The Builder-Buyer Agreement under Section 13 of the RERA Act
 * is the second template in the real estate batch and it
 * introduces the modern apartment sale agreement that has
 * become the standard transactional document for the purchase
 * of an apartment in any organised real estate project. To
 * grasp where this template fits, you should compare it with
 * the conveyancing templates already in your library at
 * Template 4 (Agreement for Sale) and Template 19 (Sale Deed).
 *
 * THE RELATIONSHIP TO THE EARLIER CONVEYANCING TEMPLATES:
 *
 *   The Agreement for Sale at Template 4 is the traditional
 *   pre-RERA document by which a buyer and seller agree to
 *   the sale of immovable property, with the actual transfer
 *   of title taking place later through a registered Sale
 *   Deed. The Agreement for Sale at Template 4 is generally
 *   used for the sale of plots, independent houses, or
 *   resale apartments where the seller already owns the
 *   property and the transaction is essentially a transfer
 *   of existing rights. The Builder-Buyer Agreement at
 *   Template 87, by contrast, is used for the primary sale
 *   of an apartment in a real estate project being developed
 *   by a builder. The transaction has a fundamentally
 *   different character because at the time the agreement
 *   is executed, the apartment may not yet exist (the
 *   project may still be under construction or even at the
 *   planning stage), and the buyer is therefore committing
 *   to purchase a future property based on the developer's
 *   promises about how the project will be completed.
 *
 *   This forward-looking character of the Builder-Buyer
 *   Agreement creates several distinctive legal issues that
 *   the traditional Agreement for Sale does not address. The
 *   buyer needs protection against the risk that the
 *   developer will not complete the project, will not deliver
 *   the apartment in accordance with the agreed
 *   specifications, or will not transfer clear title at the
 *   end of the construction period. The traditional Agreement
 *   for Sale was inadequate to provide these protections
 *   because it had been drafted for a different kind of
 *   transaction, and the absence of suitable contractual
 *   protections was one of the principal causes of the
 *   widespread exploitation of apartment buyers in India
 *   that led to the enactment of the RERA Act in 2016.
 *
 * THE RERA FRAMEWORK FOR BUILDER-BUYER AGREEMENTS:
 *
 *   The RERA Act addresses the inadequacies of the older
 *   form of the Builder-Buyer Agreement through several
 *   important provisions. Section 13(1) provides that a
 *   promoter shall not accept a sum more than ten percent
 *   of the cost of the apartment as an advance payment or
 *   an application fee from a person without first entering
 *   into a written agreement for sale with that person and
 *   registering the said agreement for sale under any law
 *   for the time being in force. This is a fundamental
 *   change from the pre-RERA practice in which developers
 *   routinely collected fifty percent or more of the
 *   consideration before executing any written agreement at
 *   all, leaving buyers in a vulnerable position with no
 *   contractual protection.
 *
 *   Section 13(2) further provides that the agreement for
 *   sale shall specify the particulars of development of
 *   the project, the construction of the building and
 *   apartment along with specifications and internal
 *   development works and external development works, the
 *   dates and the manner by which payments towards the cost
 *   of the apartment are to be made by the allottees, the
 *   date on which the possession of the apartment is to be
 *   handed over, the rates of interest payable by the
 *   promoter to the allottee and by the allottee to the
 *   promoter in case of default of payment, and such other
 *   particulars as may be prescribed.
 *
 *   The various State Governments have prescribed model
 *   forms of the agreement for sale under their respective
 *   State RERA Rules, and most large developers now use
 *   forms based on the model. The agreement is required to
 *   be registered under the Registration Act, 1908, and
 *   stamp duty is payable on the agreement at the rates
 *   prescribed by the State concerned.
 *
 * KEY COMMERCIAL PROVISIONS:
 *
 *   The most important commercial provisions in any
 *   Builder-Buyer Agreement are the description of the
 *   apartment, the total consideration, the payment
 *   schedule, the date of possession, the consequences of
 *   delay, the common areas and facilities, the maintenance
 *   arrangements, the formation of the apartment owners'
 *   association, and the dispute resolution mechanism.
 *   Each of these provisions has been the subject of
 *   substantial litigation under the RERA framework, and a
 *   well-drafted Builder-Buyer Agreement should address each
 *   of them clearly and unambiguously.
 *
 *   The description of the apartment must include the unit
 *   number, the floor, the carpet area (which is the
 *   area enclosed within the walls of the apartment,
 *   excluding common areas), the built-up area (which
 *   includes the carpet area plus the wall thickness), and
 *   the super built-up area (which includes the proportionate
 *   share of common areas). The carpet area is the most
 *   important measurement under RERA because Section 4(2)(h)
 *   requires the developer to disclose the carpet area
 *   separately and prohibits the developer from selling
 *   apartments on the basis of super built-up area alone.
 *
 *   The payment schedule must be linked to the construction
 *   milestones, with each instalment becoming payable only
 *   when a specified stage of construction has been reached.
 *   This is intended to prevent the practice of demanding
 *   the entire consideration upfront and then using the
 *   funds for purposes other than the construction.
 *
 *   The date of possession is the most critical provision
 *   in the agreement because it triggers the developer's
 *   liability under Section 18 of the RERA Act if the
 *   possession is not delivered by the agreed date. The
 *   agreement typically provides for a grace period of
 *   three to six months, beyond which the developer becomes
 *   liable to pay interest on the amounts received.
 *
 * STRUCTURAL FEATURES OF THIS TEMPLATE:
 *
 *   The Builder-Buyer Agreement follows the basic structural
 *   conventions of commercial drafting that you have seen in
 *   the earlier commercial templates at 56 to 60 and 74,
 *   namely the date and place of execution, the parties
 *   clause introduced by "BY AND BETWEEN," the recitals
 *   beginning with "WHEREAS," the operative provisions
 *   organised into numbered clauses, the testimonium, and
 *   the signature block. The distinctive features of the
 *   Builder-Buyer Agreement are the detailed description of
 *   the apartment in Schedule A, the payment schedule linked
 *   to construction milestones in Clause 4, the specific
 *   date of possession in Clause 7, and the RERA-specific
 *   provisions on disclosures and the apartment owners'
 *   association.
 */

