/**
 * APPLICATION FOR CONDONATION OF DELAY
 * UNDER SECTION 5 OF THE LIMITATION ACT, 1963
 * ──────────────────────────────────────────────
 * Category : Civil Procedure — Procedural Application
 * Court    : The Court hearing the underlying matter
 * Statute  : Section 5, Limitation Act, 1963
 * Source   : Standard form used in Indian civil practice
 *
 * The Application for Condonation of Delay is one of the most
 * universally used procedural devices in Indian civil practice.
 * To understand why it deserves a place in your library, consider
 * how strict Indian limitation law is. The Limitation Act, 1963
 * prescribes specific time periods for almost every kind of legal
 * action: thirty days for some appeals, ninety days for others,
 * three years for most ordinary civil suits, twelve years for
 * suits relating to immovable property, and so on. These periods
 * are not mere guidelines. Section 3 of the Limitation Act says
 * categorically that every suit, appeal or application instituted
 * after the prescribed period of limitation shall be DISMISSED,
 * regardless of whether the opposite party raises limitation as
 * a defence. The court is required to apply limitation suo motu.
 * This means that if you file your appeal even one day late, the
 * appellate court must dismiss it as time-barred unless you can
 * persuade it to overlook the delay.
 *
 * Section 5 of the Limitation Act is the safety valve in this
 * otherwise rigid system. It provides that any appeal or any
 * application other than an application under any of the
 * provisions of Order XXI of the Code of Civil Procedure may be
 * admitted after the prescribed period if the appellant or the
 * applicant satisfies the court that he had "sufficient cause"
 * for not preferring the appeal or making the application within
 * such period. Notice the precise scope of Section 5. It applies
 * to appeals and applications, but it does NOT apply to original
 * suits. If you file a fresh suit after the limitation period
 * has expired, Section 5 cannot save you, and the suit must be
 * dismissed. Section 5 also explicitly excludes execution
 * applications under Order XXI, which have their own special
 * limitation regime.
 *
 * THE "SUFFICIENT CAUSE" DOCTRINE:
 *
 *   The phrase "sufficient cause" is the most heavily litigated
 *   expression in the entire Limitation Act. The Indian courts
 *   have generated thousands of judgments on what does and does
 *   not amount to sufficient cause, and the doctrine has evolved
 *   in a recognisable direction over the decades. The general
 *   approach today can be summarised in a few principles.
 *
 *   First, the courts have repeatedly held that the expression
 *   "sufficient cause" should be construed LIBERALLY so as to
 *   advance substantial justice. The leading case on this is
 *   Collector, Land Acquisition, Anantnag versus Mst. Katiji
 *   (1987), in which the Supreme Court said that when the
 *   substance of the matter requires hearing, the courts should
 *   adopt a liberal approach to procedural requirements like
 *   limitation. The Court observed that "the doctrine of equality
 *   before law demands that all litigants, including the State as
 *   a litigant, are accorded the same treatment and the law is
 *   administered in an even-handed manner."
 *
 *   Second, the burden is on the applicant to EXPLAIN EVERY
 *   DAY of the delay. This is sometimes called the "day-to-day
 *   explanation" rule. It does not mean that the applicant has
 *   to file separate explanations for every single day, but it
 *   does mean that the cumulative explanation must account for
 *   the entire period of delay. Vague statements like "the
 *   appellant was busy with other work" or "the file got
 *   misplaced" will not normally be sufficient if they leave
 *   significant chunks of time unexplained.
 *
 *   Third, the explanation must demonstrate that the delay was
 *   NOT DELIBERATE OR CONTUMACIOUS. The applicant must show
 *   that there was no negligence, no inaction, and no want of
 *   bona fides. Even if the explanation is somewhat weak, the
 *   court will usually condone the delay if it is satisfied
 *   that the applicant was not deliberately seeking to harass
 *   the other side or to gain some unfair advantage.
 *
 *   Fourth, the courts have generally held that a SHORT DELAY
 *   should be condoned more easily than a long delay. A delay
 *   of a few days is usually condoned almost as a matter of
 *   course if any reasonable explanation is offered, while a
 *   delay of several months requires a much more substantial
 *   showing.
 *
 *   Fifth, the courts have repeatedly emphasised that REFUSING
 *   TO CONDONE DELAY is to be seen as a serious matter because
 *   it forecloses the substantive remedy entirely. Once the
 *   delay is not condoned, the case is dismissed on a procedural
 *   ground without any consideration of the merits, and the
 *   applicant has no further remedy. The courts therefore lean
 *   in favour of condonation when there is any reasonable
 *   ground to do so.
 *
 *   Sixth, the courts treat the GOVERNMENT differently from
 *   private litigants. Government bodies generally have to
 *   meet a higher standard because they are presumed to have
 *   adequate resources to comply with limitation periods. The
 *   Supreme Court has in some cases criticised the government
 *   for routinely seeking condonation of long delays.
 *
 * THE DUAL FILING REQUIREMENT:
 *
 *   An application for condonation of delay is never filed in
 *   isolation. It is always filed ALONGSIDE the substantive
 *   document that is being filed late — typically an appeal,
 *   a revision, or some other interlocutory application. The
 *   court hears the condonation application first and only
 *   considers the substantive document if the delay is
 *   condoned. If the delay is not condoned, the substantive
 *   document is dismissed automatically. This means that the
 *   condonation application is procedurally critical: without
 *   it being allowed, the substantive matter never gets heard
 *   at all.
 *
 *   Notice how this template is structured to be filed
 *   alongside an appeal. The case header refers to the appeal
 *   number, and the body explains the delay in filing the
 *   appeal. If you wanted to use this template for a different
 *   substantive document, such as a revision petition or a
 *   review application, you would need to modify the references
 *   accordingly.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at how the body opens. Paragraph 1 identifies
 *   the underlying matter (the appeal in this case) and admits
 *   the delay. Paragraph 2 quantifies the delay precisely in
 *   days. This precision is important because the court will
 *   evaluate the explanation against the specific length of
 *   the delay.
 *
 *   Second, look at how the explanation is set out in paragraph
 *   3. The explanation is broken down chronologically into
 *   distinct phases of the delay, each accounted for separately.
 *   This is the conventional way of presenting a day-to-day
 *   explanation.
 *
 *   Third, look at the "sufficient cause" assertion in paragraph
 *   4 and the bona fides paragraph at paragraph 5. These two
 *   paragraphs invoke the legal standard explicitly and tell the
 *   court what conclusions to draw from the explanation given.
 *
 *   Fourth, look at the prayer. It asks for two distinct things:
 *   condonation of the delay, and admission of the appeal. Both
 *   are necessary because condonation alone would not produce
 *   any procedural effect — the appeal still has to be admitted
 *   and registered before it can proceed.
 */

const {
  Document, Paragraph, TextRun,
  AlignmentType, TabStopType, TabStopPosition,
  Footer, PageNumber, LevelFormat
} = require("docx");

function legalPara(children, opts = {}) {
  return new Paragraph({
    spacing: { after: 120, line: 360 },
    alignment: AlignmentType.JUSTIFIED, ...opts, children,
  });
}
function centeredBold(text, size = 24) {
  return new Paragraph({
    alignment: AlignmentType.CENTER, spacing: { after: 60 },
    children: [new TextRun({ text, bold: true, size, font: "Times New Roman" })],
  });
}
const spacer = new Paragraph({ spacing: { after: 120 }, children: [] });

