/**
 * APPLICATION FOR REVIEW OF JUDGMENT UNDER ORDER XLVII
 * RULE 1 OF THE CODE OF CIVIL PROCEDURE, 1908
 * ──────────────────────────────────────────────────────
 * Category : Civil Procedure — Post-judgment Reconsideration
 * Court    : The same court that passed the impugned judgment
 * Statute  : Section 114 read with Order XLVII Rule 1, Code of
 *            Civil Procedure, 1908
 * Source   : Standard form used in Indian civil practice
 *
 * The Application for Review of Judgment under Order XLVII is one
 * of the most conceptually distinctive remedies in Indian civil
 * procedure because it asks a court to reconsider a decision it
 * has already made. To grasp how unusual this is, you have to
 * think about how the judicial system normally treats finality.
 * Once a court delivers a judgment, that judgment is meant to
 * resolve the dispute between the parties and to bring an end to
 * the litigation. The losing party may appeal to a higher court,
 * but the original court is generally functus officio with
 * respect to the matter, meaning that it has discharged its
 * function and no longer has any further jurisdiction over the
 * case. This finality principle is essential to the orderly
 * working of the judicial system because without it, every
 * judgment would be subject to reopening and the courts would
 * never be free of any case.
 *
 * Review under Order XLVII is a narrow exception to this
 * finality principle. It allows the same court that passed the
 * judgment to reconsider that judgment in a few specific
 * circumstances. The exception is justified by the recognition
 * that even the most careful court can sometimes make mistakes
 * that should be corrected at source rather than forcing the
 * losing party to undergo the expense and delay of an appeal.
 *
 * REVIEW vs APPEAL — A FUNDAMENTAL DISTINCTION:
 *
 *   You should never confuse review with appeal because they
 *   serve completely different purposes and operate on
 *   completely different principles. An appeal goes to a
 *   higher court and asks that higher court to substitute its
 *   own judgment for that of the lower court. The appellate
 *   court can re-examine the evidence, reconsider the legal
 *   issues, and reach a different conclusion on the merits if
 *   it disagrees with the trial court. The Memorandum of
 *   Appeal at Template 42 in your library reflects this
 *   appellate function.
 *
 *   A review, by contrast, goes to the SAME court that passed
 *   the original judgment and asks that court to correct an
 *   error in its own decision. Crucially, review is NOT an
 *   opportunity to reargue the case. The court conducting a
 *   review will not re-examine the evidence, will not
 *   reconsider the legal arguments that were made at the
 *   original hearing, and will not substitute its current view
 *   for the view it took earlier. The Supreme Court has
 *   repeatedly emphasised that review is a very limited remedy
 *   and that an unsuccessful litigant cannot use review as a
 *   substitute for appeal.
 *
 * THE THREE GROUNDS FOR REVIEW:
 *
 *   Order XLVII Rule 1 of the CPC sets out three specific
 *   grounds on which review may be sought, and these grounds
 *   are exhaustive. A review application that does not fall
 *   within one of these three grounds must be dismissed
 *   regardless of how meritorious it may otherwise appear.
 *
 *   The first ground is the DISCOVERY of new and important
 *   matter or evidence which, after the exercise of due
 *   diligence, was not within the knowledge of the applicant
 *   or could not be produced by the applicant at the time when
 *   the decree was passed. Notice the carefully worded
 *   limitations on this ground. The new matter must be
 *   IMPORTANT, not merely additional. It must be something
 *   that could not have been discovered through DUE DILIGENCE.
 *   And it must have been UNAVAILABLE at the time of the
 *   original hearing, not merely overlooked. These limitations
 *   are designed to prevent parties from holding back evidence
 *   for tactical reasons and then trying to introduce it
 *   through review.
 *
 *   The second ground is a MISTAKE OR ERROR APPARENT ON THE
 *   FACE OF THE RECORD. This is the most heavily litigated
 *   ground and the one that requires the most careful
 *   analysis. The Supreme Court has repeatedly held that an
 *   error is "apparent on the face of the record" only if it
 *   can be detected by a mere inspection of the record without
 *   any elaborate argument. An error that requires reasoning,
 *   analysis, or reference to outside material is NOT an
 *   error apparent on the face of the record, even if the
 *   court that passed the original judgment was demonstrably
 *   wrong. The test is essentially whether the error is
 *   immediately obvious to anyone who reads the judgment.
 *   This deliberately narrow test is what separates review
 *   from appeal: an appeal can correct any error, however
 *   subtle, but a review can only correct errors that are
 *   patent on the face of the judgment itself.
 *
 *   The third ground is "any other sufficient reason." This
 *   sounds open-ended, but the Supreme Court has interpreted
 *   it very narrowly. The Court has held that "any other
 *   sufficient reason" must be a reason analogous to the
 *   first two grounds, not a fresh general ground. So
 *   procedural irregularities that go to the root of the
 *   matter, fundamental violations of natural justice, and
 *   similar matters may qualify, but mere disagreement with
 *   the conclusions reached in the judgment will not.
 *
 * THE IDENTITY OF THE REVIEWING JUDGE:
 *
 *   Order XLVII Rule 5 contains an interesting procedural
 *   requirement that is worth knowing. It says that no
 *   application for review shall be heard by a different
 *   judge than the one who passed the original decree, except
 *   in cases where the original judge is no longer attached
 *   to the court (for example, because of retirement,
 *   transfer, or elevation to a higher court). This rule
 *   reflects the principle that the judge who made the
 *   original decision is in the best position to recognise
 *   their own errors and to correct them, and it also
 *   prevents litigants from forum-shopping for a more
 *   sympathetic judge through the review process.
 *
 * THE LIMITATION PERIOD:
 *
 *   Under Article 124 of the Limitation Act, an application
 *   for review of a decree or order must be filed within
 *   thirty days from the date of the decree or order. This
 *   strict limitation period reflects the urgency with which
 *   review applications must be processed if they are to
 *   serve their corrective function. Section 5 of the
 *   Limitation Act (the condonation of delay provision at
 *   Template 48) does apply to review applications, so a
 *   party can seek condonation if there is sufficient cause
 *   for delay.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at the case header. It uses "Review Petition"
 *   as the case identifier, which is the standard caption for
 *   review applications in most Indian courts. The petition
 *   is filed in the same court that passed the impugned
 *   judgment.
 *
 *   Second, look at the parties. The labels are interesting.
 *   The petitioner is identified by both the original role
 *   in the suit (plaintiff or defendant) and the current role
 *   in the review (petitioner). This dual labelling is the
 *   same pattern you have seen in other post-judgment
 *   templates like the Memorandum of Appeal at Template 42
 *   and the Execution Application at Template 36.
 *
 *   Third, look at the body. Paragraph 1 identifies the
 *   underlying suit and judgment. Paragraph 2 quotes or
 *   summarises the relevant findings from the impugned
 *   judgment. Paragraph 3 identifies the specific error or
 *   the new matter on which review is sought. Paragraph 4
 *   ties the application to one of the three statutory
 *   grounds. Paragraph 5 confirms compliance with the
 *   limitation period.
 *
 *   Fourth, look at the prayer. It asks for the impugned
 *   judgment to be reviewed and the corrected judgment to be
 *   passed in place of it. The court has the option of
 *   either modifying the existing judgment or passing a
 *   completely fresh judgment after review.
 */

