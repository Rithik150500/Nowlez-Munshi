/**
 * DEMAND NOTICE UNDER SECTION 8 OF THE INSOLVENCY AND
 * BANKRUPTCY CODE, 2016 (FORM 3)
 * ──────────────────────────────────────────────────────
 * Category : Insolvency Law — Pre-litigation Notice
 * Statute  : Section 8, Insolvency and Bankruptcy Code, 2016
 * Form     : Form 3 of the Insolvency and Bankruptcy
 *            (Application to Adjudicating Authority) Rules, 2016
 * Source   : Standard form prescribed under the IBC Rules
 *
 * The Demand Notice under Section 8 of the IBC is the first
 * insolvency template in your library and it introduces an
 * entirely new statutory regime that none of the existing
 * templates touch. To grasp where this notice fits in the
 * broader picture, you have to understand the distinctive
 * structure of Indian insolvency law as it has developed since
 * the enactment of the Insolvency and Bankruptcy Code in 2016.
 *
 * THE IBC REVOLUTION:
 *
 *   Before 2016, debt recovery and insolvency in India were
 *   governed by a patchwork of overlapping statutes that had
 *   accumulated over the decades, including the Sick Industrial
 *   Companies Act of 1985, the Recovery of Debts Due to Banks
 *   and Financial Institutions Act of 1993, the Securitisation
 *   and Reconstruction of Financial Assets and Enforcement of
 *   Security Interest Act of 2002, and the winding up
 *   provisions of the Companies Act. Each of these statutes
 *   addressed a different aspect of insolvency and they often
 *   produced inconsistent and overlapping results. The system
 *   was notoriously slow, with corporate insolvency
 *   proceedings often taking five to ten years to resolve.
 *
 *   The Insolvency and Bankruptcy Code, 2016 was enacted to
 *   replace this fragmented regime with a unified, time-bound
 *   process for the resolution of corporate insolvency. The
 *   Code created a new institutional framework consisting of
 *   the Insolvency and Bankruptcy Board of India (IBBI) as
 *   the regulator, the National Company Law Tribunal (NCLT)
 *   as the adjudicating authority for corporate matters, the
 *   Debt Recovery Tribunal (DRT) as the adjudicating
 *   authority for individual matters, and licensed Insolvency
 *   Professionals (IPs) who actually conduct the resolution
 *   process. The Code prescribed strict timelines that aim to
 *   complete the entire corporate insolvency resolution
 *   process within 270 days, although in practice this
 *   timeline is rarely met.
 *
 * THE TWO TYPES OF CREDITORS:
 *
 *   The IBC makes a fundamental distinction between two
 *   categories of creditors that you need to understand
 *   precisely. A FINANCIAL CREDITOR is a person to whom a
 *   financial debt is owed, where financial debt means a debt
 *   along with interest that is disbursed against the
 *   consideration for the time value of money. The classic
 *   examples of financial creditors are banks that have
 *   lent money, debenture holders, and other lenders. An
 *   OPERATIONAL CREDITOR, by contrast, is a person to whom an
 *   operational debt is owed, where operational debt means a
 *   claim in respect of the provision of goods or services,
 *   employment, or a debt in respect of the payment of dues
 *   arising under any law to a government authority. The
 *   classic examples of operational creditors are suppliers
 *   of goods and services, employees with salary arrears, and
 *   the tax authorities.
 *
 *   This distinction matters because the IBC treats financial
 *   and operational creditors very differently. Financial
 *   creditors can file an insolvency application directly under
 *   Section 7 of the Code without any precondition. Operational
 *   creditors, by contrast, must first send a demand notice
 *   under Section 8 of the Code, wait for a response from the
 *   corporate debtor, and only then file an application under
 *   Section 9 if the debt remains unpaid and undisputed. This
 *   asymmetry reflects the policy judgment that financial
 *   creditors are typically better placed to assess the credit
 *   risk before lending money, while operational creditors are
 *   often unable to refuse credit as a condition of doing
 *   business.
 *
 * THE FUNCTION OF THE SECTION 8 DEMAND NOTICE:
 *
 *   Section 8 of the IBC requires an operational creditor who
 *   wishes to initiate insolvency proceedings against a
 *   corporate debtor to first deliver a demand notice or a
 *   copy of an invoice demanding payment of the unpaid
 *   operational debt. The corporate debtor then has TEN DAYS
 *   from the receipt of the notice to either (a) bring to the
 *   notice of the operational creditor the existence of a
 *   pre-existing dispute regarding the debt, or (b) make
 *   payment of the unpaid debt. If the corporate debtor does
 *   neither of these things within the ten-day period, the
 *   operational creditor becomes entitled to file an
 *   application under Section 9 of the Code.
 *
 *   The ten-day window is critical because it gives the
 *   corporate debtor a meaningful opportunity to either pay
 *   the debt or to demonstrate that the debt is genuinely
 *   disputed. If the corporate debtor brings to the notice of
 *   the operational creditor the existence of a pre-existing
 *   dispute, the Section 9 application becomes liable to be
 *   rejected by the NCLT under Section 9(5)(d) of the Code.
 *   This is because the Supreme Court has held in Mobilox
 *   Innovations Pvt. Ltd. v. Kirusa Software Pvt. Ltd. (2018)
 *   that the IBC is not a debt recovery mechanism and cannot
 *   be used to enforce disputed debts. The NCLT's role at the
 *   admission stage is limited to verifying that there is an
 *   undisputed operational debt that has not been paid, not
 *   to adjudicate on the merits of any underlying dispute.
 *
 * THE STATUTORY FORM:
 *
 *   Unlike most of the templates in your library, which use
 *   conventional drafting language that has evolved through
 *   practice, the Section 8 Demand Notice must be in a
 *   PRESCRIBED FORM. Rule 5 of the Insolvency and Bankruptcy
 *   (Application to Adjudicating Authority) Rules, 2016
 *   specifies that the demand notice shall be in Form 3 (or
 *   in some cases Form 4 if the notice is served along with
 *   an invoice). The prescribed form contains specific
 *   blanks and requirements that must be completed in a
 *   particular way, and the NCLT will reject demand notices
 *   that do not substantially comply with the prescribed
 *   form.
 *
 *   This is why Template 61 is laid out in a way that closely
 *   tracks the structure of Form 3. The template follows the
 *   numbered paragraphs of the form precisely, with each
 *   paragraph containing the specific information required by
 *   the statutory form. This is a rigid format compared to
 *   the more flexible drafting of the other notices in your
 *   library, but the rigidity is necessary because the form
 *   itself is prescribed by statute.
 *
 * COMPARISON WITH OTHER PRE-LITIGATION NOTICES:
 *
 *   Your library now has four pre-litigation notices: the
 *   Section 138 NI Act Notice at Template 14, the Section 80
 *   CPC Notice at Template 34, the Section 106 TPA Notice at
 *   Template 38, and now the Section 8 IBC Demand Notice at
 *   Template 61. They share the basic function of being
 *   statutory preconditions to litigation, but they differ in
 *   important ways. The Section 138 notice gives 30 days for
 *   payment. The Section 80 notice gives 60 days for the
 *   government to respond. The Section 106 notice gives 15
 *   days for vacation. The Section 8 IBC notice gives only
 *   10 days for response, which is the shortest of all four
 *   periods because the IBC is designed to be fast-tracked.
 *   The Section 8 notice also differs from the others in
 *   that it must be in a prescribed statutory form, while the
 *   others can be drafted in any reasonable form provided
 *   they substantially comply with the relevant statutory
 *   requirements.
 */

const {
  Document, Paragraph, TextRun,
  AlignmentType, TabStopType, TabStopPosition,
  Footer, PageNumber, LevelFormat, BorderStyle
} = require("docx");

function legalPara(children, opts = {}) {
  return new Paragraph({
    spacing: { after: 120, line: 360 },
    alignment: AlignmentType.JUSTIFIED, ...opts, children,
  });
}
function centeredBold(text, size = 24) {
  return new Paragraph({
    alignment: AlignmentType.CENTER, spacing: { after: 60 },
    children: [new TextRun({ text, bold: true, size, font: "Times New Roman" })],
  });
}
const spacer = new Paragraph({ spacing: { after: 120 }, children: [] });
function hrule() {
  return new Paragraph({
    spacing: { after: 200 },
    border: { bottom: { style: BorderStyle.SINGLE, size: 6, color: "333333", space: 1 } },
    children: [],
  });
}

