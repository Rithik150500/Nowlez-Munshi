/**
 * APPLICATION TO SET ASIDE ARBITRAL AWARD UNDER
 * SECTION 34 OF THE ARBITRATION AND CONCILIATION ACT, 1996
 * ──────────────────────────────────────────────────────────
 * Category : Alternative Dispute Resolution — Challenge to Award
 * Court    : Principal Civil Court of original jurisdiction in a district,
 *            or High Court (for international commercial arbitration)
 * Statute  : Section 34, Arbitration and Conciliation Act, 1996
 * Source   : Standard form used in Indian arbitration practice
 *
 * The Application to Set Aside an Arbitral Award under Section 34
 * is the third and final member of the arbitration trio in your
 * library, completing the picture that began with Templates 49
 * (appointment of arbitrator under Section 11) and 51 (interim
 * measures under Section 9). To grasp where Section 34 fits, you
 * have to understand the entire life cycle of an arbitration
 * proceeding as Indian law conceives it.
 *
 * THE LIFE CYCLE OF AN ARBITRATION:
 *
 *   An arbitration begins when a party invokes the arbitration
 *   clause in a contract by sending a notice to the other party.
 *   If the parties cannot agree on an arbitrator, one of them
 *   files a Section 11 application in the High Court to have an
 *   arbitrator appointed (Template 49). Once the tribunal is
 *   constituted, the arbitration proceeds with statements of
 *   claim and defence, examination of evidence, and oral
 *   arguments. Throughout this process, either party can
 *   approach the court under Section 9 for interim protective
 *   measures if necessary (Template 51). Eventually, the
 *   tribunal delivers its award, which is the equivalent of a
 *   court judgment in arbitration.
 *
 *   At this point, the losing party has a single, narrow
 *   opportunity to challenge the award before a regular court,
 *   and that opportunity is Section 34. If the losing party does
 *   not file a Section 34 application within the prescribed
 *   period, or if the application is dismissed on its merits,
 *   the award becomes final and can be enforced as a decree of
 *   the court under Section 36.
 *
 * THE NARROWNESS OF SECTION 34:
 *
 *   The most important thing to understand about Section 34 is
 *   how NARROW its scope is. Indian arbitration policy strongly
 *   favours the finality of arbitral awards, on the theory that
 *   parties who chose arbitration should have to live with the
 *   results of arbitration rather than re-litigating the merits
 *   in court. Section 34 therefore does NOT permit a court to
 *   re-examine the evidence, to substitute its own view of the
 *   facts for that of the tribunal, or to correct errors of law
 *   that the tribunal may have made. The grounds for setting
 *   aside an award are exhaustively listed in the section, and
 *   the court has no power to go beyond them.
 *
 *   The grounds under Section 34(2) fall into two categories.
 *   The first category is procedural and includes the following:
 *   that a party was under some incapacity, that the arbitration
 *   agreement was not valid, that the party was not given proper
 *   notice of the appointment of the arbitrator or of the
 *   arbitral proceedings or was otherwise unable to present its
 *   case, that the award deals with a dispute not contemplated
 *   by or not falling within the terms of the submission to
 *   arbitration, and that the composition of the tribunal or
 *   the arbitral procedure was not in accordance with the
 *   agreement of the parties or with the Act. The second
 *   category, set out in Section 34(2)(b), allows the court to
 *   set aside an award if it finds that the subject matter of
 *   the dispute is not capable of settlement by arbitration
 *   under the law in force, or that the award is in conflict
 *   with the PUBLIC POLICY OF INDIA.
 *
 * THE PUBLIC POLICY GROUND AND ITS NARROWING:
 *
 *   The public policy ground has been the most heavily litigated
 *   provision in the entire Act, and the doctrine has narrowed
 *   significantly over the years. In ONGC versus Saw Pipes
 *   (2003), the Supreme Court interpreted public policy
 *   broadly to include "patent illegality" of any kind,
 *   effectively allowing courts to review arbitral awards for
 *   ordinary errors of law. This expansive interpretation
 *   undermined the finality of arbitration in India and led to
 *   widespread criticism from the international arbitration
 *   community.
 *
 *   In response, the 2015 amendment to the Act significantly
 *   narrowed the public policy ground. The amended Section 34
 *   now specifies that an award can be set aside on public
 *   policy grounds only if it is induced or affected by fraud
 *   or corruption, if it is in contravention with the
 *   fundamental policy of Indian law, or if it is in conflict
 *   with the most basic notions of morality or justice. The
 *   amendment further added an Explanation 1 stating that the
 *   review of an award on the fundamental policy ground does
 *   NOT entail a review on the merits of the dispute. A
 *   separate ground of "patent illegality" was retained for
 *   purely domestic awards (not international commercial
 *   arbitration), but the amendment limited even this ground
 *   by specifying that an award shall not be set aside merely
 *   on the ground of an erroneous application of the law or
 *   by re-appreciation of evidence.
 *
 * THE THREE-MONTH LIMITATION:
 *
 *   Section 34(3) prescribes an unusually strict limitation
 *   period for setting aside applications. The application
 *   must be filed within three months from the date on which
 *   the party making the application receives the signed copy
 *   of the award. The court has the discretion to extend this
 *   period by a further thirty days on satisfaction of
 *   sufficient cause, but NOT THEREAFTER. This is one of the
 *   strictest limitation periods in Indian law, and the
 *   Supreme Court has repeatedly held that an application
 *   filed even one day after the maximum period of three
 *   months and thirty days must be rejected as time-barred,
 *   regardless of the reason for the delay. Section 5 of the
 *   Limitation Act (the general condonation of delay
 *   provision at Template 48) does NOT apply to Section 34
 *   applications because Section 34(3) contains its own
 *   self-contained limitation regime.
 *
 *   This strict limitation reflects the policy of finality
 *   that animates the entire Act. The legislature wanted to
 *   ensure that arbitral awards either stand or fall quickly,
 *   without years of post-award litigation hanging over them.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at the case caption. It uses "OMP (COMM)" for
 *   Original Miscellaneous Petition in Commercial matters, which
 *   is the standard caption used by the Delhi High Court for
 *   Section 34 applications relating to commercial arbitrations.
 *
 *   Second, look at the body of the petition. Paragraph 1
 *   identifies the underlying dispute and arbitration. Paragraph
 *   2 narrates the course of the arbitration. Paragraph 3 sets
 *   out the date of the impugned award. Paragraph 4 confirms
 *   that the application is being filed within the limitation
 *   period. The grounds then attack the award on the specific
 *   grounds permitted by Section 34.
 *
 *   Third, look at how the grounds are drafted. Each ground
 *   tracks one of the specific clauses of Section 34(2) and
 *   identifies a particular vice in the award. The petitioner
 *   cannot rely on general dissatisfaction with the result —
 *   each ground must be tied to a specific statutory category.
 */

