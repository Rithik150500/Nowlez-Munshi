/**
 * COMPLAINT OF SEXUAL HARASSMENT AT THE WORKPLACE
 * UNDER SECTION 9 OF THE SEXUAL HARASSMENT OF WOMEN
 * AT WORKPLACE (PREVENTION, PROHIBITION AND
 * REDRESSAL) ACT, 2013
 * ─────────────────────────────────────────────────────
 * Category : Labour and Employment Law — Workplace
 *            Compliance / Sexual Harassment
 * Forum    : Internal Complaints Committee (IC)
 * Statute  : Section 9, Sexual Harassment of Women at
 *            Workplace (Prevention, Prohibition and
 *            Redressal) Act, 2013 ("PoSH Act")
 * Source   : Standard form used in Indian PoSH practice
 *
 * The Complaint of Sexual Harassment at the Workplace under the
 * PoSH Act, 2013 is the third template in the labour law batch
 * and it introduces a quite different kind of employment-related
 * proceeding from the Statement of Claim and Section 33C(2)
 * Application that you have just seen. The PoSH complaint is
 * not a litigation document filed before a court or a tribunal
 * in the ordinary sense. Instead, it is a workplace grievance
 * filed before the Internal Complaints Committee of the
 * employer's establishment, and it operates under a specialised
 * statutory framework that combines elements of employment law,
 * gender justice, and constitutional protection.
 *
 * THE BACKGROUND TO THE PoSH ACT:
 *
 *   The PoSH Act, 2013 has its origins in the landmark
 *   judgment of the Supreme Court of India in Vishaka versus
 *   State of Rajasthan (1997), in which the Court was called
 *   upon to address the brutal gang rape of a social worker
 *   who had been campaigning against child marriage in
 *   Rajasthan. The Court held that the existing legal
 *   framework was inadequate to deal with the scourge of
 *   sexual harassment at the workplace, and it laid down a
 *   set of binding guidelines (the "Vishaka Guidelines")
 *   that were to be followed by all employers until
 *   appropriate legislation was enacted by Parliament. The
 *   Vishaka Guidelines required employers to take preventive
 *   steps against sexual harassment, to constitute an
 *   internal complaints mechanism, and to take appropriate
 *   action against perpetrators.
 *
 *   The Vishaka Guidelines remained in force for sixteen
 *   years until they were finally codified into statute by
 *   the PoSH Act, 2013. The Act largely follows the Vishaka
 *   framework but goes further in several important
 *   respects, including by extending the protection beyond
 *   employees in the formal sense to include all women
 *   working in any workplace (including students, interns,
 *   trainees, and even visitors), by mandating the
 *   constitution of an Internal Complaints Committee in
 *   every workplace with ten or more employees, by
 *   providing for a Local Complaints Committee at the
 *   district level for the unorganised sector, and by
 *   prescribing detailed procedures for the inquiry and the
 *   action to be taken on a complaint.
 *
 * THE DEFINITION OF SEXUAL HARASSMENT:
 *
 *   The most important provision of the PoSH Act for the
 *   purposes of drafting a complaint is the definition of
 *   "sexual harassment" in Section 2(n), which sets out the
 *   types of unwelcome conduct that constitute sexual
 *   harassment under the Act. The definition includes any
 *   one or more of the following unwelcome acts or behaviour
 *   (whether directly or by implication): physical contact
 *   and advances; a demand or request for sexual favours;
 *   making sexually coloured remarks; showing pornography;
 *   and any other unwelcome physical, verbal, or non-verbal
 *   conduct of a sexual nature.
 *
 *   In addition, Section 3(2) of the Act lists certain
 *   circumstances in which the conduct may amount to sexual
 *   harassment even if the conduct itself does not fall
 *   within the strict definition of Section 2(n), including
 *   implied or explicit promise of preferential treatment,
 *   implied or explicit threat of detrimental treatment,
 *   implied or explicit threat about her present or future
 *   employment status, interference with her work or
 *   creating an intimidating or offensive or hostile work
 *   environment for her, and humiliating treatment likely to
 *   affect her health or safety.
 *
 * THE INTERNAL COMPLAINTS COMMITTEE:
 *
 *   The principal forum for the resolution of sexual
 *   harassment complaints under the Act is the Internal
 *   Complaints Committee (IC), which every employer with
 *   ten or more employees is required to constitute under
 *   Section 4 of the Act. The IC must be chaired by a
 *   senior woman employee of the establishment, and must
 *   include at least one external member from a non-
 *   governmental organisation or association committed to
 *   women's rights. The IC has the powers of a civil court
 *   under the Code of Civil Procedure, 1908 in respect of
 *   summoning witnesses, requiring the production of
 *   documents, and any other matter prescribed.
 *
 *   The IC is required to conduct an inquiry into the
 *   complaint within a period of ninety days from the date
 *   of receipt of the complaint, and to submit a report of
 *   its findings to the employer within ten days of the
 *   completion of the inquiry. If the IC finds that the
 *   complaint is substantiated, it can recommend a range of
 *   actions to the employer including a written apology, a
 *   warning, censure, withholding of promotion or
 *   increments, suspension, or termination of service. If
 *   the IC finds that the complaint is malicious or false,
 *   it can recommend action against the complainant, but
 *   this power is to be exercised sparingly and the mere
 *   inability to substantiate the complaint is not by
 *   itself sufficient to warrant action against the
 *   complainant.
 *
 * THE TIME LIMIT FOR FILING:
 *
 *   Section 9(1) of the Act provides that a complaint of
 *   sexual harassment must be filed within a period of
 *   three months from the date of the incident, and in the
 *   case of a series of incidents, within a period of three
 *   months from the date of the last incident. Section 9(1)
 *   further provides that the IC may, for reasons to be
 *   recorded in writing, extend the time limit by a further
 *   period of three months if the IC is satisfied that the
 *   circumstances were such as to prevent the woman from
 *   filing the complaint within the said period.
 *
 *   This relatively short time limit reflects the policy
 *   judgment that sexual harassment complaints should be
 *   addressed promptly while the evidence is fresh and the
 *   memory of the witnesses is reliable, but it has
 *   sometimes been criticised as being too restrictive in
 *   cases where the complainant takes time to summon the
 *   courage to come forward.
 *
 * STRUCTURAL FEATURES OF THIS TEMPLATE:
 *
 *   The PoSH complaint has a more informal structure than
 *   the litigation documents in your library because it is
 *   not filed before a court but before a workplace
 *   committee. There is no court header or cause title in
 *   the traditional sense, and the document is more in the
 *   nature of a letter or a representation than a pleading.
 *   The body sets out the personal details of the
 *   complainant, the relationship to the workplace, the
 *   identity of the respondent, the specific incidents of
 *   harassment, the impact on the complainant, and the
 *   relief sought.
 *
 *   The template uses placeholders for the most sensitive
 *   details rather than detailed narrative because the
 *   actual complaint must be drafted with great care to
 *   ensure that it is accurate, that it does not
 *   unintentionally identify protected witnesses or
 *   sensitive information, and that it is consistent with
 *   the documentary evidence. Confidentiality is critical in
 *   PoSH proceedings under Section 16 of the Act, which
 *   prohibits the publication or making known of the
 *   contents of a complaint or of the proceedings of the IC.
 */

