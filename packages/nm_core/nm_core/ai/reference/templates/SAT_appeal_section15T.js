/**
 * APPEAL TO THE SECURITIES APPELLATE TRIBUNAL (SAT)
 * UNDER SECTION 15T OF THE SECURITIES AND EXCHANGE
 * BOARD OF INDIA ACT, 1992
 * ─────────────────────────────────────────────────────
 * Category : Securities Law — Appellate Tribunal
 * Forum    : Securities Appellate Tribunal (SAT)
 * Statute  : Section 15T, SEBI Act, 1992;
 *            Section 22B, Securities Contracts (Regulation) Act, 1956;
 *            Section 23A, Depositories Act, 1996
 * Source   : Standard form used in Indian securities practice
 *
 * The Appeal to the Securities Appellate Tribunal under Section
 * 15T of the SEBI Act, 1992 is the third template in the
 * specialised tribunals batch and it introduces the world of
 * securities regulation in India. To grasp where this template
 * fits, you have to understand the regulatory structure of the
 * Indian securities market and the role of the Securities
 * Appellate Tribunal within that structure.
 *
 * THE STRUCTURE OF SECURITIES REGULATION IN INDIA:
 *
 *   The Indian securities market is regulated by the Securities
 *   and Exchange Board of India (SEBI), which was established
 *   under the SEBI Act, 1992 in the wake of several major
 *   securities scandals in the late 1980s and early 1990s.
 *   SEBI is a statutory regulator with wide powers over the
 *   securities market, including the power to grant or refuse
 *   registration to market intermediaries (such as stock
 *   brokers, sub-brokers, merchant bankers, depository
 *   participants, mutual funds, and credit rating agencies),
 *   the power to investigate violations of the securities
 *   laws, the power to impose monetary penalties under
 *   Section 15A to 15HA of the SEBI Act, the power to issue
 *   directions and to debar persons from accessing the
 *   securities market, and the power to settle proceedings
 *   through consent orders.
 *
 *   SEBI exercises these powers through its quasi-judicial
 *   jurisdiction, which means that it acts both as the
 *   prosecutor and as the adjudicator in cases involving
 *   alleged violations of the securities laws. This dual role
 *   has been controversial, but it is constitutionally
 *   permitted because of the doctrine of "necessary
 *   implication" under which a regulator can exercise
 *   adjudicatory powers in respect of matters within its
 *   regulatory remit.
 *
 *   To provide a check on SEBI's quasi-judicial powers, the
 *   SEBI Act provides for an independent appellate forum in
 *   the form of the Securities Appellate Tribunal. The SAT
 *   was established under Section 15K of the SEBI Act and
 *   has jurisdiction to hear appeals against orders of SEBI
 *   under Section 15T. The SAT is composed of a Presiding
 *   Officer who must be a sitting or retired Supreme Court
 *   judge or a Chief Justice of a High Court, and two other
 *   members who are typically experts in securities law,
 *   economics, finance, or accountancy.
 *
 * THE EXPANDED JURISDICTION OF SAT:
 *
 *   Although the SAT was originally established to hear
 *   appeals against SEBI orders, its jurisdiction has been
 *   progressively expanded over the years to cover appeals
 *   from other financial sector regulators as well. Under
 *   Section 22B of the Securities Contracts (Regulation)
 *   Act, 1956, the SAT hears appeals against orders of the
 *   Recognised Stock Exchanges. Under Section 23A of the
 *   Depositories Act, 1996, the SAT hears appeals against
 *   orders relating to depositories. Under Section 15Z of
 *   the Insurance Regulatory and Development Authority Act,
 *   1999, the SAT hears appeals against orders of the IRDAI.
 *   And under Section 36 of the Pension Fund Regulatory and
 *   Development Authority Act, 2013, the SAT hears appeals
 *   against orders of the PFRDA.
 *
 *   This expanded jurisdiction makes the SAT the unified
 *   appellate forum for the entire financial services
 *   regulatory landscape in India, with the exception of
 *   banking matters which still go to the appropriate
 *   forum under the Banking Regulation Act, 1949.
 *
 * THE THREE-TIER APPELLATE STRUCTURE IN SECURITIES MATTERS:
 *
 *   Securities matters in India follow a three-tier
 *   appellate structure that you should understand. The
 *   first tier is SEBI itself, which acts as the
 *   adjudicator at first instance through its various
 *   adjudicating officers, whole-time members, and
 *   committees. The second tier is the SAT, which hears
 *   appeals against SEBI orders under Section 15T of the
 *   SEBI Act. The third tier is the Supreme Court, which
 *   hears appeals against SAT orders under Section 15Z of
 *   the SEBI Act, but only on questions of law.
 *
 *   This compressed three-tier structure is similar to the
 *   NCLT-NCLAT-Supreme Court structure that you saw at
 *   Template 77, with the difference that in securities
 *   matters the High Courts are completely bypassed. This
 *   reflects the policy of expert specialisation in the
 *   adjudication of securities disputes, on the theory that
 *   the SAT and the Supreme Court are better placed than
 *   the High Courts to handle the technical aspects of
 *   securities regulation.
 *
 * THE LIMITATION PERIOD:
 *
 *   Section 15T(3) of the SEBI Act prescribes a limitation
 *   period of forty-five days from the date on which a copy
 *   of the SEBI order is received by the appellant. The SAT
 *   has discretion under Section 15T(3) to extend this
 *   period by a further forty-five days on sufficient
 *   cause, but not beyond. This is similar to the
 *   limitation periods you have seen for other appellate
 *   tribunals, and it reflects the policy of speedy
 *   disposal that is common to all specialised tribunals.
 *
 * THE GROUNDS FOR APPEAL:
 *
 *   Appeals to the SAT can be filed on questions of fact
 *   as well as questions of law, and the SAT has the same
 *   broad appellate powers as the NCLAT. The SAT can
 *   re-examine the evidence, reconsider the legal issues,
 *   and reach a different conclusion on the merits. In
 *   practice, SAT appeals are typically based on grounds
 *   that the SEBI order is contrary to law, that the
 *   SEBI order is not supported by the evidence on record,
 *   that the penalty imposed is excessive or
 *   disproportionate, that there has been a violation of
 *   the principles of natural justice, or that SEBI has
 *   acted beyond its jurisdiction.
 *
 *   The SAT has been particularly active in scrutinising
 *   the proportionality of penalties imposed by SEBI, and
 *   it has frequently reduced or set aside penalties that
 *   it considers excessive in relation to the gravity of
 *   the offence. This proportionality review is one of the
 *   most important contributions of the SAT to securities
 *   jurisprudence in India.
 *
 * STRUCTURAL FEATURES OF THIS TEMPLATE:
 *
 *   First, look at the case header. The appeal is filed
 *   before the SAT at Mumbai, which is the principal seat
 *   of the tribunal. The case is captioned simply as
 *   "Appeal No. ________" because there is no specific
 *   case category nomenclature in SAT practice.
 *
 *   Second, look at the parties. The appellant is the
 *   person aggrieved by the SEBI order, and the respondent
 *   is SEBI itself. Unlike the NCLAT appeals where the
 *   contest is with the opposing private party, SAT
 *   appeals are typically direct challenges to the
 *   regulator.
 *
 *   Third, look at the body. The structure follows the
 *   standard appellate format with paragraphs identifying
 *   the parties, the impugned order, the limitation, the
 *   grounds of appeal, and the relief sought.
 */

