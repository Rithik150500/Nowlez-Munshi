/**
 * WRIT PETITION (CRIMINAL) FOR WRIT OF HABEAS CORPUS
 * UNDER ARTICLE 226 OF THE CONSTITUTION OF INDIA
 * ────────────────────────────────────────────────────
 * Category : Constitutional Law — Personal Liberty
 * Court    : High Court of Delhi (or Supreme Court under Article 32)
 * Statute  : Article 226 (or Article 32) of the Constitution of India
 * Source   : Standard form used in Indian constitutional practice
 *
 * The Petition for Writ of Habeas Corpus is the most ancient and
 * the most revered constitutional remedy in any common-law system.
 * Its history goes back many centuries to the English common law,
 * where the writ originally functioned as a judicial command
 * requiring the body of a person to be brought before the court so
 * that the legality of their detention could be examined. The Latin
 * phrase "habeas corpus" literally means "you shall have the body,"
 * referring to the requirement that the detaining authority produce
 * the detained person physically before the judge. The writ
 * survived British rule, was preserved in independent India through
 * Articles 32 and 226 of the Constitution, and remains today the
 * single most powerful judicial remedy for protecting personal
 * liberty against unlawful detention.
 *
 * THE CONSTITUTIONAL BASIS:
 *
 *   The High Court has jurisdiction to issue writs of habeas
 *   corpus under Article 226 of the Constitution, and the
 *   Supreme Court has the same jurisdiction under Article 32. The
 *   two articles are largely parallel, but with one important
 *   difference. Article 32 is itself a fundamental right under
 *   Part III of the Constitution, which means that the Supreme
 *   Court is bound to entertain petitions filed under it and
 *   cannot refuse them on grounds of mere convenience. Article
 *   226, by contrast, is a power conferred on the High Court
 *   rather than a right of the petitioner, but in practice the
 *   distinction is rarely material because High Courts entertain
 *   habeas petitions readily as a matter of constitutional duty.
 *
 *   The choice between filing in the High Court and the Supreme
 *   Court usually depends on practical factors. Most habeas
 *   petitions are filed in the High Court of the state where the
 *   detention is taking place, because the High Court has
 *   territorial jurisdiction over the relevant state authorities
 *   and can issue immediate orders. The Supreme Court is
 *   generally approached only in matters of national importance
 *   or where the High Court has refused relief.
 *
 * THE UNIQUE FEATURES OF HABEAS CORPUS:
 *
 *   Habeas corpus differs from every other writ in your library
 *   in several distinctive ways, and it is worth understanding
 *   each one carefully.
 *
 *   First, the petition can be filed by ANY PERSON who is
 *   interested in the liberty of the detained person, not just
 *   the detained person themselves. This is fundamental to the
 *   utility of the remedy because the detained person is, by
 *   definition, in custody and unable to file court papers
 *   personally. So a wife may file for her detained husband, a
 *   parent may file for a detained child, a friend may file for
 *   a detained friend, or in extreme cases even a complete
 *   stranger may file as a public-spirited intervener. This is
 *   why the habeas petition is structured with TWO main figures:
 *   the PETITIONER who actually files the document, and the
 *   DETENU who is the person whose liberty is at stake.
 *
 *   Second, habeas corpus is a SUMMARY remedy. The Supreme Court
 *   and High Courts have the power to dispose of habeas
 *   petitions on the same day they are filed if the urgency so
 *   requires, bypassing normal procedural requirements like
 *   service of notice on the respondents. The court typically
 *   issues a "rule nisi" — an order requiring the detaining
 *   authority to appear and show cause why the detained person
 *   should not be released. The detaining authority then has the
 *   burden of justifying the detention.
 *
 *   Third, habeas corpus operates with a REVERSED BURDEN OF
 *   PROOF compared to ordinary writs. In a normal writ petition
 *   under Article 226, the petitioner has to plead and prove
 *   that the impugned action of the State is illegal. In a
 *   habeas petition, by contrast, once the petitioner alleges
 *   that the detained person is in custody, the burden shifts to
 *   the State to JUSTIFY the detention. The State must produce
 *   the legal authority for the detention — the warrant, the
 *   detention order, the FIR, or whatever else it relies on. If
 *   the State cannot produce a valid legal authority, or if the
 *   authority produced is found to be defective on its face, the
 *   detained person is released.
 *
 *   Fourth, habeas corpus has a BARE-FACT PLEADING style. Unlike
 *   the elaborate factual narratives in other pleadings, a
 *   habeas petition is typically very short. The essential facts
 *   are simply that the detained person is being held by the
 *   named authority, in the named place, since the named date,
 *   and that the petitioner believes the detention to be
 *   illegal. The court then conducts its own inquiry rather than
 *   relying on the petitioner's narrative. This bare-fact style
 *   is a deliberate design choice that allows the remedy to be
 *   invoked quickly and without the cost of detailed pleading.
 *
 *   Fifth, the prayer in a habeas petition is uniquely PERSONAL.
 *   Unlike other writs which ask the court to issue directions
 *   to government departments or to quash administrative orders,
 *   the habeas prayer asks the court to direct the respondents
 *   "to produce the detenu before this Hon'ble Court and to set
 *   him at liberty." This is the actual physical relief that
 *   habeas corpus is designed to provide.
 *
 * THE GROUNDS FOR HABEAS CORPUS:
 *
 *   A habeas petition can be filed on any ground that would
 *   render the detention illegal. The most common grounds
 *   include arrest without lawful authority, detention beyond
 *   the period authorised by law, detention under a defective
 *   detention order, detention for an improper purpose,
 *   detention by an authority not competent to detain, custody
 *   in violation of fundamental rights, and detention without
 *   informing the detained person of the grounds of detention.
 *   The constitutional safeguards in Article 22 of the
 *   Constitution provide a particularly fertile source of
 *   grounds, because Article 22 imposes strict procedural
 *   requirements on both ordinary criminal arrest and on
 *   preventive detention.
 *
 * THE LANDMARK CASES YOU SHOULD KNOW:
 *
 *   The history of habeas corpus in independent India has been
 *   shaped by several landmark cases that you should be aware of
 *   even if this is just a drafting exercise. ADM Jabalpur v.
 *   Shivkant Shukla (1976) was the notorious case in which the
 *   Supreme Court held during the Emergency that the right to
 *   move the High Courts for habeas corpus could be suspended,
 *   leading to one of the darkest moments in Indian constitutional
 *   history. That decision was effectively overruled by K.S.
 *   Puttaswamy v. Union of India (2017), which restored the
 *   inviolability of personal liberty as a fundamental right.
 *   More recently, Arnesh Kumar v. State of Bihar (2014) and
 *   Satender Kumar Antil v. CBI (2022) have laid down strict
 *   guidelines on when arrest is permissible at all, and these
 *   cases provide the substantive grounds for many modern habeas
 *   petitions.
 *
 * STRUCTURAL FEATURES OF THIS TEMPLATE:
 *
 *   First, look at the cause title. The petitioner is named
 *   first, and the detenu is identified separately as the
 *   subject of the petition. This is unique to habeas corpus
 *   and reflects the third-party nature of the petition.
 *
 *   Second, look at how the case is captioned as "Writ Petition
 *   (Criminal)" rather than "Writ Petition (Civil)." Habeas
 *   corpus matters in India are conventionally treated as
 *   criminal because they involve personal liberty rather than
 *   civil rights, even though the High Court's writ
 *   jurisdiction itself is technically civil in nature.
 *
 *   Third, look at the identification of the respondents. The
 *   detaining authority is named specifically (the Inspector,
 *   the Police Station, the Superintendent of the Jail), and
 *   the State is also impleaded through the Home Secretary.
 *   This multi-respondent structure ensures that the order can
 *   be enforced against whichever official actually has custody
 *   of the detenu.
 *
 *   Fourth, look at the brevity of the body. The petition has
 *   only the essential facts and grounds — no elaborate
 *   narrative, no extensive citation of authority. The court
 *   is expected to act quickly on the bare allegations and to
 *   conduct its own inquiry.
 */

