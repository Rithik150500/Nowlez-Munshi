/**
 * PETITION UNDER SECTION 241 OF THE COMPANIES ACT, 2013
 * FOR OPPRESSION AND MISMANAGEMENT
 * ──────────────────────────────────────────────────────
 * Category : Corporate Law — Minority Shareholder Remedies
 * Tribunal : National Company Law Tribunal (NCLT)
 * Statute  : Sections 241 to 246, Companies Act, 2013
 * Source   : Standard form used in Indian corporate practice
 *
 * The Petition under Section 241 of the Companies Act, 2013 is
 * the fifth and final template in this batch and it introduces
 * an entirely new dimension of corporate law that complements
 * Template 59 (Shareholders' Agreement) in your library. To
 * grasp the relationship between the two templates, you have
 * to understand that Template 59 is a transactional document
 * that sets out how shareholders agree to govern their
 * relationship when everything is going well, while Template 70
 * is a litigation document that provides a remedy when
 * everything has gone wrong and the cooperative structures of
 * the shareholders' agreement have broken down.
 *
 * THE OPPRESSION AND MISMANAGEMENT REMEDY:
 *
 *   The basic problem that Section 241 addresses is the
 *   tension between majority rule and minority protection in
 *   corporate governance. Under the ordinary principles of
 *   company law, the majority of shareholders have the right
 *   to make decisions for the company, and minority
 *   shareholders are bound by those decisions even if they
 *   disagree. This is sometimes called the rule in Foss v.
 *   Harbottle, after the leading 19th-century English case
 *   that established the principle. But the rule of majority
 *   power has obvious dangers because the majority may use
 *   their power to oppress the minority, to enrich themselves
 *   at the expense of the company, or to mismanage the
 *   company in ways that destroy its value. The oppression
 *   and mismanagement remedy is the principal exception to
 *   the Foss v. Harbottle rule and gives minority
 *   shareholders a way to seek redress when the majority
 *   has abused its power.
 *
 *   The remedy was first introduced in Indian company law by
 *   Sections 397 and 398 of the Companies Act, 1956. Those
 *   sections were carried forward into the Companies Act,
 *   2013 as Sections 241 to 246, with some modifications
 *   that broadened the scope of the remedy and shifted the
 *   forum from the High Court to the National Company Law
 *   Tribunal. The change of forum has had a significant
 *   practical effect because the NCLT is intended to be a
 *   faster and more specialised tribunal than the ordinary
 *   civil courts.
 *
 * THE TWO LIMBS OF SECTION 241:
 *
 *   Section 241 of the Companies Act, 2013 provides for
 *   relief in two distinct situations that you should learn
 *   to identify. The first situation, set out in Section
 *   241(1)(a), is OPPRESSION, which arises where the affairs
 *   of the company have been or are being conducted in a
 *   manner prejudicial to public interest, prejudicial or
 *   oppressive to the petitioning member, or prejudicial to
 *   the interests of the company itself. The second
 *   situation, set out in Section 241(1)(b), is
 *   MISMANAGEMENT, which arises where there has been a
 *   material change in the management or control of the
 *   company that is likely to result in the affairs of the
 *   company being conducted in a manner prejudicial to its
 *   interests.
 *
 *   These two limbs cover different kinds of corporate
 *   misconduct. Oppression typically involves specific
 *   actions taken by the majority against the minority, such
 *   as the issuance of new shares to dilute the minority's
 *   stake, the diversion of corporate opportunities to the
 *   personal benefit of the majority, the entry into
 *   related-party transactions on unfair terms, the
 *   exclusion of minority directors from board meetings, or
 *   the denial of access to corporate information.
 *   Mismanagement typically involves more general failures
 *   in the conduct of the company's affairs, such as the
 *   commission of acts that bring the company into
 *   disrepute, the failure to pursue profitable
 *   opportunities, the reckless commitment of the company's
 *   assets to high-risk ventures, or the persistent failure
 *   to comply with statutory requirements.
 *
 * THE LOCUS STANDI REQUIREMENT UNDER SECTION 244:
 *
 *   The most important threshold issue in any Section 241
 *   petition is the locus standi requirement under Section
 *   244 of the Act. Not every shareholder can file an
 *   oppression and mismanagement petition. The petition can
 *   only be filed by members who satisfy one of the
 *   following thresholds: in the case of a company having a
 *   share capital, by not less than one hundred members of
 *   the company or not less than one-tenth of the total
 *   number of its members, whichever is less; or by any
 *   member or members holding not less than one-tenth of
 *   the issued share capital of the company, provided that
 *   the applicant or applicants have paid all calls and
 *   other sums due on their shares. In the case of a company
 *   not having a share capital, by not less than one-fifth
 *   of the total number of its members.
 *
 *   These thresholds are designed to prevent vexatious
 *   petitions by individual disgruntled shareholders while
 *   still allowing genuine minority groups to seek redress.
 *   The Tribunal has the power under Section 244(1) to waive
 *   the threshold requirements in appropriate cases, and
 *   this waiver power has been frequently exercised in
 *   practice to allow petitions by single shareholders or
 *   small groups who can show genuine grievances.
 *
 * THE WIDE POWERS OF THE NCLT UNDER SECTION 242:
 *
 *   One of the most striking features of the oppression and
 *   mismanagement remedy is the breadth of the powers that
 *   the Tribunal can exercise once it finds that oppression
 *   or mismanagement has occurred. Section 242 of the Act
 *   gives the NCLT a remarkably wide range of powers,
 *   including: the regulation of the future conduct of the
 *   company's affairs; the purchase of the shares or
 *   interests of any members of the company by other members
 *   or by the company itself; the consequent reduction of the
 *   share capital of the company; the termination, setting
 *   aside, or modification of any agreement between the
 *   company and any other person; the setting aside of any
 *   transfer or transaction that is voidable; the removal
 *   of the managing director, manager, or any of the
 *   directors of the company; the recovery of undue gains
 *   made by any managing director, manager, or director and
 *   the manner of utilisation of such recovery; the
 *   imposition of costs as may be deemed fit by the Tribunal;
 *   and any other matter for which it is just and equitable
 *   that provision should be made.
 *
 *   The most commonly invoked power in practice is the
 *   buy-out remedy, by which the Tribunal directs that the
 *   majority shareholders purchase the shares of the
 *   minority at a fair value to be determined by an
 *   independent valuer. This buy-out remedy effectively
 *   allows the minority to exit the company at a fair price
 *   when the relationship between the majority and the
 *   minority has broken down beyond repair. It is the
 *   functional equivalent of a divorce in the corporate
 *   context, and it has become the most popular outcome of
 *   Section 241 petitions in modern Indian practice.
 *
 * STRUCTURAL FEATURES OF THIS TEMPLATE:
 *
 *   First, look at the case header. The petition is filed
 *   before the NCLT under the case category "CP" (Company
 *   Petition), which is the standard NCLT abbreviation for
 *   petitions filed under the Companies Act. This is similar
 *   to the IBC templates at 62 and 63 in your library, which
 *   also use NCLT as the forum.
 *
 *   Second, look at the parties. The petitioner is described
 *   as a member of the company, and the respondents include
 *   the company itself (as Respondent No. 1), the offending
 *   directors (as Respondents Nos. 2 and 3), and any other
 *   persons whose conduct is being challenged. The company
 *   is always made a party because the relief sought
 *   typically affects the corporate entity itself.
 *
 *   Third, look at the body. Paragraphs 1 and 2 establish the
 *   identities of the petitioner and the company. Paragraph
 *   3 establishes the locus standi of the petitioner under
 *   Section 244. Paragraph 4 onwards sets out the specific
 *   acts of oppression and mismanagement that the petitioner
 *   alleges. The grounds section explains why these acts
 *   constitute oppression or mismanagement within the
 *   meaning of Section 241.
 *
 *   Fourth, look at the prayer. It typically includes both
 *   the buy-out remedy and a range of other reliefs from the
 *   menu set out in Section 242. The petitioner is generally
 *   advised to ask for everything that might be appropriate
 *   so that the Tribunal has the maximum flexibility to
 *   craft an appropriate remedy.
 */

