/**
 * APPLICATION FOR RECORDING OF COMPROMISE UNDER ORDER XXIII
 * RULE 3 OF THE CODE OF CIVIL PROCEDURE, 1908
 * ─────────────────────────────────────────────────────────────
 * Category : Civil Procedure — Settlement of Suit
 * Court    : The Court hearing the underlying suit
 * Statute  : Order XXIII Rule 3, Code of Civil Procedure, 1908
 * Source   : Standard form used in Indian civil practice
 *
 * The Application for Recording of Compromise under Order XXIII
 * Rule 3 addresses one of the most important and most often
 * overlooked aspects of Indian civil litigation, namely the fact
 * that the vast majority of civil cases do not end in judgments
 * after a contested trial. They end in settlements between the
 * parties. Court statistics from across India consistently show
 * that something like sixty to seventy percent of all civil
 * cases that are filed are eventually resolved through
 * compromise rather than through adjudication on the merits.
 * This is true partly because litigation is expensive and
 * uncertain, partly because Indian courts are slow and parties
 * grow tired of waiting, partly because court-annexed mediation
 * has become more common, and partly because parties often come
 * to realise during the course of the litigation that some
 * middle-ground solution serves their interests better than a
 * winner-takes-all judgment ever could.
 *
 * The procedural device by which such settlements are
 * incorporated into the formal court record and given binding
 * legal effect is the compromise decree under Order XXIII Rule
 * 3. This template provides the application that converts a
 * private settlement into a court decree, and it is one of the
 * most quietly important documents in your library because it
 * represents the actual end-point of so many cases.
 *
 * THE LEGAL STATUS OF A COMPROMISE DECREE:
 *
 *   To grasp why this procedural device matters, you have to
 *   understand the difference between a private settlement
 *   agreement and a compromise decree. A private settlement is
 *   simply a contract between the parties saying that the
 *   dispute is resolved on certain terms. If one party later
 *   reneges on the settlement, the other party would have to
 *   file a fresh suit to enforce the contract, which is a slow
 *   and uncertain remedy. A compromise decree, by contrast, is
 *   a court decree that incorporates the terms of the
 *   settlement. If one party reneges on a compromise decree,
 *   the other party can directly file an execution application
 *   under Order XXI of the CPC (the same procedure used for
 *   regular decrees, which you saw at Template 36 in your
 *   library) and have the decree enforced through court
 *   process. This makes the compromise decree a much more
 *   powerful and reliable instrument than a mere private
 *   settlement.
 *
 *   Under Order XXIII Rule 3, when the parties to a suit
 *   inform the court that they have agreed to compromise the
 *   dispute, the court is required to record the compromise
 *   and pass a decree in accordance with the terms of the
 *   compromise. The decree so passed is called a "consent
 *   decree" or a "compromise decree" and has the same legal
 *   effect as any other decree of the court.
 *
 * THE 1976 AMENDMENT — THE LAWFULNESS REQUIREMENT:
 *
 *   Order XXIII Rule 3 was amended in 1976 to add an important
 *   requirement that you should understand. Before the
 *   amendment, the court was essentially required to record any
 *   compromise that the parties brought to it, with very little
 *   power of independent inquiry. This led to abuses in some
 *   cases where the parties used the compromise mechanism to
 *   achieve outcomes that would not have been permissible
 *   through litigation, such as transferring property in
 *   violation of statutory restrictions or settling disputes
 *   that were not actually within the scope of the suit.
 *
 *   The 1976 amendment added a critical proviso. It says that
 *   the compromise must be in writing and signed by the
 *   parties, and it must be a LAWFUL compromise. The court is
 *   now required to satisfy itself that the compromise is
 *   lawful before recording it. This means that the court has
 *   the power to refuse to record a compromise that is
 *   illegal, that is in violation of public policy, that is
 *   unconscionable, or that goes beyond the subject matter of
 *   the suit.
 *
 *   The lawfulness requirement is in practice usually a
 *   formality because the vast majority of compromises are
 *   straightforward settlements of money claims or property
 *   disputes between consenting adults, but it is an
 *   important safeguard against abuse and you should be aware
 *   of it when drafting compromise applications.
 *
 * THE WRITING AND SIGNATURE REQUIREMENTS:
 *
 *   Order XXIII Rule 3 specifies that the compromise must be
 *   in writing and signed by the parties. This was a
 *   significant change from the earlier law, which permitted
 *   oral compromises to be recorded on the basis of statements
 *   made by counsel in court. The writing requirement ensures
 *   that there is a clear documentary record of what was
 *   agreed and prevents disputes about the terms of the
 *   settlement.
 *
 *   In practice, the parties typically prepare a written
 *   compromise deed setting out the terms of the settlement
 *   and sign it. The compromise deed is then filed in court
 *   along with this application asking the court to record
 *   the compromise and pass a decree in terms of the
 *   compromise. The compromise deed becomes part of the court
 *   record and is referenced in the operative portion of the
 *   decree.
 *
 * THE FINALITY OF COMPROMISE DECREES:
 *
 *   One of the most important features of compromise decrees
 *   is their finality. A compromise decree generally cannot be
 *   appealed because it is based on the consent of the
 *   parties, and a party cannot consent to something and then
 *   appeal against it. The only way to challenge a compromise
 *   decree is to file a separate suit alleging that the
 *   compromise itself was invalid because it was obtained by
 *   fraud, coercion, mistake, or some other vitiating factor.
 *   This makes the compromise decree a particularly attractive
 *   way of achieving finality in litigation, since the
 *   ordinary appellate mechanisms are largely unavailable.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at the case header. The application uses the
 *   IN format that signals it is filed within the existing
 *   parent suit. This is consistent with all the other
 *   interlocutory applications in your library.
 *
 *   Second, look at how the parties are described. Both the
 *   plaintiff and the defendant are described as joint
 *   applicants, since the compromise application is made
 *   jointly by both sides. This joint posture is unusual
 *   among the templates in your library — most templates have
 *   an applicant on one side and a non-applicant or respondent
 *   on the other — but it reflects the cooperative nature of
 *   compromise. You may notice the parallel with the Mutual
 *   Consent Divorce petition at Template 11, where the parties
 *   are also co-petitioners rather than adversaries.
 *
 *   Third, look at the body of the application. Paragraph 1
 *   identifies the underlying suit. Paragraph 2 explains how
 *   the compromise was reached. Paragraph 3 references the
 *   written compromise deed. Paragraph 4 contains the
 *   statutory averment that the compromise is lawful and
 *   bona fide. Paragraph 5 asks the court to record the
 *   compromise and pass a decree in its terms.
 *
 *   Fourth, look at the signing block. Both the plaintiff and
 *   the defendant sign the application, and the application
 *   is also signed by their respective counsel. This four-way
 *   signature confirms that all the relevant actors have
 *   consented to the compromise.
 */

const {
  Document, Paragraph, TextRun,
  AlignmentType, TabStopType, TabStopPosition,
  Footer, PageNumber, LevelFormat
} = require("docx");

function legalPara(children, opts = {}) {
  return new Paragraph({
    spacing: { after: 120, line: 360 },
    alignment: AlignmentType.JUSTIFIED, ...opts, children,
  });
}
function centeredBold(text, size = 24) {
  return new Paragraph({
    alignment: AlignmentType.CENTER, spacing: { after: 60 },
    children: [new TextRun({ text, bold: true, size, font: "Times New Roman" })],
  });
}
