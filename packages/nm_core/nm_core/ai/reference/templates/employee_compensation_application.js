/**
 * APPLICATION FOR COMPENSATION UNDER THE EMPLOYEE'S
 * COMPENSATION ACT, 1923 (FORMERLY THE WORKMEN'S
 * COMPENSATION ACT, 1923)
 * ─────────────────────────────────────────────────────
 * Category : Labour and Employment Law — Workplace Injury
 *            Compensation
 * Forum    : Commissioner for Employee's Compensation
 * Statute  : Sections 3, 4, 4A, 10, and 22 of the Employee's
 *            Compensation Act, 1923
 * Source   : Standard form used in Indian employee compensation
 *            practice
 *
 * The Application for Compensation under the Employee's
 * Compensation Act, 1923 is the fifth and final template in the
 * labour law batch and it introduces the strict liability regime
 * for compensation for workplace injuries that has been part of
 * Indian law since the colonial era. To grasp where this template
 * fits, you have to understand the basic principle of strict
 * liability that the Act applies, the limited categories of
 * injuries that the Act covers, and the formula for calculating
 * compensation that the Act prescribes.
 *
 * THE STRICT LIABILITY PRINCIPLE:
 *
 *   The Employee's Compensation Act, 1923 was originally
 *   enacted as the Workmen's Compensation Act, 1923 and was
 *   one of the earliest pieces of social welfare legislation
 *   in India. The Act was modelled on the English Workmen's
 *   Compensation Act, 1897 and was intended to address the
 *   inadequacy of the common law of torts in compensating
 *   workers who were injured in the course of their
 *   employment. Under the common law principles that applied
 *   before the Act, an injured worker could only recover
 *   compensation from the employer by proving that the
 *   employer had been negligent in causing the injury, and
 *   even then the worker faced several common law defences
 *   including the doctrines of contributory negligence,
 *   common employment, and voluntary assumption of risk.
 *   These defences made it extremely difficult for injured
 *   workers to obtain effective compensation, and they
 *   placed the entire risk of workplace injuries on the
 *   workers themselves rather than on the employers who
 *   were better placed to manage and insure against the
 *   risk.
 *
 *   The Workmen's Compensation Act addressed this problem
 *   by introducing a regime of strict liability under which
 *   the employer becomes liable to pay compensation to a
 *   workman for any injury caused by an accident arising
 *   out of and in the course of employment, regardless of
 *   whether the employer was negligent or whether the
 *   workman was contributorily negligent. The strict
 *   liability principle is set out in Section 3(1) of the
 *   Act, which provides that if personal injury is caused
 *   to an employee by accident arising out of and in the
 *   course of his employment, his employer shall be liable
 *   to pay compensation in accordance with the provisions
 *   of the Act.
 *
 *   This strict liability regime represents a fundamental
 *   trade-off between the employer and the employee. The
 *   employer accepts liability for all workplace injuries
 *   regardless of fault, and in exchange the employee
 *   accepts compensation calculated according to a fixed
 *   statutory formula rather than the open-ended damages
 *   that might be available in a tort action. This
 *   trade-off is intended to provide certainty for both
 *   sides and to ensure that injured workers receive prompt
 *   and predictable compensation without having to engage
 *   in lengthy litigation.
 *
 * THE EMPLOYEE'S COMPENSATION (AMENDMENT) ACT, 2009:
 *
 *   The Workmen's Compensation Act was renamed as the
 *   Employee's Compensation Act in 2009 and was substantially
 *   amended to address several inadequacies in the original
 *   1923 Act. The 2009 amendment introduced several
 *   important changes that you should be aware of when
 *   drafting any application under the Act. First, the
 *   amendment removed the wage ceiling that had previously
 *   restricted the application of the Act to workers
 *   earning below a specified amount, and made the Act
 *   applicable to all employees regardless of wages.
 *   Second, the amendment significantly increased the
 *   amounts of compensation payable under the Act, bringing
 *   them more in line with the actual cost of living and
 *   the actual loss suffered by injured workers and their
 *   families. Third, the amendment broadened the categories
 *   of workers covered by the Act and expanded the
 *   definition of "occupational disease" in Schedule III to
 *   the Act.
 *
 *   These amendments have made the Employee's Compensation
 *   Act a much more meaningful source of protection for
 *   injured workers in modern India, although the
 *   compensation payable is still substantially less than
 *   what might be recoverable in a tort action against a
 *   negligent employer.
 *
 * THE INJURIES COVERED:
 *
 *   The Act covers two broad categories of work-related
 *   harm. The first category is "personal injury caused to
 *   an employee by accident arising out of and in the course
 *   of his employment," which is the most common basis for
 *   claims under the Act and which covers most workplace
 *   injuries including those caused by falls, machinery,
 *   chemicals, electricity, and similar hazards. The phrase
 *   "arising out of and in the course of" employment has
 *   been the subject of substantial judicial interpretation,
 *   and the courts have generally given it a liberal
 *   construction to cover injuries that have a reasonable
 *   nexus with the employment even if they did not occur
 *   during actual work hours or at the actual workplace.
 *
 *   The second category is "occupational disease" listed in
 *   Schedule III to the Act, which includes a long list of
 *   diseases that are recognised as being caused or
 *   aggravated by particular kinds of employment, including
 *   silicosis, asbestosis, lead poisoning, mercury
 *   poisoning, and various other industrial diseases. The
 *   inclusion of occupational diseases is intended to
 *   address the long-tail nature of certain workplace
 *   exposures that may not result in immediate visible
 *   injury but that cause disease over a longer period of
 *   time.
 *
 * THE COMPENSATION FORMULA:
 *
 *   The compensation payable under the Act is calculated
 *   according to a formula set out in Section 4 of the Act,
 *   which depends on the nature of the injury, the age of
 *   the employee, and the monthly wages of the employee.
 *   For death, the compensation is fifty percent of the
 *   monthly wages multiplied by the relevant factor set out
 *   in Schedule IV to the Act, subject to a minimum of
 *   Rs. 1,20,000. For permanent total disablement, the
 *   compensation is sixty percent of the monthly wages
 *   multiplied by the relevant factor, subject to a minimum
 *   of Rs. 1,40,000. For permanent partial disablement, the
 *   compensation is calculated as a proportion of the
 *   compensation for permanent total disablement based on
 *   the percentage of loss of earning capacity. For
 *   temporary disablement, the compensation is twenty-five
 *   percent of the monthly wages payable as a half-monthly
 *   payment.
 *
 *   The relevant factor in Schedule IV depends on the age of
 *   the employee at the time of the accident, with younger
 *   employees attracting a higher factor (because they have
 *   more years of working life remaining) and older
 *   employees attracting a lower factor.
 *
 * STRUCTURAL FEATURES OF THIS TEMPLATE:
 *
 *   The Application for Compensation has a relatively
 *   simple structure that focuses on establishing the four
 *   essential elements of a claim: that the applicant is an
 *   employee within the meaning of the Act, that the
 *   accident occurred during the course of employment, that
 *   the accident arose out of the employment, and that the
 *   employee suffered personal injury as a result of the
 *   accident. The body also sets out the wages of the
 *   employee, the nature and extent of the injury, and the
 *   compensation calculated according to the statutory
 *   formula.
 */

