/**
 * APPEAL TO THE COMMISSIONER OF INCOME TAX (APPEALS)
 * UNDER SECTION 246A OF THE INCOME TAX ACT, 1961
 * ─────────────────────────────────────────────────────
 * Category : Tax Law — First Appellate Tier
 * Forum    : Commissioner of Income Tax (Appeals) — CIT(A)
 * Statute  : Section 246A, Income Tax Act, 1961
 * Form     : Form 35 prescribed under the Income Tax Rules, 1962
 * Source   : Standard form used in Indian tax practice
 *
 * The Appeal to the Commissioner of Income Tax (Appeals) is the
 * fourth template in this batch and it introduces tribunal
 * practice in the tax domain, which is one of the most
 * specialised areas of Indian legal practice. To grasp where
 * the CIT(A) appeal fits, you have to understand the multi-tier
 * structure of the Indian income tax appellate system, which is
 * one of the most elaborate appellate hierarchies in any area
 * of Indian law.
 *
 * THE FOUR-TIER APPELLATE STRUCTURE IN INCOME TAX:
 *
 *   The Indian income tax system provides four levels of
 *   appeal against an order passed by an Assessing Officer.
 *
 *   The first tier is the Commissioner of Income Tax (Appeals),
 *   abbreviated as CIT(A), which is what this template covers.
 *   The CIT(A) is a senior officer of the Income Tax
 *   Department who functions as an independent appellate
 *   authority and hears appeals against the orders of
 *   Assessing Officers. The appeal lies under Section 246A of
 *   the Income Tax Act, 1961, and is filed in Form 35
 *   prescribed under the Income Tax Rules, 1962. The CIT(A)
 *   has wide powers under Section 251 of the Act, including
 *   the power to confirm, reduce, enhance, or annul the
 *   assessment, and the power to set aside the assessment and
 *   refer the case back to the Assessing Officer. The CIT(A)
 *   is required to dispose of the appeal within one year of
 *   the end of the financial year in which the appeal is
 *   filed, although in practice this timeline is rarely met.
 *
 *   The second tier is the Income Tax Appellate Tribunal
 *   (ITAT), which is a quasi-judicial body established under
 *   Section 252 of the Act. The ITAT hears appeals against
 *   orders of the CIT(A) under Section 253 of the Act and is
 *   the highest fact-finding authority in the income tax
 *   system. The ITAT consists of judicial members and
 *   accountant members who sit in benches at various
 *   locations across India. An appeal to the ITAT must
 *   ordinarily be filed within sixty days from the date of
 *   communication of the CIT(A)'s order.
 *
 *   The third tier is the High Court, which has jurisdiction
 *   under Section 260A of the Act to hear appeals from the
 *   ITAT on substantial questions of law (not on questions of
 *   fact). The High Court appeal must be filed within 120
 *   days from the date on which the ITAT's order is received
 *   by the appellant.
 *
 *   The fourth and final tier is the Supreme Court of India,
 *   which can be approached either through a special leave
 *   petition under Article 136 of the Constitution or, in
 *   appropriate cases, through an appeal certificate granted
 *   by the High Court itself.
 *
 *   This four-tier structure means that a single income tax
 *   dispute can potentially go through four levels of appeal
 *   over many years, and well-organised tax disputes between
 *   the Department and large corporate assessees can take
 *   fifteen to twenty years to reach final resolution.
 *
 * THE TYPES OF ORDERS APPEALABLE BEFORE CIT(A):
 *
 *   Section 246A(1) of the Act specifies the types of orders
 *   against which an appeal lies before the CIT(A). The most
 *   common appealable orders are: assessment orders passed
 *   under Section 143(3) of the Act after a regular scrutiny
 *   assessment; reassessment orders passed under Section 147
 *   of the Act after the reopening of an assessment; orders
 *   passed under Section 144 of the Act after a best judgment
 *   assessment in case of non-cooperation by the assessee;
 *   penalty orders passed under various sections including
 *   Section 271(1)(c) for concealment of income, Section
 *   271AAC for unexplained income, and Section 270A for
 *   under-reporting and misreporting; rectification orders
 *   passed under Section 154 of the Act; and orders passed
 *   under Section 201 of the Act treating the assessee as in
 *   default for failure to deduct or pay tax at source.
 *
 *   Each of these orders gives rise to its own characteristic
 *   grounds of appeal, and a well-drafted CIT(A) appeal
 *   should identify the specific order being challenged and
 *   the specific grounds on which it is being challenged.
 *
 * THE PROCEDURAL REQUIREMENTS:
 *
 *   The procedural requirements for filing a CIT(A) appeal
 *   are strict and unforgiving. The appeal must be filed in
 *   Form 35 within thirty days from the date on which the
 *   impugned order is communicated to the assessee. The
 *   appeal can be filed beyond the thirty-day period only
 *   if the CIT(A) is satisfied that there was sufficient
 *   cause for the delay, and the application for condonation
 *   of delay must be made along with the appeal.
 *
 *   The appeal must be accompanied by the prescribed appeal
 *   fee, which depends on the amount of total income
 *   determined by the Assessing Officer. The fees are very
 *   modest by the standards of Indian commercial litigation:
 *   Rs. 250 if the assessed income is less than Rs. 1 lakh,
 *   Rs. 500 if it is between Rs. 1 lakh and Rs. 2 lakh, Rs.
 *   1000 if it is more than Rs. 2 lakh, and Rs. 250 in any
 *   other case (such as penalty orders).
 *
 *   The most important practical issue with CIT(A) appeals is
 *   the question of pre-deposit. Filing an appeal does not
 *   automatically stay the recovery of the disputed demand.
 *   The Department is entitled to proceed with recovery of the
 *   tax demand even while the appeal is pending. The CBDT
 *   has issued an instruction that the Assessing Officer should
 *   typically grant a stay of recovery if the assessee deposits
 *   twenty percent of the disputed demand. The remaining
 *   eighty percent is then suspended pending the disposal of
 *   the appeal. This twenty percent pre-deposit rule is not
 *   a statutory requirement but it is the standard
 *   administrative practice and most assessees deposit
 *   twenty percent at the time of filing the appeal.
 *
 * STRUCTURAL FEATURES OF THIS TEMPLATE:
 *
 *   The CIT(A) appeal does not have a court header in the
 *   traditional sense. Instead, it follows the structure of
 *   Form 35 prescribed under Rule 45 of the Income Tax
 *   Rules. The form contains specific blanks for the
 *   appellant's details, the order being appealed, the
 *   grounds of appeal, the relief claimed, and the verification.
 *
 *   First, look at the identifying information at the top.
 *   The PAN of the appellant, the assessment year, the
 *   section under which the impugned order was passed, and
 *   the date of the impugned order are all set out in a
 *   tabular format because this information is critical for
 *   the CIT(A) to identify the case.
 *
 *   Second, look at the body. The grounds of appeal are
 *   organised as numbered paragraphs, each setting out one
 *   specific objection to the impugned order. The grounds
 *   should be specific, focused, and supported by reference
 *   to the relevant provisions of law and to the facts of
 *   the case.
 *
 *   Third, look at the relief claimed. The relief should
 *   identify the specific reduction in the assessment that
 *   the appellant seeks, rather than asking for general
 *   relief.
 */

