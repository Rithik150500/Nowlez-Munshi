/**
 * SUIT FOR COPYRIGHT INFRINGEMENT UNDER SECTION 55
 * OF THE COPYRIGHT ACT, 1957
 * ────────────────────────────────────────────────────
 * Category : Intellectual Property — Civil Court Remedy
 * Court    : District Court / High Court (depending on value)
 * Statute  : Section 55, Copyright Act, 1957
 * Source   : Standard form used in Indian copyright practice
 *
 * The Suit for Copyright Infringement is the second IP template
 * in your library and it introduces civil court remedies for the
 * violation of copyright. To grasp where the copyright suit fits
 * in your library, you should compare it with the other IP-style
 * suits you have seen, particularly the Suit for Permanent
 * Injunction at Template 1 and the Suit for Specific Performance
 * at Template 17. The copyright infringement suit follows the
 * same general structural pattern as those ordinary civil suits
 * but with several important differences that reflect the
 * distinctive nature of copyright as a property right.
 *
 * THE NATURE OF COPYRIGHT AS A PROPERTY RIGHT:
 *
 *   Before drafting any copyright infringement suit, you have
 *   to understand what copyright actually is and what it
 *   protects. Unlike a trademark, which protects a brand
 *   identifier used in trade, copyright protects original
 *   creative expression. Section 14 of the Copyright Act,
 *   1957 confers on the owner of copyright the exclusive
 *   right to reproduce the work, to issue copies of the work
 *   to the public, to perform the work in public, to
 *   communicate the work to the public, to make any
 *   cinematograph film or sound recording in respect of the
 *   work, to make any translation of the work, and to make
 *   any adaptation of the work. These exclusive rights are
 *   collectively called the "bundle of rights" that constitute
 *   copyright, and any unauthorised exercise of any of these
 *   rights by a third party amounts to infringement under
 *   Section 51 of the Act.
 *
 *   Copyright in India arises automatically the moment a
 *   work is created in a tangible form. There is no
 *   requirement of registration for copyright to exist or for
 *   the owner to have rights in the work. However, the
 *   Copyright Act does provide for voluntary registration
 *   under Sections 44 and 45, and a registered copyright
 *   enjoys certain evidentiary advantages in litigation.
 *   Specifically, Section 48 of the Act provides that the
 *   Register of Copyrights is prima facie evidence of the
 *   particulars entered therein, including the ownership of
 *   the copyright.
 *
 * THE CATEGORIES OF COPYRIGHTABLE WORKS:
 *
 *   Section 13 of the Act lists the categories of works in
 *   which copyright can subsist. These categories include:
 *   original literary, dramatic, musical, and artistic works;
 *   cinematograph films; and sound recordings. Each of these
 *   categories has its own particular requirements and its
 *   own particular bundle of rights. Literary works include
 *   books, articles, poems, computer programs, and databases.
 *   Dramatic works include plays, screenplays, and other
 *   works intended for performance. Musical works are
 *   protected separately from any underlying lyrics, which
 *   are protected as literary works. Artistic works include
 *   paintings, sculptures, drawings, photographs, and works
 *   of architecture. Cinematograph films and sound recordings
 *   are treated as separate categories of works in their own
 *   right, with the producer of the film or sound recording
 *   being the first owner of copyright.
 *
 * THE REMEDIES UNDER SECTION 55:
 *
 *   Section 55 of the Act sets out the civil remedies
 *   available for copyright infringement. The remedies are
 *   both compensatory and preventive in nature, and a
 *   well-drafted copyright suit will typically seek all of
 *   them in the alternative or in combination.
 *
 *   The first remedy is the INJUNCTION, which is the most
 *   important practical remedy in most copyright cases. The
 *   injunction can be permanent or temporary, mandatory or
 *   prohibitory. A permanent injunction prohibits the
 *   defendant from continuing the infringing activity in the
 *   future. A temporary injunction (also called an interim
 *   injunction) prohibits the infringing activity during the
 *   pendency of the suit, and is granted under Order XXXIX
 *   Rules 1 and 2 of the CPC on the same three-element test
 *   that you have seen in earlier templates: prima facie
 *   case, balance of convenience, and irreparable injury.
 *
 *   The second remedy is DAMAGES, which can be sought either
 *   on a compensatory basis (to compensate the plaintiff for
 *   the actual loss caused by the infringement) or on the
 *   basis of an account of profits (to disgorge the profits
 *   that the defendant has made through the infringement).
 *   The plaintiff can elect between these two measures, and
 *   the choice depends on which produces the higher recovery
 *   in the particular case.
 *
 *   The third remedy is the DELIVERY UP of infringing copies,
 *   which requires the defendant to hand over to the
 *   plaintiff all infringing copies of the work along with
 *   any plates, moulds, or other tools used to produce them.
 *   This remedy is meant to prevent the defendant from
 *   continuing to use the infringing materials in the future.
 *
 *   The fourth remedy, which is sometimes overlooked but
 *   which can be very important in commercial cases, is the
 *   awarding of COSTS, which can include the legal costs of
 *   the proceedings as well as any costs incurred by the
 *   plaintiff in tracing and identifying the infringing
 *   activity.
 *
 * THE JURISDICTION QUESTION:
 *
 *   One important practical issue with copyright suits is the
 *   question of jurisdiction. Section 62 of the Copyright Act
 *   contains a remarkable provision that allows the plaintiff
 *   to file the suit in the district court within whose
 *   jurisdiction the plaintiff resides or carries on business,
 *   even if the cause of action did not arise within that
 *   jurisdiction. This is a significant departure from the
 *   ordinary rule under Section 20 of the CPC, which requires
 *   the suit to be filed where the defendant resides or where
 *   the cause of action arose. The Section 62 jurisdiction
 *   provision is intended to make it easier for copyright
 *   owners to enforce their rights against infringers based
 *   in other parts of India, and it is a powerful procedural
 *   advantage that copyright owners should be aware of.
 *
 *   The Supreme Court has narrowed the Section 62 advantage
 *   somewhat in Indian Performing Rights Society v. Sanjay
 *   Dalia (2015), holding that the plaintiff cannot use
 *   Section 62 to file the suit in a court where neither the
 *   plaintiff nor the cause of action has any real connection.
 *   But within those limits, Section 62 remains an important
 *   procedural tool for copyright plaintiffs.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   The structural features of Template 72 closely parallel
 *   those of the Suit for Permanent Injunction at Template 1
 *   and the other civil suit templates in your library. The
 *   suit has a cause title with the parties arranged on
 *   either side of "VERSUS," a body that establishes the
 *   plaintiff's title, the defendant's wrongful act, and the
 *   resulting injury, and a prayer that asks for specific
 *   relief.
 *
 *   The distinctive features of the copyright suit are these.
 *   First, paragraph 2 establishes the plaintiff's ownership
 *   of the copyright with reference to the relevant
 *   provisions of the Copyright Act. Second, paragraph 3
 *   describes the work in which copyright is claimed and
 *   demonstrates that the work is original and falls within
 *   one of the categories protected by Section 13. Third,
 *   paragraph 5 sets out the specific acts of infringement
 *   committed by the defendant. Fourth, paragraph 6 invokes
 *   Section 62 jurisdiction. And fifth, the prayer asks for
 *   the full menu of Section 55 remedies including
 *   injunction, damages, account of profits, and delivery up.
 */

