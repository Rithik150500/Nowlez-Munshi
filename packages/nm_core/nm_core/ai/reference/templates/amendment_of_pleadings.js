/**
 * APPLICATION FOR AMENDMENT OF PLEADINGS
 * UNDER ORDER VI RULE 17 OF THE CODE OF CIVIL PROCEDURE
 * ─────────────────────────────────────────────────────────
 * Category : Civil Procedure — Procedural Application
 * Court    : The Court hearing the underlying suit
 * Statute  : Order VI Rule 17, Code of Civil Procedure, 1908
 * Source   : Standard form used in Indian civil practice
 *
 * The Application for Amendment of Pleadings is the procedural
 * workhorse that allows parties to fix defects in their plaints,
 * written statements, replies and other pleadings after they have
 * already been filed. To understand why this remedy is necessary,
 * you have to think about the practical reality of how
 * litigation actually unfolds. When a plaintiff first drafts a
 * plaint, they are working with whatever facts they happen to
 * know at the time, often before any discovery has taken place
 * and often under time pressure to file before limitation
 * expires. Similarly, when a defendant files a written
 * statement, they may not yet have all the documents they need
 * to plead their defence accurately. As the case progresses, new
 * facts come to light, new legal arguments occur to counsel, and
 * earlier pleadings turn out to be incomplete or even erroneous.
 * Without some mechanism to correct these defects, parties would
 * be locked into their original pleadings forever, and many cases
 * would be decided on the basis of inadequate or stale facts
 * rather than the truth.
 *
 * Order VI Rule 17 solves this problem by giving the court a
 * discretionary power to permit amendments. The text of the rule
 * has changed twice in the history of the Code, and the changes
 * tell an interesting story about how Indian procedural law has
 * tried to balance two competing concerns: the need to allow
 * genuine corrections that serve the truth, and the need to
 * prevent abuse through last-minute amendments that derail the
 * trial.
 *
 * THE EVOLUTION OF ORDER VI RULE 17:
 *
 *   Before the 1999 amendment to the Code, Order VI Rule 17 read
 *   simply that the court "may at any stage of the proceedings
 *   allow either party to alter or amend his pleadings in such
 *   manner and on such terms as may be just," and that all such
 *   amendments shall be made as may be necessary for the purpose
 *   of determining the real questions in controversy. This was
 *   a liberal regime under which courts routinely allowed
 *   amendments at virtually any stage, including during trial
 *   and even at the appellate level.
 *
 *   In 1999, the rule was DELETED entirely as part of a
 *   reform package aimed at speeding up civil litigation. The
 *   intention was to force parties to be more careful at the
 *   pleading stage. However, this absolute prohibition proved
 *   unworkable in practice because it produced obvious
 *   injustices in genuine cases.
 *
 *   In 2002, the rule was reinstated with a critical new
 *   PROVISO. The reinstated rule retains the liberal language
 *   of the pre-1999 version, but adds that "no application for
 *   amendment shall be allowed after the trial has commenced,
 *   unless the court comes to the conclusion that in spite of
 *   due diligence, the party could not have raised the matter
 *   before the commencement of trial." This proviso created a
 *   two-stage regime that operates today.
 *
 * THE TWO PHASES OF AMENDMENT LAW:
 *
 *   Under the post-2002 rule, amendment law operates in two
 *   distinct phases that you should understand precisely.
 *
 *   In the FIRST PHASE, before the trial has commenced (i.e.
 *   before issues have been framed and the parties have begun
 *   leading evidence), the court applies a LIBERAL test. The
 *   only question is whether the amendment is necessary to
 *   determine the real questions in controversy between the
 *   parties. If it is, the court will generally allow it,
 *   subject only to costs being paid to the other side for any
 *   inconvenience caused. The Supreme Court has repeatedly
 *   held that in this phase, amendments should be allowed
 *   liberally to ensure that justice is done on the basis of
 *   the actual merits of the dispute.
 *
 *   In the SECOND PHASE, after the trial has commenced, the
 *   court applies a much STRICTER test. The court will allow
 *   the amendment only if the applicant can demonstrate that,
 *   despite due diligence, they could not have raised the
 *   matter before the trial began. This is a high bar because
 *   it requires the applicant to explain not just why they
 *   want to amend now, but also why they could not have
 *   amended earlier. Mere oversight, change of mind, or
 *   reconsideration of strategy is not "due diligence" — the
 *   applicant must show some genuine reason why the new matter
 *   was not within their knowledge or could not reasonably
 *   have been ascertained before the trial began.
 *
 *   This two-phase structure means that the most important
 *   factual question for any amendment application is: HAS THE
 *   TRIAL COMMENCED? Different courts have answered this
 *   question slightly differently, but the dominant view is
 *   that "trial commences" when issues are framed and the
 *   first witness is examined-in-chief. Up to that point, the
 *   liberal regime applies; after that point, the strict regime
 *   applies. The applicant should always try to file the
 *   amendment application before the first witness goes into
 *   the box, because the difference in the legal test is
 *   enormous.
 *
 * THE LIMITS ON AMENDMENT:
 *
 *   Even in the liberal phase, there are some limits on what
 *   amendments will be allowed. The Supreme Court has held that
 *   the following kinds of amendments will not normally be
 *   permitted: amendments that would cause the suit to become
 *   barred by limitation (a plaintiff cannot use an amendment
 *   to introduce a fresh cause of action that would have been
 *   time-barred if filed independently), amendments that
 *   completely change the nature of the suit (you cannot turn a
 *   suit for specific performance into a suit for damages, or a
 *   suit for declaration into a suit for injunction), amendments
 *   that withdraw an admission previously made (because that
 *   would prejudice the other side), and amendments that are
 *   sought mala fide for the purpose of delay or harassment.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at the case header. The application uses the
 *   "IN" format that signals an interlocutory application
 *   within an existing parent suit, just like the Temporary
 *   Injunction IA at Template 22 and the Indigent Person
 *   Application at Template 33.
 *
 *   Second, look at the dual labelling of the parties. The
 *   applicant retains their original role in the suit
 *   (plaintiff or defendant) plus their current role in the
 *   application (applicant). This dual labelling is the same
 *   pattern you have seen in every interlocutory application
 *   so far.
 *
 *   Third, look at how the body is structured. Paragraph 1
 *   identifies the underlying suit. Paragraphs 2 and 3 explain
 *   why the amendment is needed and what it consists of.
 *   Paragraph 4 sets out the proposed amendment in precise
 *   terms. Paragraph 5 addresses the timing issue (whether the
 *   trial has commenced). Paragraph 6 invokes the principle
 *   that amendments should be liberally allowed.
 *
 *   Fourth, look at the prayer. Notice that the applicant must
 *   pray for two distinct reliefs: leave to amend the existing
 *   pleading, and permission to file a fresh amended pleading
 *   incorporating the changes. Without the second prayer, the
 *   applicant would have permission to amend but no formal
 *   document showing the amendments in their final form.
 */

const {
  Document, Paragraph, TextRun,
  AlignmentType, TabStopType, TabStopPosition,
  Footer, PageNumber, LevelFormat
} = require("docx");

function legalPara(children, opts = {}) {
  return new Paragraph({
    spacing: { after: 120, line: 360 },
    alignment: AlignmentType.JUSTIFIED, ...opts, children,
  });
}
function centeredBold(text, size = 24) {
  return new Paragraph({
    alignment: AlignmentType.CENTER, spacing: { after: 60 },
    children: [new TextRun({ text, bold: true, size, font: "Times New Roman" })],
  });
}
