/**
 * REPRESENTATION FOR PRE-GRANT OPPOSITION TO PATENT
 * UNDER SECTION 25(1) OF THE PATENTS ACT, 1970
 * ──────────────────────────────────────────────────────
 * Category : Intellectual Property — Patent Challenge
 * Forum    : Controller of Patents
 * Statute  : Section 25(1), Patents Act, 1970
 * Form     : Form 7A prescribed under the Patents Rules, 2003
 * Source   : Standard form used in Indian patent practice
 *
 * The Patent Pre-Grant Opposition under Section 25(1) of the
 * Patents Act, 1970 is the third intellectual property template
 * in your library and it introduces the entirely separate
 * procedural framework that governs patent challenges in India.
 * To grasp where the pre-grant opposition fits, you have to
 * understand the basic life cycle of a patent application in
 * India and the unique two-tiered opposition system that the
 * Patents Act provides.
 *
 * THE LIFE CYCLE OF A PATENT APPLICATION:
 *
 *   The grant of a patent in India follows a sequence of
 *   steps that runs over several years. The first step is the
 *   filing of the patent application by the applicant, which
 *   includes the title of the invention, the complete
 *   specification (including the description of the invention,
 *   the drawings if any, and the claims), the abstract, and
 *   various procedural forms. The second step is the
 *   publication of the application in the Patent Office
 *   Journal, which typically occurs eighteen months from the
 *   date of filing or earlier on request by the applicant.
 *   Publication makes the patent application public for the
 *   first time and triggers the period during which third
 *   parties can file pre-grant oppositions. The third step is
 *   the request for examination, which the applicant must
 *   file within forty-eight months of the priority date if
 *   the application is to be examined at all. The fourth step
 *   is the substantive examination by an examiner of patents,
 *   who checks whether the application meets the requirements
 *   of patentability under the Act. If the examiner has any
 *   objections, an examination report (called the First
 *   Examination Report or FER) is issued and the applicant
 *   is given an opportunity to respond. The fifth step, if
 *   the application is found to be in order for grant, is
 *   the actual grant of the patent and its publication in
 *   the Patent Office Journal as a granted patent.
 *
 *   The pre-grant opposition fits between the publication of
 *   the application and the actual grant of the patent. The
 *   post-grant opposition, which is governed by Section 25(2)
 *   of the Act, fits in the year after the grant of the
 *   patent.
 *
 * THE TWO-TIERED OPPOSITION SYSTEM:
 *
 *   The Patents Act provides a unique two-tiered opposition
 *   system that gives third parties two separate windows
 *   within which to challenge a patent application. The
 *   first window is the PRE-GRANT OPPOSITION under Section
 *   25(1), which can be filed at any time after the
 *   publication of the application but before the grant of
 *   the patent. The pre-grant opposition is filed in the
 *   form of a "representation" rather than a formal
 *   opposition petition, and it is decided by the Controller
 *   of Patents in a streamlined procedure. The second window
 *   is the POST-GRANT OPPOSITION under Section 25(2), which
 *   can be filed by any "interested person" (a narrower
 *   class than the pre-grant opposition) within one year of
 *   the grant of the patent. The post-grant opposition is a
 *   more formal proceeding decided by an Opposition Board.
 *
 *   This two-tiered system is unique among major patent
 *   jurisdictions and reflects the Indian policy of being
 *   particularly cautious about the grant of patents,
 *   especially in fields like pharmaceuticals where the
 *   balance between innovation and access has been a
 *   contentious issue.
 *
 * THE STANDING TO FILE A PRE-GRANT OPPOSITION:
 *
 *   Section 25(1) of the Act provides that a representation
 *   for opposition can be filed by "any person." This is a
 *   remarkably wide formulation that does not require the
 *   opponent to have any particular standing or any direct
 *   commercial interest in the matter. In theory, anyone in
 *   the world can file a pre-grant opposition against any
 *   patent application in India, and the Indian courts have
 *   interpreted this provision liberally to encourage public
 *   participation in patent challenges. In practice,
 *   pre-grant oppositions are filed by competitors,
 *   public-interest groups, generic drug manufacturers,
 *   and other parties who have an interest in keeping the
 *   subject matter of the patent application in the public
 *   domain.
 *
 * THE GROUNDS FOR PRE-GRANT OPPOSITION:
 *
 *   Section 25(1) of the Act sets out eleven specific grounds
 *   on which a pre-grant opposition can be filed. These
 *   grounds are exhaustive, and a pre-grant opposition that
 *   does not fall within one of the eleven grounds must be
 *   rejected regardless of how meritorious it may otherwise
 *   appear.
 *
 *   The eleven grounds, summarised in plain language, are
 *   as follows. First, that the alleged invention was
 *   wrongfully obtained from the opponent or any other
 *   person. Second, that the alleged invention was published
 *   before the priority date in any specification or other
 *   document in any country. Third, that the alleged
 *   invention was claimed in an earlier patent application
 *   in India that has an earlier priority date. Fourth, that
 *   the alleged invention was publicly known or publicly
 *   used in India before the priority date (the "public
 *   knowledge or use" ground). Fifth, that the alleged
 *   invention is obvious and does not involve any inventive
 *   step (the "obviousness" or "lack of inventive step"
 *   ground). Sixth, that the subject matter of the
 *   application is not an invention within the meaning of
 *   the Act, or is an invention that is not patentable under
 *   Section 3 or Section 4 of the Act. Seventh, that the
 *   complete specification does not sufficiently and clearly
 *   describe the invention or the method by which it is to
 *   be performed (the "insufficiency of disclosure" ground).
 *   Eighth, that the applicant has failed to disclose the
 *   information required by Section 8 of the Act regarding
 *   foreign patent applications. Ninth, that in the case of
 *   a convention application, the application was not made
 *   within twelve months of the foreign filing. Tenth, that
 *   the complete specification does not disclose or wrongly
 *   mentions the source or geographical origin of biological
 *   material used for the invention. Eleventh, that the
 *   invention was anticipated having regard to the knowledge,
 *   oral or otherwise, available within any local or
 *   indigenous community in India or elsewhere (the
 *   "traditional knowledge" ground).
 *
 *   The most heavily litigated grounds in Indian patent
 *   practice are the obviousness ground and the
 *   non-patentability ground under Section 3, particularly
 *   Section 3(d), which excludes from patentability the mere
 *   discovery of a new form of a known substance unless it
 *   results in a significant enhancement of efficacy. This
 *   provision was famously invoked by the Supreme Court in
 *   Novartis AG v. Union of India (2013) to deny patent
 *   protection to a new form of an existing cancer drug,
 *   and it has become a central feature of Indian patent
 *   policy.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at the form. The pre-grant opposition is
 *   filed as a "representation" in Form 7A prescribed under
 *   Rule 55(1A) of the Patents Rules, 2003. This is a less
 *   formal document than the post-grant opposition, which
 *   uses Form 7.
 *
 *   Second, look at the structure. The representation has a
 *   header identifying the patent application being opposed
 *   and the opponent, followed by a statement of the grounds
 *   of opposition organised under separate headings for each
 *   of the Section 25(1) grounds being relied upon, followed
 *   by a prayer asking the Controller to refuse the grant of
 *   the patent.
 *
 *   Third, look at the technical detail. Patent oppositions
 *   require a much higher level of technical detail than
 *   trademark or copyright disputes because the issues
 *   typically turn on the technical content of the invention
 *   and the prior art. A well-drafted pre-grant opposition
 *   will identify specific items of prior art and will
 *   explain in technical terms how those items disclose or
 *   render obvious the subject matter of the impugned
 *   application.
 */

