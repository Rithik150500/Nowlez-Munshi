/**
 * NOTICE UNDER SECTION 13(2) OF THE SECURITISATION AND
 * RECONSTRUCTION OF FINANCIAL ASSETS AND ENFORCEMENT
 * OF SECURITY INTEREST ACT, 2002 (SARFAESI ACT)
 * ──────────────────────────────────────────────────────
 * Category : Banking Law — Security Enforcement Notice
 * Statute  : Section 13(2), SARFAESI Act, 2002
 * Issuer   : Authorised Officer of the Secured Creditor (Bank)
 * Source   : Standard form used in Indian banking practice
 *
 * The Notice under Section 13(2) of the SARFAESI Act is the
 * fourth template in the debt recovery and insolvency batch and
 * it introduces an entirely separate statutory regime that
 * differs in fundamental ways from both the IBC route covered by
 * Templates 61 to 63 and the DRT route that you will see at
 * Template 65. To grasp where SARFAESI fits in the Indian debt
 * recovery framework, you have to understand the historical
 * problem that the Act was designed to solve.
 *
 * THE PROBLEM SARFAESI WAS DESIGNED TO SOLVE:
 *
 *   Before 2002, Indian banks faced a serious problem when
 *   borrowers defaulted on their loans. Even when the loan was
 *   secured by mortgaged property, the bank could not simply
 *   take possession of the property and sell it to recover the
 *   debt. The bank had to file a suit in a civil court (or
 *   later, in the Debt Recovery Tribunal), prove the debt and
 *   the default, obtain a decree, and then go through the
 *   tortuous process of executing the decree against the
 *   secured property. This entire process could take ten years
 *   or more, during which the borrower could continue to use
 *   the property and the bank's loan portfolio would
 *   accumulate non-performing assets. The result was a
 *   banking system burdened with vast amounts of bad debt
 *   that it could not effectively recover.
 *
 *   The Securitisation and Reconstruction of Financial Assets
 *   and Enforcement of Security Interest Act, 2002, almost
 *   universally called the SARFAESI Act, was enacted to give
 *   secured creditors a much faster and more effective
 *   mechanism for enforcing their security interests. The Act
 *   allows certain categories of secured creditors,
 *   principally banks and financial institutions, to enforce
 *   their security interests WITHOUT THE INTERVENTION OF ANY
 *   COURT OR TRIBUNAL. The bank can take possession of the
 *   secured property, sell it, and apply the proceeds towards
 *   the satisfaction of the debt, all through its own internal
 *   processes and without having to file any litigation. This
 *   was a revolutionary change that significantly improved the
 *   ability of Indian banks to recover their dues.
 *
 * THE STATUTORY MECHANISM:
 *
 *   The SARFAESI mechanism operates through a sequence of
 *   steps that are carefully prescribed by the Act. The first
 *   step is the classification of the borrower's account as a
 *   Non-Performing Asset (NPA) in accordance with the
 *   prudential norms of the Reserve Bank of India. An account
 *   becomes an NPA when the borrower has been in default of
 *   payment for ninety days or more.
 *
 *   The second step, which is what this template covers, is
 *   the issuance of a notice under Section 13(2) of the Act.
 *   The notice is issued by an Authorised Officer of the
 *   secured creditor (typically a designated officer of the
 *   bank) to the borrower, calling upon the borrower to
 *   discharge the entire outstanding liability within SIXTY
 *   DAYS from the date of the notice. The notice must
 *   identify the secured asset against which the bank intends
 *   to take action and must provide a detailed account of the
 *   amount due.
 *
 *   The third step, if the borrower fails to discharge the
 *   liability within the sixty-day period, is the taking of
 *   measures under Section 13(4) of the Act. These measures
 *   can include taking possession of the secured asset,
 *   taking over the management of the borrower's business,
 *   appointing a manager to administer the secured asset, and
 *   eventually selling or transferring the secured asset to a
 *   third party purchaser.
 *
 *   The fourth step is the disposal of the secured asset
 *   through public auction or private sale, with the proceeds
 *   being applied towards the satisfaction of the debt. Any
 *   surplus is returned to the borrower, and any deficit
 *   remains as a personal liability of the borrower that can
 *   be recovered through other proceedings.
 *
 * THE BORROWER'S RIGHTS:
 *
 *   The SARFAESI Act gives borrowers several important rights
 *   that you should be aware of, even when drafting a notice
 *   on behalf of the bank, because these rights determine the
 *   limits of what the bank can actually do.
 *
 *   First, under Section 13(3A) of the Act, the borrower can
 *   make a written representation or objection to the
 *   Authorised Officer within the sixty-day notice period.
 *   The Authorised Officer is required to consider the
 *   representation and to communicate the reasons for not
 *   accepting it within fifteen days. This is an important
 *   procedural safeguard that gives the borrower a meaningful
 *   opportunity to challenge the bank's claim before the
 *   bank takes possession.
 *
 *   Second, the borrower can file an appeal under Section 17
 *   of the Act before the Debt Recovery Tribunal within
 *   forty-five days from the date on which the bank takes any
 *   measure under Section 13(4). This appeal allows the
 *   borrower to challenge the bank's actions on grounds such
 *   as the absence of a valid security interest, the absence
 *   of any default, or procedural irregularities in the
 *   issuance of the Section 13(2) notice.
 *
 *   Third, the borrower can approach the High Court under
 *   Article 226 of the Constitution if there are exceptional
 *   circumstances such as gross procedural unfairness or
 *   violation of fundamental rights. The Supreme Court has
 *   held in cases like Mardia Chemicals Ltd. v. Union of
 *   India that the SARFAESI mechanism is constitutionally
 *   valid but that the courts retain a supervisory
 *   jurisdiction in cases of manifest illegality.
 *
 * COMPARISON WITH THE IBC ROUTE:
 *
 *   The SARFAESI route differs from the IBC route in several
 *   important respects that you should understand. First,
 *   SARFAESI is available only to SECURED creditors (banks
 *   and financial institutions with a registered security
 *   interest), while the IBC is available to both secured
 *   and unsecured creditors as well as to operational
 *   creditors. Second, SARFAESI is faster because it does
 *   not require any court or tribunal proceeding before
 *   action can be taken, while the IBC requires admission by
 *   the NCLT. Third, SARFAESI is asset-specific because it
 *   only deals with the particular secured asset, while the
 *   IBC initiates a process that affects the entire corporate
 *   debtor and all its creditors. Fourth, SARFAESI is silent
 *   on the question of resolution because its sole purpose
 *   is to allow the bank to recover its dues, while the IBC
 *   prioritises resolution as a going concern over liquidation.
 *
 *   Banks typically use both routes in parallel for major
 *   defaults, with SARFAESI providing immediate enforcement
 *   against the secured assets and the IBC providing the
 *   broader option of corporate insolvency if the SARFAESI
 *   recovery is insufficient.
 *
 * THE STRUCTURAL FEATURES OF THIS TEMPLATE:
 *
 *   Notice that this template is a NOTICE rather than a court
 *   pleading. It is issued by the bank directly to the
 *   borrower and does not require any court approval. The
 *   notice is issued by an "Authorised Officer" of the bank,
 *   who is a designated officer empowered under the SARFAESI
 *   Act to take enforcement action on behalf of the bank.
 *   The notice must be served on the borrower in the manner
 *   prescribed by the Security Interest (Enforcement) Rules,
 *   2002, which include service by registered post, courier,
 *   email, and affixation at the borrower's last known
 *   address.
 */

