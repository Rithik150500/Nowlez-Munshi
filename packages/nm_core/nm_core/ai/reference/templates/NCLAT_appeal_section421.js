/**
 * APPEAL TO THE NATIONAL COMPANY LAW APPELLATE TRIBUNAL
 * (NCLAT) UNDER SECTION 421 OF THE COMPANIES ACT, 2013
 * ──────────────────────────────────────────────────────
 * Category : Corporate Law / Insolvency — Appellate Tribunal
 * Forum    : National Company Law Appellate Tribunal (NCLAT)
 * Statute  : Section 421, Companies Act, 2013;
 *            Section 61, Insolvency and Bankruptcy Code, 2016
 * Source   : Standard form used in Indian corporate practice
 *
 * The Appeal to the National Company Law Appellate Tribunal
 * under Section 421 of the Companies Act, 2013 is the second
 * template in the specialised tribunals batch and it provides
 * the appellate tier that sits above the NCLT templates at 62,
 * 63, and 70 in your library. To grasp where this template fits,
 * you need to understand the relationship between the NCLT and
 * the NCLAT and the broader appellate hierarchy that connects
 * them to the Supreme Court.
 *
 * THE NCLT-NCLAT-SUPREME COURT HIERARCHY:
 *
 *   The Companies Act, 2013 created a unified two-tier
 *   tribunal system for company law and insolvency matters
 *   that replaced the older system in which company law
 *   disputes went to the High Courts and the Company Law
 *   Board. The first tier is the National Company Law
 *   Tribunal, which is the trial-level tribunal that hears
 *   company petitions, IBC applications, and other matters
 *   of original jurisdiction. The second tier is the
 *   National Company Law Appellate Tribunal, which hears
 *   appeals against orders of the NCLT under Section 421 of
 *   the Companies Act and under Section 61 of the IBC. The
 *   third and final tier is the Supreme Court, which hears
 *   appeals against orders of the NCLAT under Section 423
 *   of the Companies Act and under Section 62 of the IBC,
 *   but only on questions of law.
 *
 *   This three-tier structure means that any company law or
 *   insolvency dispute can potentially go through three
 *   levels of adjudication, and the choice of strategy at
 *   each level can have profound consequences for the
 *   eventual outcome. Unlike the four-tier income tax
 *   system that you saw at Templates 69 and 76, the company
 *   law and insolvency hierarchy is more compressed and
 *   does not include any High Court intermediate tier.
 *
 * THE TWO PARALLEL JURISDICTIONS OF NCLAT:
 *
 *   The most important conceptual feature of the NCLAT that
 *   you need to understand is that it has two parallel
 *   jurisdictions that operate under different statutory
 *   provisions and have different procedural rules. The
 *   first is the Companies Act jurisdiction, which is
 *   governed by Section 421 of the Companies Act, 2013 and
 *   which covers appeals against orders of the NCLT in
 *   matters relating to company law (such as oppression and
 *   mismanagement under Section 241 of the Act, mergers and
 *   amalgamations under Sections 230-232, reduction of
 *   capital under Section 66, and so on). The second is the
 *   IBC jurisdiction, which is governed by Section 61 of the
 *   Insolvency and Bankruptcy Code, 2016 and which covers
 *   appeals against orders of the NCLT in IBC matters (such
 *   as the admission or rejection of a Section 7, 9, or 10
 *   application, the approval or rejection of a resolution
 *   plan, the order of liquidation, and so on).
 *
 *   The two jurisdictions have different limitation periods
 *   and different procedural rules. Under Section 421(3) of
 *   the Companies Act, an appeal must be filed within
 *   forty-five days from the date on which a copy of the
 *   order of the NCLT is made available to the person
 *   aggrieved. Under Section 61(2) of the IBC, an appeal
 *   must be filed within thirty days from the date of the
 *   order of the NCLT, extendable by a further fifteen days
 *   on sufficient cause. The IBC limitation is much
 *   stricter and reflects the policy of speedy resolution
 *   that animates the entire IBC framework.
 *
 *   This template is drafted as an appeal under Section 421
 *   of the Companies Act, but you should be aware that the
 *   same template can be adapted for an IBC appeal under
 *   Section 61 by changing the statutory references and
 *   the limitation period.
 *
 * THE GROUNDS FOR APPEAL:
 *
 *   Appeals to the NCLAT can be filed on both questions of
 *   fact and questions of law, which makes the NCLAT a
 *   broader forum than the High Courts in income tax matters
 *   that you saw at Template 76 (which can interfere only
 *   on substantial questions of law). The NCLAT can
 *   re-examine the evidence, reconsider the legal issues,
 *   and reach a different conclusion on the merits if it
 *   disagrees with the NCLT. This makes the NCLAT a true
 *   appellate forum in the traditional sense, and the
 *   appellant has a real opportunity to obtain relief if the
 *   NCLT's order was wrong.
 *
 *   In practice, NCLAT appeals are most commonly based on
 *   one or more of the following grounds. First, that the
 *   NCLT has misapplied the relevant provisions of the
 *   Companies Act or the IBC. Second, that the NCLT has
 *   ignored or misappreciated the evidence on record. Third,
 *   that the NCLT has acted in violation of the principles
 *   of natural justice. Fourth, that the NCLT has acted
 *   without jurisdiction or in excess of its jurisdiction.
 *   Fifth, that the NCLT has failed to follow binding
 *   precedents.
 *
 * THE PROCEDURE BEFORE THE NCLAT:
 *
 *   The NCLAT operates from its principal bench at New
 *   Delhi, with a circuit bench at Chennai for cases from
 *   the southern region. Unlike the NCLT, which has multiple
 *   benches across India, the NCLAT is a relatively small
 *   tribunal that handles all the appeals from the entire
 *   country at one or two locations.
 *
 *   The procedure before the NCLAT is generally simpler than
 *   before a High Court but more formal than before a
 *   regulatory authority. The appeal is filed in the
 *   prescribed form with the prescribed fee and is
 *   accompanied by the impugned order, the petition or
 *   application before the NCLT, and the documents relied
 *   upon by the appellant. The NCLT is the formal
 *   respondent only in form, and the actual contest is with
 *   the opposing party from the original NCLT proceedings.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at the case header. The appeal is captioned
 *   as "Company Appeal (AT)" — the AT stands for Appellate
 *   Tribunal — which is the standard NCLAT abbreviation.
 *   For IBC appeals, the caption is "Company Appeal (AT)
 *   (Insolvency)" with the (Insolvency) suffix indicating
 *   the IBC jurisdiction.
 *
 *   Second, look at how the parties are described. The
 *   appellant is identified by both the original role in
 *   the NCLT proceedings and the current role in the
 *   appeal. This dual labelling is the same pattern you saw
 *   at Template 42 (Memorandum of Appeal), Template 54
 *   (Review), and other appellate templates in your library.
 *
 *   Third, look at the body. Paragraphs 1 and 2 establish
 *   the parties. Paragraph 3 sets out the impugned order.
 *   Paragraphs 4 and 5 narrate the proceedings before the
 *   NCLT. Paragraph 6 explains why the appellant is
 *   aggrieved. Paragraphs 7 onwards set out the grounds of
 *   appeal.
 *
 *   Fourth, look at the prayer. It typically asks the NCLAT
 *   to set aside the impugned order, to allow the original
 *   petition or to remand the matter to the NCLT for fresh
 *   consideration, and to grant interim relief pending the
 *   appeal.
 */

