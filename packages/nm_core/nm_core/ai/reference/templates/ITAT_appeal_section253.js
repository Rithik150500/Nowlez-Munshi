/**
 * APPEAL TO THE INCOME TAX APPELLATE TRIBUNAL (ITAT)
 * UNDER SECTION 253 OF THE INCOME TAX ACT, 1961
 * ─────────────────────────────────────────────────────
 * Category : Tax Law — Second Appellate Tier
 * Forum    : Income Tax Appellate Tribunal (ITAT)
 * Statute  : Section 253, Income Tax Act, 1961
 * Form     : Form 36 prescribed under Rule 47 of the Income Tax Rules, 1962
 * Source   : Standard form used in Indian tax practice
 *
 * The Appeal to the Income Tax Appellate Tribunal under Section
 * 253 is the first template in this specialised tribunals batch
 * and it completes the second tier of tax appellate practice in
 * your library. To grasp where this template fits, you should
 * recall the four-tier appellate structure of the Indian income
 * tax system that I explained when introducing the CIT(A) appeal
 * at Template 69. The first tier is the CIT(A), which hears
 * appeals against the orders of Assessing Officers under Section
 * 246A of the Act. The second tier is the ITAT, which hears
 * appeals against the orders of the CIT(A) under Section 253 of
 * the Act and which is the subject of this template. The third
 * tier is the High Court, which hears appeals from the ITAT on
 * substantial questions of law under Section 260A. And the
 * fourth tier is the Supreme Court, which can be approached
 * through a special leave petition under Article 136 of the
 * Constitution.
 *
 * THE NATURE OF THE ITAT:
 *
 *   The Income Tax Appellate Tribunal is one of the oldest and
 *   most respected tribunals in India. It was established in
 *   1941, even before the Indian Independence, and it has
 *   developed a body of jurisprudence over the decades that is
 *   often more nuanced and thoughtful than the corresponding
 *   jurisprudence of the High Courts. The ITAT is described in
 *   Section 254(1) of the Act as the "final fact-finding
 *   authority" in income tax matters, which means that the
 *   factual findings recorded by the ITAT are binding on the
 *   parties and on the higher courts unless they are perverse
 *   or unsupported by evidence. The High Court can interfere
 *   with the ITAT's order only on substantial questions of
 *   law, not on questions of fact.
 *
 *   The ITAT is composed of two kinds of members, namely
 *   judicial members (who are typically retired district
 *   judges, advocates with substantial experience, or former
 *   members of the Indian Legal Service) and accountant
 *   members (who are typically chartered accountants or
 *   former officers of the Income Tax Department with
 *   substantial experience in tax matters). The two kinds of
 *   members sit in benches of two members each, with one
 *   judicial member and one accountant member. This
 *   composition reflects the dual character of tax disputes,
 *   which involve both legal questions and accounting
 *   questions, and it ensures that both kinds of expertise
 *   are brought to bear on every case.
 *
 *   The ITAT operates through benches located in major
 *   cities across India, with each bench having jurisdiction
 *   over the areas in which the relevant Assessing Officers
 *   are located. The Delhi benches handle the largest volume
 *   of cases because of the concentration of corporate
 *   headquarters in Delhi, but every major city in India
 *   has at least one ITAT bench.
 *
 * THE ORDERS AGAINST WHICH AN ITAT APPEAL LIES:
 *
 *   Section 253 of the Act specifies the orders against which
 *   an ITAT appeal lies, and you should learn to identify
 *   them precisely because not every order of a tax authority
 *   can be appealed to the ITAT. Section 253(1) lists the
 *   appealable orders for the assessee, which include orders
 *   passed by the CIT(A) under various sections, orders
 *   passed by the Commissioner under Section 263 (revision
 *   on the ground of being prejudicial to the interests of
 *   revenue), orders passed by the Joint Commissioner under
 *   Section 271(1)(c) imposing penalty above a specified
 *   threshold, and orders passed by the Principal Chief
 *   Commissioner or Chief Commissioner under various
 *   sections. Section 253(2) provides for departmental
 *   appeals, which can be filed by the Department against
 *   orders of the CIT(A) that go against the Department.
 *
 *   The most common kind of ITAT appeal in modern practice
 *   is the appeal by the assessee against an order of the
 *   CIT(A) that has confirmed the additions made by the
 *   Assessing Officer. This template is drafted as such an
 *   assessee appeal because it is the most pedagogically
 *   useful form to learn first.
 *
 * THE LIMITATION PERIOD:
 *
 *   Section 253(3) of the Act prescribes a limitation period
 *   of sixty days from the date on which the order being
 *   appealed against is communicated to the assessee. This
 *   is somewhat longer than the thirty-day limitation period
 *   for a CIT(A) appeal at Template 69, but it is still
 *   strict, and a delayed appeal can only be entertained
 *   with the condonation of the ITAT under Section 253(5).
 *   The grounds for condonation are similar to those under
 *   the general condonation of delay provision at Template
 *   48, namely sufficient cause. Unlike the limitation under
 *   Section 34 of the Arbitration Act that you saw at
 *   Template 52, the ITAT has discretion to condone delay
 *   in appropriate cases.
 *
 * THE FILING FEE:
 *
 *   The filing fee for an ITAT appeal is much higher than
 *   for a CIT(A) appeal and is structured as a percentage of
 *   the assessed income or the disputed tax demand. The
 *   detailed fee structure is set out in Section 253(6) of
 *   the Act, but the broad principle is that the higher the
 *   stakes, the higher the fee. This is meant to discourage
 *   frivolous appeals while still allowing genuine appeals
 *   to proceed. The fee can be a substantial amount in
 *   high-stakes corporate cases, sometimes running into
 *   lakhs of rupees.
 *
 * THE PROCEDURE BEFORE THE ITAT:
 *
 *   The ITAT follows a hybrid procedure that is partly
 *   judicial and partly administrative. The appeal is filed
 *   in Form 36 prescribed under Rule 47 of the Income Tax
 *   Rules, 1962, and it must be accompanied by certified
 *   copies of the impugned order, the assessment order, and
 *   any other relevant documents. The Department is given
 *   notice of the appeal and is required to file its
 *   response. The matter is then listed for hearing before
 *   a bench, and both sides make oral arguments supported
 *   by written submissions. The ITAT typically delivers its
 *   order within a few weeks of the hearing, although in
 *   complex cases the order may take longer.
 *
 *   One distinctive feature of the ITAT procedure is the
 *   "paper book" requirement, under which the appellant is
 *   required to file a paper book containing the relevant
 *   documents and case law that the appellant intends to
 *   rely upon. The paper book is filed in advance of the
 *   hearing and is intended to assist the bench in
 *   understanding the case quickly. A well-prepared paper
 *   book is essential to a successful ITAT appeal.
 *
 * STRUCTURAL FEATURES OF THIS TEMPLATE:
 *
 *   First, look at the form. The ITAT appeal is filed in
 *   Form 36 prescribed under Rule 47 of the Income Tax
 *   Rules, 1962. The form has specific blanks for the
 *   appellant's details, the impugned order, the grounds
 *   of appeal, and the relief claimed.
 *
 *   Second, look at the structure of the grounds. The
 *   grounds are organised as numbered paragraphs, with each
 *   ground addressing a specific objection to the impugned
 *   order. Unlike the CIT(A) appeal at Template 69, the
 *   grounds in an ITAT appeal must address the order of the
 *   CIT(A) rather than the original assessment order, so
 *   the language should focus on the errors of the CIT(A)
 *   rather than the errors of the Assessing Officer.
 *
 *   Third, look at the relief claimed. The relief asks the
 *   ITAT to set aside the order of the CIT(A) and to
 *   restore the assessment to the position originally
 *   claimed by the assessee, or in the alternative to
 *   remand the matter to the Assessing Officer or the
 *   CIT(A) for fresh consideration.
 */

