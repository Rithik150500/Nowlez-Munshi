/**
 * ELECTION PETITION UNDER SECTION 81 OF THE
 * REPRESENTATION OF THE PEOPLE ACT, 1951
 * ─────────────────────────────────────────────────────
 * Category : Election Law / Public Law
 * Court    : High Court (under Section 80A of the RP Act)
 * Statute  : Sections 80, 80A, 81, 82, 83, 100, and 101 of
 *            the Representation of the People Act, 1951;
 *            Article 329(b), Constitution of India
 * Source   : Standard form developed in Indian election law
 *            practice
 *
 * ── A note on Template 100 ──
 *
 * This is the hundredth template in your library, and I have
 * deliberately chosen the Election Petition for this milestone
 * because it touches on the constitutional foundations of
 * representative democracy in India and because it represents
 * one of the most distinctive forms of public law litigation
 * in the entire common law world. Indian election law has
 * developed over more than seven decades into a substantial
 * body of jurisprudence that integrates constitutional law,
 * statutory interpretation, evidence law, and public law
 * doctrine in a way that few other areas of practice do. The
 * election petition is the procedural vehicle through which
 * this jurisprudence is created, and learning to draft one
 * gives you access to the practical mechanics of Indian
 * representative democracy.
 *
 * THE CONSTITUTIONAL FOUNDATION OF ELECTION DISPUTES:
 *
 *   Indian election law is built on the constitutional
 *   foundation of Article 329(b), which is one of the most
 *   important and most distinctive provisions of the
 *   Constitution of India in matters of public law. Article
 *   329(b) provides that no election to either House of
 *   Parliament or to either House of the Legislature of a
 *   State shall be called in question except by an election
 *   petition presented to such authority and in such manner
 *   as may be provided for by or under any law made by the
 *   appropriate Legislature. The constitutional purpose of
 *   Article 329(b) is to ensure that election disputes are
 *   resolved through a specialised statutory mechanism that
 *   produces final and binding determinations within a
 *   reasonable time, rather than being subject to the
 *   ordinary processes of civil litigation that might delay
 *   the resolution of such disputes for years and that
 *   might leave the validity of the election in a state of
 *   prolonged uncertainty.
 *
 *   Article 329(b) has the important consequence of ousting
 *   the ordinary jurisdiction of the civil courts in
 *   election matters and of channelling all election
 *   disputes into the specialised statutory mechanism. This
 *   means that an election dispute cannot be raised by way
 *   of an ordinary civil suit, by way of a writ petition
 *   under Article 226, or by any other mode except an
 *   election petition under the relevant statute. The
 *   Supreme Court has repeatedly emphasised this exclusivity
 *   in cases like N.P. Ponnuswami versus Returning Officer
 *   (1952) and Mohinder Singh Gill versus Chief Election
 *   Commissioner (1978), and has held that Article 329(b)
 *   creates a complete and self-contained code for the
 *   resolution of election disputes.
 *
 * THE STATUTORY FRAMEWORK UNDER THE RP ACT:
 *
 *   The statutory framework for election petitions
 *   contemplated by Article 329(b) is set out in Part VI
 *   of the Representation of the People Act, 1951, which
 *   covers Sections 80 to 122 of the Act. The framework
 *   has several distinctive features that you should
 *   understand. First, under Section 80, an election can
 *   be called in question only by an election petition
 *   presented in accordance with the provisions of Part
 *   VI. Second, under Section 80A, the High Court within
 *   whose jurisdiction the election was held has exclusive
 *   jurisdiction to try election petitions, with appeals
 *   lying directly to the Supreme Court under Section 116A.
 *   Third, under Section 81, an election petition can be
 *   presented by any candidate at the election or any
 *   elector of the constituency, and must be presented
 *   within forty-five days from the date of the election
 *   of the returned candidate (or, if the returned
 *   candidate is more than one, the later of the two
 *   dates).
 *
 *   The forty-five day limitation period is mandatory and
 *   cannot be extended by the court even on the ground of
 *   sufficient cause. This is one of the most rigorous
 *   limitation periods in Indian law and reflects the
 *   constitutional policy of expeditious resolution of
 *   election disputes. Election petitions filed beyond
 *   forty-five days are liable to be summarily dismissed
 *   under Section 86 without any inquiry into the merits.
 *
 * THE GROUNDS FOR DECLARING AN ELECTION VOID:
 *
 *   The grounds on which an election can be declared void
 *   are set out in Section 100 of the Act, which provides
 *   that the High Court shall declare the election of the
 *   returned candidate to be void if the High Court is of
 *   the opinion that any of the following grounds is made
 *   out. First, that on the date of the election the
 *   returned candidate was not qualified or was disqualified
 *   to be chosen to fill the seat under the Constitution or
 *   any of the relevant Acts. Second, that any corrupt
 *   practice has been committed by the returned candidate or
 *   his election agent or by any other person with the
 *   consent of the returned candidate or his election agent.
 *   Third, that any nomination has been improperly rejected.
 *   Fourth, that the result of the election in so far as it
 *   concerns the returned candidate has been materially
 *   affected by the improper acceptance of any nomination,
 *   or by any corrupt practice committed in the interests of
 *   the returned candidate by any person not falling within
 *   the second ground, or by the improper reception, refusal,
 *   or rejection of any vote, or the reception of any vote
 *   that is void, or by any non-compliance with the
 *   provisions of the Constitution or of the Act or of any
 *   rules or orders made under the Act.
 *
 *   The "corrupt practices" referred to in Section 100 are
 *   defined in Section 123 of the Act and include bribery,
 *   undue influence, the appeal to vote on grounds of
 *   religion or caste or community or language, the
 *   promotion of feelings of enmity or hatred between
 *   different classes of citizens, the publication of false
 *   statements about the personal character of a candidate,
 *   the hiring of vehicles to convey voters to polling
 *   stations, the incurring of election expenditure in
 *   excess of the prescribed limit, and the obtaining or
 *   procurement of assistance from any government officer.
 *
 * THE STRUCTURAL FEATURES OF AN ELECTION PETITION:
 *
 *   An election petition has several distinctive structural
 *   features that you should understand and that distinguish
 *   it from the other forms of public law litigation in
 *   your library. First, the petition must comply with the
 *   formal requirements of Section 81, including the
 *   requirement that the petitioner must sign the petition
 *   and verify it in the manner laid down in the Code of
 *   Civil Procedure for the verification of pleadings, and
 *   the requirement that the petition must be supported by
 *   an affidavit setting out the corrupt practices alleged
 *   in the petition.
 *
 *   Second, under Section 82 of the Act, certain persons
 *   must be joined as respondents to the petition. The
 *   returned candidate must always be joined. If the
 *   petition contains allegations of corrupt practices by
 *   any other candidate, that other candidate must also be
 *   joined. The failure to join all the necessary
 *   respondents is fatal to the petition and results in
 *   summary dismissal under Section 86.
 *
 *   Third, under Section 83 of the Act, the petition must
 *   contain a concise statement of the material facts on
 *   which the petitioner relies, set forth full particulars
 *   of any corrupt practice alleged including the names of
 *   the parties alleged to have committed such corrupt
 *   practice and the date and place of the commission of
 *   each such practice, and be signed by the petitioner and
 *   verified in the manner laid down in the Code of Civil
 *   Procedure. The requirement of "full particulars" of
 *   corrupt practices is strictly enforced and is one of
 *   the most common grounds for the summary dismissal of
 *   election petitions.
 *
 *   Fourth, under Section 86, the High Court shall dismiss
 *   an election petition that does not comply with the
 *   provisions of Sections 81, 82, or 117. This is a
 *   mandatory dismissal and the High Court has no
 *   discretion to waive the procedural requirements. As a
 *   result, the drafting of an election petition requires
 *   particular care and attention to the procedural
 *   formalities, because even minor procedural defects can
 *   result in the loss of the entire substantive claim.
 *
 *   Fifth, the relief sought in an election petition is
 *   typically a declaration that the election of the
 *   returned candidate is void under Section 100, and
 *   sometimes a further declaration that the petitioner or
 *   another candidate is the duly elected candidate under
 *   Section 101. The latter relief is available only in
 *   limited circumstances and requires proof that but for
 *   the votes obtained by the returned candidate by corrupt
 *   practices or otherwise, the petitioner or another
 *   candidate would have been elected.
 */

