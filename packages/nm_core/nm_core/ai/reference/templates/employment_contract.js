/**
 * EMPLOYMENT CONTRACT
 * ──────────────────────
 * Category : Commercial Drafting — Employment Relationship
 * Type     : Bilateral employer-employee agreement
 * Statute  : Indian Contract Act, 1872; Code on Wages, 2019;
 *            Industrial Relations Code, 2020; OSH Code, 2020;
 *            Code on Social Security, 2020; Shops and
 *            Establishments Acts (state-specific)
 * Source   : Standard form used in Indian commercial practice
 *
 * The Employment Contract is the third commercial drafting
 * template in your library and it introduces a relationship
 * that is fundamentally different from anything you have
 * encountered so far. All the previous templates in your
 * library involve relationships between equals: parties to a
 * lawsuit, parties to a sale, parties to a partnership, parties
 * to an arbitration. The employment relationship, by contrast,
 * is hierarchical. One party (the employer) has authority to
 * direct the work of the other party (the employee), and the
 * employee is under an obligation to follow the employer's
 * lawful directions. This vertical structure has important
 * legal consequences that distinguish employment law from
 * ordinary contract law and that you should understand before
 * you begin drafting.
 *
 * THE LABOUR LAW FRAMEWORK:
 *
 *   Indian employment law is one of the most heavily regulated
 *   areas of Indian commercial law. Until very recently, the
 *   framework consisted of dozens of separate statutes that had
 *   accumulated over the decades, creating an enormously
 *   complex regulatory environment. The most important of
 *   these older statutes were the Industrial Disputes Act,
 *   1947, which protected the security of employment for
 *   workmen; the Payment of Wages Act, 1936, which regulated
 *   the timing and method of wage payment; the Minimum Wages
 *   Act, 1948, which established floor wages; the Employees'
 *   Provident Funds and Miscellaneous Provisions Act, 1952,
 *   which provided for retirement benefits; the Payment of
 *   Gratuity Act, 1972, which provided for terminal benefits;
 *   the Maternity Benefit Act, 1961, which provided for
 *   maternity leave; and the Factories Act, 1948, which
 *   regulated working conditions. In addition, each state had
 *   its own Shops and Establishments Act regulating
 *   non-factory workplaces.
 *
 *   In 2019 and 2020, the Government of India consolidated
 *   most of these statutes into four labour codes that are
 *   intended to simplify the regulatory framework. The Code
 *   on Wages, 2019 consolidates the laws on wages and bonus
 *   payments. The Industrial Relations Code, 2020 consolidates
 *   the laws on trade unions, industrial disputes, and
 *   employment security. The Occupational Safety, Health and
 *   Working Conditions Code, 2020 consolidates the laws on
 *   safety and working conditions. And the Code on Social
 *   Security, 2020 consolidates the laws on retirement and
 *   welfare benefits. These codes have been notified but their
 *   implementation has been delayed pending the framing of
 *   rules and the resolution of various political issues, so
 *   in many states the older statutes continue to apply in
 *   practice. A well-drafted employment contract should refer
 *   to both the older and newer regimes wherever relevant.
 *
 * THE EMPLOYMENT vs INDEPENDENT CONTRACTOR DISTINCTION:
 *
 *   The most important threshold question in any employment
 *   case is whether the relationship in question is actually
 *   an employment relationship or whether it is an independent
 *   contractor relationship governed by an entirely different
 *   legal regime. This distinction matters enormously because
 *   employees enjoy a wide range of statutory protections that
 *   do not apply to independent contractors. Employees are
 *   entitled to provident fund contributions, gratuity,
 *   maternity benefits, statutory bonus, and a host of other
 *   benefits. They cannot be terminated without cause if they
 *   qualify as workmen under the Industrial Disputes Act. They
 *   are protected by minimum wage laws and by working hours
 *   regulations. Independent contractors, by contrast, are
 *   simply parties to a commercial contract for services and
 *   do not enjoy any of these protections.
 *
 *   The Indian courts use a multi-factor test to distinguish
 *   the two categories. The most important factor is the
 *   degree of control that the principal exercises over the
 *   manner in which the work is done. An employer typically
 *   directs not just what work is to be done but also how,
 *   when, and where it is done. An independent contractor is
 *   typically left free to determine the manner of performance
 *   and is judged only by the results delivered. Other factors
 *   include the integration of the worker into the principal's
 *   organisation, the provision of tools and equipment, the
 *   sharing of profits and risks, and the exclusivity of the
 *   arrangement.
 *
 *   This template is a true employment contract. Template 60
 *   in this batch will give you the parallel Service Agreement
 *   for an independent contractor relationship, and the
 *   contrast between the two templates will help you see the
 *   distinction in concrete terms.
 *
 * THE NON-COMPETE PROBLEM:
 *
 *   One of the most distinctive features of Indian employment
 *   law is the limited enforceability of post-employment
 *   restrictive covenants. Section 27 of the Indian Contract
 *   Act provides that every agreement by which any one is
 *   restrained from exercising a lawful profession, trade or
 *   business of any kind is to that extent void. This
 *   provision has been interpreted very strictly by Indian
 *   courts in the employment context. The general rule is
 *   that non-compete clauses that operate after the
 *   termination of employment are unenforceable, regardless
 *   of how reasonable they may appear in scope or duration.
 *   The Supreme Court has reaffirmed this position in several
 *   cases, most notably Niranjan Shankar Golikari v. Century
 *   Spinning Co., where it held that restrictive covenants
 *   operating during employment are valid but those operating
 *   after employment are void.
 *
 *   This is a striking departure from the position in most
 *   common law jurisdictions where reasonable post-employment
 *   non-compete clauses are routinely enforced. Indian
 *   employers therefore have to rely on other mechanisms to
 *   protect their interests after an employee leaves, such as
 *   confidentiality clauses (which protect specific
 *   information rather than competition generally),
 *   non-solicitation clauses (which prohibit poaching of
 *   customers and employees rather than competition itself),
 *   and gardening leave provisions (which keep the employee
 *   on the payroll but out of the office during the notice
 *   period).
 *
 *   Notice how Template 58 reflects this constraint in
 *   Clauses 9 and 10. The confidentiality and intellectual
 *   property clauses are drafted broadly because they are
 *   enforceable. The non-solicitation clause is included
 *   because it has been upheld by some Indian courts in
 *   appropriate circumstances. But there is no broad
 *   non-compete clause because it would be unenforceable
 *   under Section 27 of the Contract Act.
 *
 * THE TERMINATION QUESTION:
 *
 *   Indian law does not recognise the doctrine of "at-will
 *   employment" that prevails in many other common law
 *   jurisdictions. An employer cannot simply terminate an
 *   employee without any reason, and the employee always has
 *   recourse to courts or labour authorities if the
 *   termination is alleged to be wrongful. The position is
 *   particularly stringent for employees who qualify as
 *   "workmen" under the Industrial Disputes Act, 1947 (now
 *   Industrial Relations Code, 2020). For workmen, termination
 *   must be for a justifiable cause, must follow a fair
 *   inquiry procedure, and may be challenged before the
 *   Labour Court. For non-workmen employees, the position is
 *   somewhat more flexible but the contract terms continue to
 *   bind the employer, and arbitrary termination can give
 *   rise to a claim for damages or specific performance.
 */

