/**
 * ORIGINAL APPLICATION TO THE NATIONAL GREEN TRIBUNAL (NGT)
 * UNDER SECTION 14 OF THE NATIONAL GREEN TRIBUNAL ACT, 2010
 * ──────────────────────────────────────────────────────────
 * Category : Environmental Law — Specialised Tribunal
 * Forum    : National Green Tribunal (NGT)
 * Statute  : Sections 14, 15, 18, National Green Tribunal Act, 2010
 * Source   : Standard form used in Indian environmental practice
 *
 * The Original Application to the National Green Tribunal under
 * Section 14 of the NGT Act, 2010 is the fifth and final
 * template in the specialised tribunals batch and it introduces
 * environmental law as an entirely separate area of Indian
 * commercial and public law that none of your existing templates
 * have covered. To grasp where this template fits, you have to
 * understand the historical development of environmental
 * jurisprudence in India and the role of the NGT within that
 * jurisprudence.
 *
 * THE EVOLUTION OF INDIAN ENVIRONMENTAL LAW:
 *
 *   Indian environmental law has developed in three distinct
 *   phases that you should understand. The first phase, from
 *   the 1970s to the early 1990s, was characterised by
 *   piecemeal legislation responding to specific environmental
 *   crises. The principal statutes from this phase are the
 *   Water (Prevention and Control of Pollution) Act, 1974
 *   (which created the Pollution Control Boards), the Air
 *   (Prevention and Control of Pollution) Act, 1981, and the
 *   Environment (Protection) Act, 1986 (which was passed in
 *   the wake of the Bhopal gas tragedy and which gave the
 *   Central Government wide powers to take measures for the
 *   protection of the environment).
 *
 *   The second phase, from the early 1990s to 2010, was
 *   characterised by the development of a body of
 *   environmental jurisprudence by the Supreme Court of India
 *   through Public Interest Litigation under Article 32 of
 *   the Constitution. The leading cases from this phase
 *   include M.C. Mehta v. Union of India (the Ganga
 *   pollution case, the Taj Mahal case, the Delhi vehicular
 *   pollution case), Vellore Citizens Welfare Forum v. Union
 *   of India (the tannery effluents case which established
 *   the precautionary principle and the polluter pays
 *   principle as part of Indian law), and Indian Council
 *   for Enviro-Legal Action v. Union of India (the
 *   Bichhri industrial pollution case). In these cases, the
 *   Supreme Court read environmental rights into Article 21
 *   of the Constitution and established a wide range of
 *   environmental principles that have shaped Indian
 *   environmental policy ever since.
 *
 *   The third phase began with the enactment of the
 *   National Green Tribunal Act, 2010, which created a
 *   specialised tribunal for the adjudication of
 *   environmental disputes. The NGT was meant to relieve
 *   the Supreme Court and the High Courts of the burden of
 *   environmental PILs and to provide a more expert and
 *   focused forum for environmental matters. The NGT has
 *   its principal bench at New Delhi and four zonal
 *   benches at Bhopal (Central Zone), Pune (Western Zone),
 *   Kolkata (Eastern Zone), and Chennai (Southern Zone).
 *
 * THE JURISDICTION OF THE NGT:
 *
 *   The NGT has both original and appellate jurisdiction
 *   under the NGT Act. The original jurisdiction is conferred
 *   by Section 14 of the Act, which gives the NGT jurisdiction
 *   over civil cases involving substantial questions relating
 *   to the environment, including the enforcement of any
 *   legal right relating to the environment, that arise out
 *   of the implementation of the enactments specified in
 *   Schedule I to the Act. Schedule I lists seven specific
 *   environmental statutes, namely the Water (Prevention and
 *   Control of Pollution) Act, 1974, the Water (Prevention
 *   and Control of Pollution) Cess Act, 1977, the Forest
 *   (Conservation) Act, 1980, the Air (Prevention and
 *   Control of Pollution) Act, 1981, the Environment
 *   (Protection) Act, 1986, the Public Liability Insurance
 *   Act, 1991, and the Biological Diversity Act, 2002.
 *
 *   The appellate jurisdiction is conferred by Section 16 of
 *   the Act, which gives the NGT jurisdiction to hear
 *   appeals against orders passed by the Pollution Control
 *   Boards, the Forest Department, and other environmental
 *   authorities under the Schedule I statutes.
 *
 *   This template focuses on the original jurisdiction
 *   under Section 14, which is the more pedagogically
 *   useful form to learn first because it teaches the basic
 *   structure of an environmental application.
 *
 * THE STANDING REQUIREMENT:
 *
 *   The NGT Act takes a generous approach to standing,
 *   similar to the PIL jurisdiction at Template 66 in your
 *   library. Section 18(2) of the Act provides that an
 *   application or appeal may be made by any person
 *   aggrieved, including any representative body or
 *   organisation. The NGT has interpreted "person aggrieved"
 *   broadly to include not just persons who have suffered
 *   direct injury but also public-spirited individuals and
 *   environmental NGOs who are concerned about the
 *   environment in general. This generous approach to
 *   standing reflects the public character of environmental
 *   rights and the recognition that environmental harm
 *   affects entire communities rather than individual
 *   victims.
 *
 * THE SUBSTANTIAL QUESTION REQUIREMENT:
 *
 *   The most important threshold issue in any NGT
 *   application is the requirement that the matter must
 *   involve a "substantial question relating to the
 *   environment." This requirement is meant to filter out
 *   trivial environmental complaints and to ensure that the
 *   NGT focuses on matters of genuine environmental
 *   importance. Section 2(m) of the NGT Act defines
 *   "substantial question relating to the environment" as
 *   including any of the following: (a) where there is a
 *   direct violation of a specific statutory environmental
 *   obligation by a person; (b) where the gravity of damage
 *   to the environment or property is substantial; or (c)
 *   where the damage to public health is broadly
 *   measurable.
 *
 *   The NGT applies this threshold somewhat flexibly in
 *   practice, and it has been generally willing to
 *   entertain applications that raise genuine environmental
 *   concerns even if the specific monetary damage cannot
 *   be precisely quantified.
 *
 * THE TIME LIMITATION:
 *
 *   Section 14(3) of the NGT Act prescribes a limitation
 *   period of six months from the date on which the cause
 *   of action first arose, with a power to extend the
 *   period by a further sixty days for sufficient cause.
 *   This is a relatively short limitation period that
 *   reflects the need for prompt environmental redress, and
 *   environmental practitioners need to be vigilant about
 *   the timing of their applications.
 *
 *   Importantly, the limitation period runs from the date
 *   when the cause of action first arose, not from the date
 *   when the applicant first became aware of the cause of
 *   action. This can make it difficult to file applications
 *   in respect of slow-developing environmental harms that
 *   only become apparent over time, and the NGT has had to
 *   develop a doctrine of "continuing cause of action" to
 *   address this difficulty in appropriate cases.
 *
 * THE WIDE POWERS OF THE NGT:
 *
 *   The NGT has wide powers under Section 15 of the Act to
 *   provide relief and compensation to victims of pollution
 *   and other environmental damage, to provide for the
 *   restitution of property, and to provide for the
 *   restitution of the environment. The NGT can also
 *   exercise the powers of a civil court under the Code of
 *   Civil Procedure, 1908 in respect of certain matters,
 *   and it can apply the principles of sustainable
 *   development, the precautionary principle, and the
 *   polluter pays principle in deciding cases under
 *   Section 20.
 *
 *   In practice, the NGT has been a remarkably activist
 *   tribunal that has issued sweeping orders affecting
 *   industries, infrastructure projects, and government
 *   policies across a wide range of areas. Its orders have
 *   shut down polluting factories, halted construction
 *   projects, banned the use of certain chemicals, and
 *   imposed significant compensation on polluters.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at the case header. The application is
 *   captioned as "Original Application No. ________ of 20__"
 *   and is filed before the principal bench of the NGT at
 *   New Delhi or before the appropriate zonal bench
 *   depending on the location of the environmental harm.
 *
 *   Second, look at the parties. The applicant is typically
 *   either a person directly affected by the environmental
 *   harm or a representative body or NGO acting in the
 *   public interest. The respondents typically include the
 *   alleged polluter (a private party) and the relevant
 *   government authorities (the Pollution Control Board,
 *   the State Government, and the Ministry of Environment,
 *   Forest and Climate Change).
 *
 *   Third, look at the body. Paragraph 1 establishes the
 *   standing of the applicant. Paragraph 2 identifies the
 *   environmental harm. Paragraph 3 establishes the
 *   substantial question relating to the environment.
 *   Paragraph 4 sets out the alleged violation of the
 *   Schedule I statutes. Paragraph 5 invokes the
 *   precautionary principle and the polluter pays
 *   principle. Paragraphs 6 and 7 deal with limitation
 *   and jurisdiction.
 */

