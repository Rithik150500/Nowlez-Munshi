/**
 * APPLICATION FOR MAINTENANCE PENDENTE LITE
 * UNDER SECTION 24 OF THE HINDU MARRIAGE ACT, 1955
 * ───────────────────────────────────────────────────
 * Category : Matrimonial Pleading — Interlocutory Application
 * Court    : Principal Judge, Family Court, Delhi
 * Statute  : Section 24, Hindu Marriage Act, 1955
 * Source   : Standard form used in Indian matrimonial practice
 *
 * The Application for Maintenance Pendente Lite under Section 24
 * of the Hindu Marriage Act addresses one of the most pressing
 * practical problems in Indian matrimonial litigation, namely
 * what happens to a dependent spouse during the years that the
 * main case takes to be decided. To appreciate why this remedy
 * is necessary, you have to think about the time-scale of Indian
 * matrimonial cases. A contested divorce petition can easily take
 * three to five years to reach a final decree, and during all that
 * time the parties are usually living apart. The dependent spouse,
 * who is typically the wife, may have no independent source of
 * income and no place to live other than her parents' house. She
 * cannot wait years for a final decree before getting any financial
 * support, and yet the court cannot order final maintenance until
 * the case is decided. Section 24 solves this problem by creating
 * a category of "maintenance pendente lite" — interim maintenance
 * that flows from the date of the application until the final
 * disposal of the matrimonial proceedings.
 *
 * THE LATIN PHRASE "PENDENTE LITE":
 *
 *   The expression "pendente lite" is Latin for "during the
 *   pendency of the litigation." It appears throughout Indian
 *   civil procedure, not just in matrimonial cases. The phrase
 *   captures the essence of an interim remedy: it is relief that
 *   exists only while the main case is pending and that ends when
 *   the main case is decided. Other examples of pendente lite
 *   relief include the lis pendens doctrine in property law, which
 *   prevents a defendant from selling disputed property during a
 *   suit, and pendente lite injunctions, which freeze the status
 *   quo until the case is heard.
 *
 * SECTION 24 IN DETAIL:
 *
 *   Section 24 of the Hindu Marriage Act provides that where, in
 *   any proceeding under the Act, it appears to the court that
 *   either the wife or the husband, as the case may be, has no
 *   independent income sufficient for her or his support and the
 *   necessary expenses of the proceeding, the court may, on the
 *   application of either, order the respondent to pay to the
 *   petitioner the expenses of the proceeding and a monthly sum
 *   during the proceeding as may seem reasonable. The Act was
 *   amended in 2003 to add a sixty-day timeline within which the
 *   court should ordinarily dispose of the application, although
 *   in practice this timeline is rarely met.
 *
 *   Notice that the section is gender-neutral. Either the husband
 *   or the wife can claim Section 24 maintenance from the other,
 *   and the inquiry is not about marital fault but about
 *   comparative financial capacity. In practice, however, almost
 *   all Section 24 applications are filed by wives against
 *   husbands because the historical pattern of marital economics
 *   in India makes wives the dependent party in most cases.
 *
 * SECTION 24 HMA vs SECTION 144 BNSS:
 *
 *   Your library now contains two different maintenance remedies
 *   for matrimonial situations, and the contrast between them is
 *   worth understanding precisely. Template 24 is the application
 *   for maintenance under Section 144 of the Bharatiya Nagarik
 *   Suraksha Sanhita, and Template 43 is this application under
 *   Section 24 of the Hindu Marriage Act. They differ in several
 *   important ways.
 *
 *   The Section 144 BNSS application is a STANDALONE proceeding.
 *   The wife who has been deserted by her husband can file a
 *   maintenance application even if no other proceeding is
 *   pending between them. The Section 24 HMA application, by
 *   contrast, is an INTERLOCUTORY application within an existing
 *   matrimonial proceeding. It can only be filed if a divorce,
 *   judicial separation, restitution of conjugal rights, or
 *   other Hindu Marriage Act petition is already on the record.
 *   If no such proceeding is pending, Section 24 simply does not
 *   apply.
 *
 *   The Section 144 BNSS application is filed before a Magistrate
 *   in the Family Court, and the proceeding is quasi-criminal in
 *   character. The Section 24 HMA application is filed within an
 *   existing matrimonial petition before the Principal Judge of
 *   the Family Court, and the proceeding is purely civil in
 *   character.
 *
 *   The Section 144 BNSS application provides ONLY maintenance.
 *   The Section 24 HMA application provides BOTH the expenses of
 *   the matrimonial proceeding AND a monthly sum for support.
 *   This dual coverage is significant because legal expenses in
 *   contested matrimonial cases can be substantial, and the
 *   dependent spouse needs help paying her own lawyer.
 *
 *   The Section 144 BNSS application is religion-neutral and
 *   applies to everyone regardless of faith. The Section 24 HMA
 *   application is available only to Hindus, Buddhists, Jains and
 *   Sikhs, who are the parties to whom the Hindu Marriage Act
 *   applies. There are equivalent provisions in the personal laws
 *   of other communities, such as Section 36 of the Special
 *   Marriage Act and Section 36 of the Indian Divorce Act for
 *   Christians, but these have their own separate templates.
 *
 *   Many practitioners advise their clients to file applications
 *   under both Section 144 BNSS and Section 24 HMA simultaneously,
 *   so that the dependent spouse has the maximum protection
 *   available. The two amounts can be set off against each other,
 *   so the husband does not pay double, but having both
 *   applications gives the wife greater bargaining power.
 *
 * THE DERIVATIVE CASE NUMBER FORMAT:
 *
 *   Look at how the case number is structured in this template.
 *   It says "IA No. ___ of 20__ IN HMA Petition No. ___ of 20__."
 *   The "IN" word again indicates that the application is filed
 *   within an existing parent case. You have seen this same
 *   pattern in the Temporary Injunction IA at Template 22, the
 *   Contempt Petition at Template 28, and the Execution
 *   Application at Template 36. Whenever you see two case numbers
 *   connected by "IN," you are looking at a derivative proceeding.
 *
 * THE BREVITY CONVENTION:
 *
 *   This application uses the same brevity convention you saw in
 *   the Temporary Injunction IA at Template 22 and the Indigent
 *   Person Application at Template 33. The substantive facts of
 *   the matrimonial dispute are already in the main petition, so
 *   the application incorporates them by reference rather than
 *   repeating them. The application then focuses entirely on the
 *   financial position of the parties: the applicant's lack of
 *   income, the respondent's earnings and assets, and the amount
 *   of maintenance that would be reasonable.
 *
 * THE CRUCIAL FINANCIAL DISCLOSURES:
 *
 *   The substance of a Section 24 application lies in two
 *   contrasting financial pictures: the applicant's poverty and
 *   the respondent's affluence. Both must be pleaded with as much
 *   specificity as possible. The applicant must say what her
 *   monthly expenses are (rent, food, medical care, school fees
 *   for children, transportation), and the respondent's income
 *   must be alleged with reference to known facts (his
 *   employment, his salary, his other businesses, his property
 *   ownership). Vague pleadings on either side will weaken the
 *   application; specific and substantiated pleadings will
 *   strengthen it.
 */

