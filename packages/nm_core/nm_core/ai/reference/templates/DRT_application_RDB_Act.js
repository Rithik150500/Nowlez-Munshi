/**
 * ORIGINAL APPLICATION BEFORE THE DEBT RECOVERY TRIBUNAL
 * UNDER SECTION 19 OF THE RECOVERY OF DEBTS AND BANKRUPTCY
 * ACT, 1993 (FORMERLY THE RECOVERY OF DEBTS DUE TO BANKS
 * AND FINANCIAL INSTITUTIONS ACT, 1993)
 * ──────────────────────────────────────────────────────────
 * Category : Banking Law — Specialised Tribunal Recovery
 * Tribunal : Debt Recovery Tribunal (DRT)
 * Statute  : Section 19, Recovery of Debts and Bankruptcy
 *            Act, 1993 ('RDB Act')
 * Source   : Standard form prescribed under the DRT Rules
 *
 * The Original Application before the Debt Recovery Tribunal is
 * the fifth and final template in the debt recovery and
 * insolvency batch, and it introduces the third pillar of the
 * Indian debt recovery framework. To grasp where the DRT route
 * fits in relation to the IBC route covered by Templates 61 to
 * 63 and the SARFAESI route covered by Template 64, you have to
 * understand the historical evolution of debt recovery in India
 * and the specific niche that the RDB Act was designed to fill.
 *
 * THE HISTORICAL EVOLUTION:
 *
 *   The RDB Act, 1993 was the first major reform in Indian
 *   debt recovery law. Before its enactment, banks had to
 *   recover their dues through ordinary civil suits in
 *   ordinary civil courts, which were notoriously slow and
 *   often took ten or fifteen years to produce a final
 *   decree. The 1991 Narasimham Committee on the Financial
 *   System recommended the establishment of a specialised
 *   tribunal for the speedy adjudication of bank recovery
 *   claims, and the RDB Act was enacted in 1993 to give
 *   effect to this recommendation. The Act established the
 *   Debt Recovery Tribunal (DRT) at the first level and the
 *   Debt Recovery Appellate Tribunal (DRAT) at the appellate
 *   level, both staffed by specialised members and bound by
 *   simplified procedures designed to produce faster
 *   adjudication.
 *
 *   The RDB Act was reasonably effective for its time, but as
 *   the volume of bank non-performing assets grew during the
 *   late 1990s and early 2000s, the DRTs themselves became
 *   overwhelmed and proceedings began to take many years
 *   despite the simplified procedures. This led to the
 *   enactment of the SARFAESI Act in 2002, which gave banks
 *   the option to enforce their security interests directly
 *   without any tribunal proceeding (Template 64). And
 *   finally, the IBC was enacted in 2016 to provide a
 *   comprehensive insolvency framework that goes beyond
 *   simple debt recovery to address the resolution of
 *   distressed corporate debtors as a whole (Templates 61
 *   to 63).
 *
 *   In 2016, the RDB Act itself was amended and renamed as
 *   the Recovery of Debts and Bankruptcy Act, 1993 (with
 *   "Bankruptcy" being added to the title). The amendment
 *   clarified the relationship between the DRT and the IBC
 *   and reinforced the DRT's continuing role in individual
 *   insolvency matters and in cases that fall outside the
 *   scope of the IBC.
 *
 * WHEN IS THE DRT ROUTE USED?
 *
 *   With three different debt recovery mechanisms now
 *   available, you might wonder why the DRT route is used at
 *   all. The answer is that each of the three mechanisms has
 *   its own specific niche, and the DRT route is the right
 *   choice in several specific scenarios.
 *
 *   First, the DRT route is used for UNSECURED debts. The
 *   SARFAESI Act applies only to secured debts, so a bank
 *   that has lent money on an unsecured basis cannot use
 *   SARFAESI for recovery. The IBC route is theoretically
 *   available but only if the threshold of Rs. 1 crore is
 *   met and only if the debtor is a corporate person. For
 *   unsecured loans below the IBC threshold, or unsecured
 *   loans to non-corporate debtors, the DRT route may be the
 *   only effective option.
 *
 *   Second, the DRT route is used for debts where the SARFAESI
 *   enforcement has been insufficient. After taking possession
 *   of and selling the secured asset under SARFAESI, the bank
 *   may still be left with an unrecovered balance. The DRT
 *   route is then used to recover this balance from the
 *   personal assets of the borrower or guarantor.
 *
 *   Third, the DRT route is used when the bank prefers to
 *   obtain a money decree against the borrower rather than
 *   initiate insolvency proceedings. The IBC is a powerful
 *   tool but it is also a blunt one, because it triggers a
 *   process that affects the entire corporate debtor and all
 *   its creditors. A bank that simply wants to recover its
 *   own dues without dragging the rest of the company into
 *   insolvency may prefer the targeted DRT route.
 *
 *   Fourth, the DRT has jurisdiction over individual debtors
 *   that the IBC does not yet fully cover. The IBC contains
 *   provisions for individual insolvency at Sections 78 to
 *   187, but these provisions have not been notified for all
 *   classes of individuals. For individual debtors who are
 *   not covered by the notified provisions of the IBC, the
 *   DRT route remains the principal mechanism for recovery.
 *
 * THE PECUNIARY THRESHOLD:
 *
 *   The DRT has jurisdiction over debt recovery applications
 *   where the amount in default is Rs. 20 lakh or more. Below
 *   this threshold, banks must approach ordinary civil courts
 *   for recovery. The Rs. 20 lakh threshold is much lower
 *   than the IBC threshold of Rs. 1 crore, which means that
 *   the DRT route is the principal recovery mechanism for
 *   bank debts in the range of Rs. 20 lakh to Rs. 1 crore
 *   where SARFAESI is not available.
 *
 * THE SIMPLIFIED PROCEDURE:
 *
 *   One of the most important features of the DRT route is
 *   the simplified procedure prescribed by the RDB Act and
 *   the Debt Recovery Tribunal (Procedure) Rules, 1993. The
 *   DRT is not bound by the elaborate provisions of the Code
 *   of Civil Procedure, 1908, except in respect of certain
 *   specified matters. Instead, the Tribunal is required to
 *   follow the principles of natural justice and to dispose
 *   of applications expeditiously. The proceedings are
 *   intended to be much faster than ordinary civil
 *   litigation, although in practice the case load of most
 *   DRTs has grown to a point where the proceedings are not
 *   significantly faster than ordinary court litigation.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at the case header. The application is
 *   captioned as "OA" — Original Application — which is the
 *   standard caption for applications before the DRT. This
 *   distinguishes it from "RA" (Recovery Application) which
 *   is the term used for applications to enforce certificates
 *   of recovery, and from "MA" (Miscellaneous Application)
 *   which is the term used for interlocutory applications.
 *
 *   Second, look at the parties. The applicant is described
 *   as the bank, and the respondents include the borrower,
 *   the guarantors and any other persons against whom recovery
 *   is sought. Like a regular civil suit, the DRT
 *   application can be filed against multiple defendants who
 *   are jointly and severally liable for the debt.
 *
 *   Third, look at the body. The structure is similar to a
 *   civil plaint with paragraphs setting out the parties, the
 *   loan facility, the security, the default, the demand for
 *   payment, and the relief sought. But the prayer is
 *   distinctive because the DRT can grant a "certificate of
 *   recovery" rather than just a money decree, and the
 *   certificate can be enforced by the Recovery Officer
 *   attached to the DRT through a streamlined execution
 *   procedure.
 */

