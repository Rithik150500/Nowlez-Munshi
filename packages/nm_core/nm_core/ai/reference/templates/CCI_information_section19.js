/**
 * INFORMATION TO THE COMPETITION COMMISSION OF INDIA
 * UNDER SECTION 19(1)(a) OF THE COMPETITION ACT, 2002
 * ─────────────────────────────────────────────────────
 * Category : Competition Law — Regulatory Complaint
 * Forum    : Competition Commission of India (CCI)
 * Statute  : Section 19(1)(a), Competition Act, 2002;
 *            Sections 3 and 4, Competition Act, 2002
 * Form     : Form 1 of the Competition Commission of India
 *            (General) Regulations, 2009
 * Source   : Standard form used in Indian competition practice
 *
 * The Information to the Competition Commission of India under
 * Section 19(1)(a) of the Competition Act, 2002 is the fourth
 * template in the specialised tribunals batch and it introduces
 * an entirely separate area of Indian commercial law that none
 * of your existing templates have covered. To grasp where this
 * template fits, you have to understand what competition law is,
 * what it tries to achieve, and how the Competition Commission
 * of India operates.
 *
 * THE FUNCTION OF COMPETITION LAW:
 *
 *   Competition law (sometimes called antitrust law in
 *   American usage) is the body of law that aims to prevent
 *   anti-competitive practices in the marketplace and to
 *   protect the interests of consumers by promoting and
 *   sustaining competition. The basic idea is that
 *   competition between businesses produces benefits for
 *   consumers in the form of lower prices, better quality,
 *   greater variety, and more innovation, while the absence
 *   of competition allows businesses to charge higher
 *   prices, produce lower quality goods, offer less variety,
 *   and innovate less. Competition law tries to prevent
 *   businesses from engaging in practices that would
 *   undermine competition and thereby harm consumers.
 *
 *   In India, the principal competition statute is the
 *   Competition Act, 2002, which was enacted to replace the
 *   older Monopolies and Restrictive Trade Practices Act,
 *   1969 (the MRTP Act). The Competition Act introduced a
 *   modern competition law regime that focuses on harm to
 *   competition rather than on the size of businesses, and
 *   it is in many respects modelled on the European Union's
 *   competition law and on the antitrust laws of the United
 *   States.
 *
 * THE THREE PILLARS OF THE COMPETITION ACT:
 *
 *   The Competition Act, 2002 rests on three substantive
 *   pillars that you should learn to identify.
 *
 *   The first pillar is the prohibition of ANTI-COMPETITIVE
 *   AGREEMENTS under Section 3 of the Act. Section 3(1)
 *   prohibits any agreement in respect of production,
 *   supply, distribution, storage, acquisition or control
 *   of goods or services which causes or is likely to
 *   cause an appreciable adverse effect on competition in
 *   India. Section 3(3) creates a presumption of
 *   anti-competitive effect in respect of certain horizontal
 *   agreements between competitors, including price-fixing
 *   agreements, output-restricting agreements, market-
 *   sharing agreements, and bid-rigging agreements (the
 *   so-called "hard core cartel" agreements). Section 3(4)
 *   addresses vertical agreements between businesses at
 *   different levels of the production or distribution
 *   chain, such as tie-in arrangements, exclusive supply
 *   agreements, exclusive distribution agreements, refusal
 *   to deal, and resale price maintenance.
 *
 *   The second pillar is the prohibition of ABUSE OF
 *   DOMINANT POSITION under Section 4 of the Act. Section
 *   4(1) prohibits any enterprise or group from abusing its
 *   dominant position, where dominant position is defined as
 *   a position of strength enjoyed by an enterprise that
 *   enables it to operate independently of competitive
 *   forces or to affect competitors or consumers in its
 *   favour. Section 4(2) lists specific kinds of abuse,
 *   including imposing unfair or discriminatory conditions
 *   in the purchase or sale of goods or services, imposing
 *   unfair or discriminatory prices, limiting or restricting
 *   production or technical development, denying market
 *   access in any manner, and using a dominant position in
 *   one market to enter into or protect another market.
 *
 *   The third pillar is the regulation of COMBINATIONS
 *   (mergers and acquisitions) under Sections 5 and 6 of
 *   the Act. The CCI must approve any combination that
 *   meets certain financial thresholds before it can be
 *   consummated, and it can approve, modify, or block the
 *   combination depending on its likely effect on
 *   competition.
 *
 *   This template focuses on the first two pillars
 *   (anti-competitive agreements and abuse of dominant
 *   position), which are the subjects of complaints filed
 *   by aggrieved parties. The combination regime is
 *   prospective rather than complaint-based and has its own
 *   distinctive procedural framework.
 *
 * THE COMPETITION COMMISSION OF INDIA:
 *
 *   The Competition Commission of India was established
 *   under Section 7 of the Competition Act, 2002 and is the
 *   principal regulator under the Act. The CCI consists of
 *   a chairperson and not less than two and not more than
 *   six other members appointed by the Central Government.
 *   The chairperson and members are required to have
 *   experience in international trade, economics, business,
 *   commerce, law, finance, accountancy, management,
 *   industry, public affairs, or competition matters.
 *
 *   The CCI has wide powers under the Act, including the
 *   power to investigate alleged contraventions of Sections
 *   3 and 4, the power to impose penalties on contraveners,
 *   the power to issue cease and desist orders, the power
 *   to direct the modification of agreements, the power to
 *   pass interim orders during the pendency of investigations,
 *   and the power to order the dissolution of cartels. The
 *   maximum penalty that the CCI can impose is up to ten
 *   percent of the average turnover of the contravening
 *   enterprise for the preceding three financial years, or
 *   in the case of a cartel, up to three times the profit
 *   for each year of the continuance of such agreement or
 *   ten percent of the turnover for each year of the
 *   continuance of such agreement, whichever is higher.
 *
 * THE INFORMATION MECHANISM:
 *
 *   Section 19(1) of the Competition Act provides three
 *   ways in which an investigation by the CCI can be
 *   initiated. First, on the receipt of an "information"
 *   from any person, consumer, or association of consumers,
 *   under Section 19(1)(a). This is the mechanism that
 *   private parties typically use, and it is the subject of
 *   this template. Second, on a reference made by the
 *   Central Government, a State Government, or a statutory
 *   authority, under Section 19(1)(b). Third, on the CCI's
 *   own knowledge, under the suo motu power.
 *
 *   The "information" mechanism is distinctive because it
 *   does not require the informant to have any particular
 *   standing or any direct legal injury. Like the standing
 *   provisions in PIL (Template 66) and trademark
 *   opposition (Template 71), the Section 19(1)(a)
 *   information mechanism allows "any person" to bring
 *   information to the CCI's attention. This wide standing
 *   provision is meant to encourage the reporting of
 *   anti-competitive conduct and to ensure that the CCI
 *   has access to information that it would not otherwise
 *   be able to obtain.
 *
 *   The information is filed in Form 1 prescribed under the
 *   CCI (General) Regulations, 2009, and it must be
 *   accompanied by a statement of facts, the relief sought,
 *   and supporting documents. The CCI examines the
 *   information at a preliminary stage and decides whether
 *   to direct the Director General to conduct an
 *   investigation into the matter. The Director General is
 *   the investigative arm of the CCI and has wide powers
 *   to gather evidence, examine witnesses, and produce a
 *   report on whether there has been a contravention of
 *   the Act.
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at the form. The information is filed in
 *   Form 1 of the CCI Regulations, which has specific
 *   blanks for the informant's details, the opposite
 *   parties, the alleged contravention, and the relief
 *   sought.
 *
 *   Second, look at the body. Paragraphs 1 and 2 establish
 *   the parties. Paragraphs 3 and 4 describe the relevant
 *   market and the position of the opposite parties in
 *   that market. Paragraph 5 sets out the alleged
 *   anti-competitive conduct. Paragraphs 6 onwards explain
 *   why the conduct constitutes a contravention of Sections
 *   3 or 4 of the Act.
 *
 *   Third, look at the relief sought. The relief asks the
 *   CCI to investigate the matter, to find that there has
 *   been a contravention, to impose appropriate penalties
 *   on the contraveners, and to issue cease and desist
 *   orders.
 */

