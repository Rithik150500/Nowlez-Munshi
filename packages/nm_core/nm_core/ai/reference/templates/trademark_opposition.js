/**
 * NOTICE OF OPPOSITION TO REGISTRATION OF TRADE MARK
 * UNDER SECTION 21 OF THE TRADE MARKS ACT, 1999
 * ────────────────────────────────────────────────────
 * Category : Intellectual Property — Pre-registration Challenge
 * Forum    : Registrar of Trade Marks
 * Statute  : Section 21, Trade Marks Act, 1999
 * Form     : Form TM-O prescribed under the Trade Marks Rules, 2017
 * Source   : Standard form used in Indian trademark practice
 *
 * The Notice of Opposition under Section 21 of the Trade Marks
 * Act, 1999 is the first intellectual property template in your
 * library and it introduces an entirely new procedural regime
 * that operates outside the ordinary civil courts. To grasp where
 * the trademark opposition fits in the broader picture of Indian
 * IP practice, you have to understand the basic life cycle of a
 * trademark registration in India and the points at which third
 * parties can intervene to challenge a proposed registration.
 *
 * THE LIFE CYCLE OF A TRADEMARK REGISTRATION:
 *
 *   The registration of a trademark in India follows a sequence
 *   of steps that you should understand before drafting any
 *   trademark document. The first step is the filing of an
 *   application for registration by the applicant, who claims
 *   either prior use of the mark or an intention to use it in
 *   the future. The application is filed before the Registrar
 *   of Trade Marks at one of the five regional offices located
 *   in Delhi, Mumbai, Kolkata, Chennai, or Ahmedabad. The
 *   second step is the examination of the application by an
 *   examiner of trademarks, who checks whether the proposed
 *   mark satisfies the absolute and relative grounds for
 *   refusal under Sections 9 and 11 of the Act. If the
 *   examiner has any objections, an examination report is
 *   issued and the applicant is given an opportunity to
 *   respond. The third step, if the application is accepted
 *   by the examiner, is the publication of the application in
 *   the Trade Marks Journal, which is a periodic publication
 *   issued by the Trade Marks Registry. The publication is
 *   meant to give notice to the world that the applicant
 *   intends to register the mark, and it triggers the
 *   four-month opposition period during which any aggrieved
 *   person can file an opposition. The fourth step, if no
 *   opposition is filed (or if any opposition is rejected),
 *   is the registration of the mark and the issuance of the
 *   registration certificate. The fifth step is the renewal
 *   of the registration every ten years to keep it in force.
 *
 *   The trademark opposition fits at the third step of this
 *   sequence, after publication but before registration. It
 *   is the principal mechanism by which third parties can
 *   prevent the registration of marks that they consider
 *   harmful to their own interests.
 *
 * WHO CAN FILE AN OPPOSITION:
 *
 *   Section 21(1) of the Act provides that "any person" can
 *   file a notice of opposition. This is a remarkably wide
 *   formulation because it does not require the opponent to
 *   show any particular standing or any direct interest in
 *   the matter. In theory, anyone in the world can oppose
 *   the registration of any mark in India, and the courts
 *   have interpreted this provision liberally to allow
 *   competitors, consumers, trade associations, and even
 *   ordinary citizens to file oppositions. In practice,
 *   however, most oppositions are filed by competitors who
 *   own similar marks and who fear that the proposed
 *   registration would create confusion in the marketplace
 *   or dilute the value of their existing marks.
 *
 * THE GROUNDS FOR OPPOSITION:
 *
 *   The grounds on which a trademark application can be
 *   opposed are set out in Sections 9 and 11 of the Act, and
 *   they fall into two broad categories that you should learn
 *   to identify.
 *
 *   The first category is the ABSOLUTE grounds for refusal
 *   under Section 9. These are grounds that go to the
 *   inherent registrability of the mark itself, regardless
 *   of any third-party rights. The absolute grounds include:
 *   that the mark is devoid of any distinctive character;
 *   that the mark consists exclusively of descriptive
 *   indications such as the kind, quality, quantity,
 *   intended purpose, or geographical origin of the goods or
 *   services; that the mark consists exclusively of marks or
 *   indications which have become customary in the current
 *   language or in the bona fide and established practices of
 *   the trade; that the mark is of such a nature as to
 *   deceive the public or cause confusion; that the mark
 *   contains scandalous or obscene matter; or that the mark
 *   is prohibited under the Emblems and Names (Prevention of
 *   Improper Use) Act, 1950.
 *
 *   The second category is the RELATIVE grounds for refusal
 *   under Section 11. These are grounds that depend on the
 *   existence of earlier rights held by third parties. The
 *   relative grounds include: that the mark is identical
 *   with or similar to an earlier registered mark and is to
 *   be used in respect of similar goods or services, where
 *   there is a likelihood of confusion on the part of the
 *   public; that the mark is identical or similar to an
 *   earlier well-known mark and the use of the proposed
 *   mark would take unfair advantage of or be detrimental to
 *   the distinctive character or repute of the earlier mark;
 *   and that the mark is liable to be prevented under the
 *   law of passing off or by virtue of any other earlier
 *   right.
 *
 *   A well-drafted opposition should identify the specific
 *   grounds being relied upon and should explain how the
 *   proposed mark falls foul of those grounds. The opponent
 *   should also produce evidence to support the grounds,
 *   particularly evidence of prior use, prior registration,
 *   reputation, and the likelihood of confusion.
 *
 * THE FOUR-MONTH OPPOSITION PERIOD:
 *
 *   Section 21(1) of the Act prescribes a strict four-month
 *   period within which the notice of opposition must be
 *   filed. The four-month period runs from the date of
 *   advertisement of the application in the Trade Marks
 *   Journal. This is one of the shortest limitation periods
 *   in Indian IP practice and it requires constant vigilance
 *   on the part of brand owners to monitor the Trade Marks
 *   Journal and identify potentially conflicting applications
 *   in time to oppose them. Many brand owners engage
 *   specialised trademark watch services that monitor the
 *   Journal on their behalf and alert them to any
 *   applications that resemble their marks.
 *
 *   Unlike the limitation period under the Section 34 of the
 *   Arbitration Act that you saw at Template 52, the
 *   four-month opposition period is not extendable. If the
 *   opponent fails to file the opposition within the
 *   four-month period, the right to oppose is lost, and the
 *   only remaining remedy is to seek the cancellation of the
 *   registered mark after registration through a separate
 *   proceeding under Section 47 or Section 57 of the Act.
 *
 * THE OPPOSITION PROCEEDING:
 *
 *   Once the notice of opposition is filed, the proceeding
 *   takes on a quasi-judicial character. The Registrar
 *   serves a copy of the notice on the applicant, who must
 *   file a counter-statement within two months. The opponent
 *   then files its evidence in support of the opposition,
 *   followed by the applicant's evidence in support of the
 *   application. After the evidence is complete, the
 *   Registrar fixes a date for hearing and decides the
 *   opposition on the basis of the evidence and the
 *   arguments advanced by both sides. The Registrar's
 *   decision can be appealed to the High Court (after the
 *   abolition of the Intellectual Property Appellate Board
 *   in 2021).
 *
 * STRUCTURAL FEATURES OF THE TEMPLATE:
 *
 *   First, look at the form. The Notice of Opposition is
 *   filed in Form TM-O prescribed under Rule 42 of the
 *   Trade Marks Rules, 2017. The form has specific blanks
 *   for the application number being opposed, the grounds of
 *   opposition, and the address for service.
 *
 *   Second, look at how the parties are described. The
 *   opponent is described with full credentials including
 *   their own trademark registrations and prior use, because
 *   establishing the opponent's standing and rights is the
 *   foundation of any successful opposition.
 *
 *   Third, look at the grounds section. The grounds are
 *   organised under separate headings for absolute grounds
 *   under Section 9 and relative grounds under Section 11,
 *   so that the Registrar can address each ground separately.
 *
 *   Fourth, look at the prayer. It asks the Registrar to
 *   refuse the registration of the impugned mark and to
 *   award costs of the opposition.
 */

